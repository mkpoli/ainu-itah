		&lsquo;pulling animal&rsquo;, and was in turn passed on into Japanese as
		<i lang="ja">tonakai</i> — Ainu here standing as the relay between Nivkh and Japanese,
		the mirror image of its receiving role elsewhere in the chapter
		<Ref k="janhunen2022" />, <Ref k="austerlitz1976" paren />.
	</p>
