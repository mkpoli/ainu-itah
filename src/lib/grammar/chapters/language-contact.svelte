	<p>
		The word <i lang="ain">nuca</i> &lsquo;Russian&rsquo; deserves a note because it is so
		often misattributed. It is <em>not</em> a borrowing from Russian; it comes from
		Orok/Uilta <i lang="ain">luča</i>, with the initial <i lang="ain">l</i> resolved to
		<i lang="ain">n</i> by the Ainu avoidance of liquid onsets (§25.5), so that the name
		the Ainu used for the Russians reached them through their Tungusic neighbours rather
		than from the Russians themselves <Ref k="chiri1942" p="§17(2)" />. It is one of the
		highest-frequency loans in the corpus: every one of Sentoku's three letters turns on
		the comings and goings of the <i lang="ain">nuca</i> (see (1)). The zither name
		<i lang="ain">tonkori</i> is a second cautionary case — the instrument the Nivkh call
		<i lang="ain">tɨŋrɨŋ</i> is organologically quite unlike the Ainu
		<i lang="ain">tonkori</i>, so the <em>name</em> spread along the trade route even
		though the <em>thing</em> did not, and the two should not be equated
		<Ref k="shiraishi2022" paren />. Finally, the west-coast mitten word
		<A w="wampakka" gl="mittens" /> is a genuine direct Uilta loan
		(Uilta <i lang="ain">wambakka</i>), but a strictly local one: it is a west-coast
		isogloss against east-coast native <A w="matumere" />, treated under the dialects
		(<Xr ch="dialects-and-sources" />).
	</p>
</S>

<S t="The Nivkh layer" id="nivkh">
	<p>
		The contact layer most likely to be under-represented in earlier grammars is the
		Nivkh one. Where the present description's predecessors cite barely a word or two,
		Shiraishi and Tangiku's appendix lists around twenty-nine items shared between Nivkh
		and Ainu, concentrated in the technologies of the central island — dog-sledding,
		sea-hunting, containers, and the fauna and tackle of the coast
		<Ref k="shiraishi2022" paren />. The geography is telling: the Nivkh dialect richest
		in shared vocabulary is that of Poronaisk, in the central contact zone where Ainu,
		Nivkh and Uilta territories met, which marks the borrowing as local rather than
		island-wide.
