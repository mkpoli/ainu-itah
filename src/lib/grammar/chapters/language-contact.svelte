	<p>
		Spoken code-switching into Japanese is also recorded, increasingly so in the latest
		generation of speakers. In Asai Take's post-war west-coast recordings, Japanese asides
		and discourse words are dropped into an otherwise Ainu grammatical frame — the
		narrator breaks into Japanese for an evaluative comment and returns to Ainu for the
		event line <Ref k="murasaki2001" paren />. We treat such material as switching rather
		than borrowing, since the inserted Japanese keeps its own phonology and is not
		integrated into Ainu morphology (§25.6).
	</p>
</S>

<S t="The Manchu–Amur trade stratum" id="trade">
	<p>
		For two centuries before the Russian and Japanese states arrived in force, Sakhalin
		hung off the northern end of the Manchu tributary-trade system that ran up the Amur:
		marten and sable pelts moved south, and silk, metalware, tea and spirits moved north,
		passing through Uilta and Nivkh hands before they reached the Ainu. The vocabulary of
		this trade is the clearest case in the language of borrowing <em>by relay</em> rather
		than face to face. Shiraishi and Tangiku's count is blunt about it: of the loanwords
		shared across the island's three languages, Nivkh and Uilta share by far the most
		(61 items), Nivkh and Ainu rather fewer (29), and Ainu and Uilta fewest of all
		(some 13) — a distribution that matches the historical record, in which the Ainu
		generally did not understand Uilta and communication ran one-way from the Uilta who
		knew Ainu <Ref k="shiraishi2022" paren />. Direct Tungusic-to-Ainu borrowing is
		therefore thin; most Tungusic-looking words in Ainu arrived through the network, not
		from the Uilta next door.
	</p>
	<p>
		The structurally most important members of this stratum are the borrowed
		counting-bases <A w="tanku" gl="hundred" /> &lsquo;hundred&rsquo; and
		<i lang="ain">wantanku</i> &lsquo;thousand&rsquo;, which match Manchu-Tungusic
		<i lang="ja">tanggū</i> and underpin the productive decimal numeral system that
		Sakhalin Ainu developed for trade arithmetic; Piłsudski was told outright that
		<i lang="ain">tanku</i> came &lsquo;from the Olcha&rsquo; with the marten trade
		<Ref k="pilsudski1912" p="141" />. Because they are already treated at length under
		the numerals, they are only cross-referenced here (<Xr ch="numerals" />). The
		remaining trade words cluster in the expected domains — drink, ritual, status, and the
		exotic goods themselves.
	</p>
