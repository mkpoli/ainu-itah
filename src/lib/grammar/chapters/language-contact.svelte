<S t="Russian and the Cyrillic register" id="russian">
	<p>
		Russian left Sakhalin Ainu almost no integrated lexicon. Such direct loans as are
		claimed are few and uncertain — <i lang="ain">kosika</i> &lsquo;cat&rsquo;
		(&lt; <i lang="ru">koshka</i>) is the stock example, but it is poorly attested and we
		flag it as unverified. The Russian contribution to the record is of a different order:
		it is a <em>register</em> and a <em>script</em>, not a word-list. Two things stand out.
	</p>
	<p>
		The first is the Cyrillic writing tradition. Bronisław Piłsudski ran a short-lived
		east-coast school at which Ainu was written in the Russian alphabet, and the most
		substantial surviving body of Ainu prose by a native hand is the set of three letters
		that Sentoku Tarōji wrote him in Cyrillic in 1906, edited by Tangiku and Ogihara
		<Ref k="tangiku2001" paren />. The orthographic side of this — how Cyrillic was made
		to register Ainu, and what it could not register (vowel length above all) — belongs to
		the orthography chapter (<Xr ch="orthography" s="cyrillic" />), and the letters are
		glossed continuously in the texts chapter (<Xr ch="texts" s="sentoku-letter" />).
	</p>
	<p>
		The second is the epistolary register the letters establish: an Ainu opening formula
		<i lang="ain">ituyaskara nispa</i> &lsquo;beloved master&rsquo; over a frame studded
		with Russian apparatus — calendar dates and place phrases (<i lang="ain">4go
		Fevral'</i> &lsquo;4th of February&rsquo;, dating clauses on the Russian model),
		ruble-and-kopeck sums, and Russian personal and place names adapted to Ainu phonology:
		Korsakov, Aleksandr → <i lang="ain">Ariksandoru</i>, <i lang="ru">lesnichiy</i>
		&lsquo;forester&rsquo; → <i lang="ain">Lesneciy</i>. These are best read not as loans
		into the language but as the written furniture of a bilingual letter-writer; several
		further items that appear in the letters — <i lang="ain">atarimay</i>,
		<i lang="ain">arenda</i> &lsquo;lease&rsquo;, <i lang="ain">starosta</i>
		&lsquo;village elder&rsquo;, <i lang="ain">menco</i>, <i lang="ain">comen</i> — we
		treat as unintegrated code-switches into Russian (or, for <i lang="ain">atarimay</i>,
		Japanese) rather than established borrowings, and mark them accordingly. The point of
		substance is that Russian influence on Sakhalin Ainu is concentrated in this written,
		formulaic, name-and-number register, and barely touches the core lexicon at all.
	</p>
</S>

<S t="Phonological adaptation of loans" id="adaptation">
	<p>
		Loanwords are reshaped to Ainu phonotactics by a small set of regular operations,
		most of them already visible in the tables above. They are synthesised here; the
		native phonological constraints they answer to are set out in
		<Xr ch="phonology" />, and only the contact-specific patterns are drawn together.
	</p>
	<p>
		<strong>Liquid-onset avoidance.</strong> Ainu has no syllable-initial
		<i lang="ain">l</i>, and a foreign initial liquid is repaired. The diagnostic case is
		<i lang="ain">nuca</i> &lsquo;Russian&rsquo; &lt; Uilta <i lang="ain">luča</i>, where
		<i lang="ain">l</i> becomes <i lang="ain">n</i> <Ref k="chiri1942" p="§17(2)" />.
		<strong>Accent → length.</strong> The pitch accent of a Japanese source is rendered as
		Sakhalin vowel length, the systematic correspondence behind <i lang="ain">kaani</i>,
		<i lang="ain">tuuki</i> against Hokkaidō short-vowel <i lang="ain">kani</i>,
		<i lang="ain">tuki</i> <Ref k="hattori1964" p="117, 261" />.
		<strong>Voicing neutralisation.</strong> Ainu lacks a voicing contrast in stops, so
		voiced onsets of Japanese loans surface voiceless: <i lang="ja">beko</i> →
		<i lang="ain">peko</i> &lsquo;cow&rsquo;, <i lang="ja">buta</i> →
		<i lang="ain">puta</i> &lsquo;pig&rsquo;.
	</p>
	<p>
		<strong>Geminate and cluster repair.</strong> A foreign geminate or illicit cluster is
		resolved into a permitted coda-plus-onset sequence, the coda surfacing as
		<i lang="ain">h</i> by the regular coda neutralisation of the native phonology: thus
		Japanese <i lang="ja">teppō</i> → <i lang="ain">tehpo</i> &lsquo;gun&rsquo;, with
		<i lang="ain">hp</i> for <i lang="ja">pp</i> <Ref k="hattori1964" p="118" />. Two of
		Chiri's showcase adaptations carry the repair further, through whole-segment loss and
		metathesis. The headwear word <i lang="ain">atampusa</i> &lsquo;a kind of hood&rsquo;
		comes from Japanese <i lang="ja">watabōshi</i> 綿帽子 by way of an intermediate
		<i lang="ain">matampusi</i>, then loss of the initial <i lang="ain">m</i>
		<Ref k="chiri1942" p="§17(2); §19" />; and the lamp word, Hokkaidō
		<i lang="ain">ratcaku</i> &lt; Chinese 蠟燭 (via Japanese), appears in Sakhalin as
		<i lang="ain">cahraku</i> &lsquo;light, lamp&rsquo; by metathesis of the initial
		<i lang="ain">r</i> <Ref k="chiri1942" p="§20" />.
	</p>
	<p>
		<strong>The Nivkh <i lang="ain">t</i> ↔ <i lang="ain">r</i> correspondence.</strong>
		Across the Nivkh layer a Nivkh syllable-final <i lang="ain">t</i> answers to an Ainu
		<i lang="ain">r</i>: <i lang="ain">mut</i> → <i lang="ain">muhru</i>
		&lsquo;pillow&rsquo;, <i lang="ain">maχt</i> → <i lang="ain">mahru</i>
		&lsquo;towline spreader&rsquo;, <i lang="ain">motas</i> → <i lang="ain">mohrasi</i>
		&lsquo;sled runner&rsquo; <Ref k="shiraishi2022" paren />. The correspondence is
		regular enough to serve as a diagnostic of the layer, though it does not by itself fix
		the direction of any individual borrowing.
	</p>
</S>

<S t="Morphological integration" id="integration">
	<p>
		The evidence for how loanwords behave in the grammar is thin, and we state only what
		the texts attest. The clearest fact is that integrated loan nouns are ordinary nouns:
		they take the native counting frames of <Xr ch="numerals" /> and slot into the same
		syntactic positions as inherited vocabulary. In the Sentoku letters the loan
		<i lang="ain">kanpi</i> &lsquo;letter&rsquo; is counted, possessed, and governed by
		postpositions exactly as a native noun would be, and it heads relative clauses
		(<i lang="ain">e=koro kanpi</i> &lsquo;your letter&rsquo; with the regular
		second-person possessive prefix). Trade-stratum nouns likewise take native quantifier
		and classifier frames: the borrowed counting-bases <i lang="ain">tanku</i> and
		<i lang="ain">wantanku</i> are not merely loaned words but loaned grammatical
		<em>nouns</em>, themselves countable with the native adnominal numerals
		(<i lang="ain">sine tanku</i> &lsquo;one hundred&rsquo;), which is as deep an
		integration as a numeral system admits (<Xr ch="numerals" />).
	</p>
	<p>
		Whether loan <em>verbs</em> were taken in is harder to show. The integrated lexicon is
		overwhelmingly nominal — names of goods, animals, tools and materials — as is typical
		of trade borrowing, and the corpus offers no secure example of a borrowed verb stem
		inflecting with the Ainu personal prefixes. Where a foreign action concept is needed,
		the language reaches instead for a native verb or a light-verb construction with
		<A w="kara" gl="make" /> or <A w="ki" gl="do" />, leaving the loan in nominal shape.
		We flag this as an argument from silence rather than a demonstrated gap: the recorded
		corpus is small, and the latest speakers' free code-switching into Japanese (§25.1)
		shows that a foreign verb could always be reached for by simply switching language
		rather than by morphologically nativising the stem — which may be part of why a
		nativised loan-verb class never had to develop.
	</p>
</S>
