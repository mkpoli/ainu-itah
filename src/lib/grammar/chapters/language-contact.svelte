	<p>
		The dagger marks the candour the data demand. For the dog-sled word, Chiri held
		<i lang="ain">nuso</i> to be a borrowing from Nivkh <i lang="ain">nuci</i> — there is
		no native Ainu word for the rig, and the <i lang="ain">nuci</i> set is spread across
		the lower Amur — and Shiraishi and Tangiku accept this
		<Ref k="shiraishi2022" paren />. But for <i lang="ain">tonto</i> &lsquo;tanned hide&rsquo;
		and <i lang="ain">upun</i> &lsquo;storehouse&rsquo; the likelier direction is the
		reverse, Ainu → Nivkh, so they are best not counted as Ainu loans at all. The general
		point is that for a great many of these the direction is simply debated; some may even
		come from a third, lost source — the language of the Okhotsk culture — that fed both
		Ainu and Nivkh <Ref k="janhunen2022" />. The reindeer word is the most famous
		instance of an old, deep exchange in this zone: Ainu <A w="tunakay" gl="reindeer" />
