<p>
	Sakhalin was a meeting place. Within reach of a single household the language touched
	Japanese to the south, the Tungusic of the Uilta (Orok) and the wider Manchu-Amur trade
	network to the west, the isolate Nivkh (Gilyak) of the central island and the lower Amur,
	and, from the nineteenth century, Russian. The sociohistorical frame — who spoke what to
	whom, the interpreters and trade fairs, the partition of 1905 — is set out in
	<Xr ch="language-and-speakers" s="contact" />, and is not repeated here; nor is the
	regular adaptation of foreign sounds, whose basics belong to
	<Xr ch="phonology" s="loans" />. This chapter takes up what those do not cover: the
	lexical strata themselves, source language by source language, with their semantic
	domains and their evidential weight; the most thorough single treatment is Shiraishi and
	Tangiku's contact chapter in the Bugaeva handbook <Ref k="shiraishi2022" paren />, whose
	loanword appendix underlies much of the tabulation below. The sections run through the
	Japanese stratum (§25.1), the Manchu-Amur trade stratum that reached Ainu mostly through
	Tungusic and Nivkh middlemen (§25.2), the large and under-studied Nivkh layer (§25.3),
	the thin Russian lexicon and the Cyrillic writing register that is its real legacy
	(§25.4), the phonological adaptation of loans (§25.5), and their morphological
	integration (§25.6). A word of caution governs the whole: borrowing direction on
	Sakhalin is often genuinely undecidable, and several items long treated as loans into
	Ainu are now thought to run the other way. Where the evidence is unsettled it is flagged
	as such rather than smoothed over.
</p>

<S t="The Japanese stratum" id="japanese">
	<p>
		Japonic contact with Ainu is the oldest documented layer and falls into two very
		unequal parts. A small, deep stratum entered before the island period: items such as
		<A w="sippo" gl="salt" /> &lsquo;salt&rsquo; (Old Japanese <i lang="ja">shiho</i>) and
		<A w="pasuy" gl="chopsticks" /> &lsquo;chopsticks&rsquo; preserve features lost in
		later Japanese — the stop pronunciation of <i lang="ain">*p</i>, the diphthong
		<i lang="ain">*uy</i> — and so must have been received around the middle of the first
		millennium CE, when the ancestor of Ainu still lay in the immediate northern
		neighbourhood of the expanding Yamato state <Ref k="janhunen2022" />. These are
		pan-Ainu and not specifically Sakhalin; <i lang="ain">sippo</i> is recorded in the
		earliest Hokkaidō and Kuril word-lists alike.
	</p>
	<p>
		The bulk of the Japanese vocabulary is far younger and belongs to the Karafuto era of
		the Russo-Japanese frontier and the Japanese administration after 1905, when material
		culture flowed north in quantity. The diagnostic Sakhalin trait here is prosodic: the
		pitch accent of the Japanese source is rendered as <em>vowel length</em>, the
		Sakhalin counterpart of the Hokkaidō pitch contrast (<Xr ch="phonology" s="loans" />),
		so that an accented syllable surfaces long. The clearest set is the
		&lsquo;metal&rsquo; family, all built on <A w="kaani" gl="metal" /> (&lt; Japanese
		<i lang="ja">kane</i>, with the long vowel for the accent), beside Hokkaidō
		<i lang="ain">kani</i> with a short vowel <Ref k="hattori1964" p="261" />.
	</p>
