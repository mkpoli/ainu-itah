	<p>
		Two of these words carry the chapter's recurring lesson about direction and route.
		<i lang="ain">tampaku</i> &lsquo;tobacco&rsquo; is formally a Japanese loan
		(<i lang="ja">tabako</i>), but the same item travels the whole northern network —
		Nivkh <i lang="ain">tamχ</i>, Uilta <i lang="ain">saŋna</i>, Manchu
