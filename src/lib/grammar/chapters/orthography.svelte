<S t="Script correspondences on this site" id="correspondences">
	<p>
		Outside the grammar, this site displays every Ainu form in three scripts — Latin,
		Cyrillic and katakana — converted automatically from the romanisation just described.
		The conversion between Latin and Cyrillic is lossless in both directions; the kana
