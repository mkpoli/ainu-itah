	<S t="Linguistic romanisations" id="linguistic-romanisations">
		<p>
			Chiri Mashiho (<span lang="ja">知里真志保</span>) gave the Sakhalin dialect its first
			systematic linguistic transcription. In his 1942 grammar he replaced the
			Hepburn-style digraphs of earlier work with <i lang="ain">č</i>, <i lang="ain">š</i>,
			<i lang="ain">j</i> and <i lang="ain">ü</i>, and wrote the Sakhalin coda fricative as
			<i lang="ain">x</i> (<i lang="ain">max</i> 'woman', <i lang="ain">tex</i> 'hand')
			<Ref k="chiri1942" p="凡例" paren />; vowel length he left unwritten, regarding it as
			non-distinctive <Ref k="chiri1942" p="第1章" paren />, a decision since reversed by
			all later workers (see <Ref k="itabashi2001" /> on the phonemic status of length).
			Nakagawa traces how this notation fed into the strict one-letter-one-phoneme
			romanisation established in the 1950s and codified in the dialect dictionary edited
			by Hattori Shirō (<span lang="ja">服部四郎</span>)
			<Ref k="nakagawa2006" p="3–5" paren />: there every vowel-initial syllable is preceded
			by an apostrophe for the glottal stop, /c/ is written <i lang="ain">c</i>, the coda
			fricative <i lang="ain">h</i>, and long vowels are doubled
			<Ref k="hattori1964" p="34" paren />.
		</p>
		<p>
			Murasaki Kyōko (<span lang="ja">村崎恭子</span>) adopted the Hattori system, in
			simplified form, for her grammar and for all her text editions: glottal stop
			<i lang="ain">'</i> before vowel-initial syllables, doubled long vowels, particles
			written as separate words <Ref k="murasaki1976" />, <Ref k="murasaki2025" p="凡例" />.
			Her transcription of the tales of Asai Take (<span lang="ja">浅井タケ</span>) is the
			single largest body of Sakhalin text in this notation <Ref k="murasaki2001" paren />. Recent philological work on the older
			corpora (Sakaguchi, Dal Corso) keeps the same letter values but omits the
			apostrophe and separates person clitics with an equals sign
			<Ref k="sakaguchi2021" p="45" paren />, which is also the practice of this grammar.
		</p>
	</S>
</S>

<S t="Katakana" id="katakana">
	<p>
		Kana transcription of Ainu reaches back to the Edo-period interpreters, and native
		kana writing — Yamamoto Tasuke (<span lang="ja">山本多助</span>), Nabesawa Motozō
		(<span lang="ja">鍋沢元蔵</span>), Sunazawa Kura (<span lang="ja">砂沢クラ</span>) and
		others — has been
		studied in detail by <Ref k="kirikae1997" /> and <Ref k="nakagawa2006" p="24–31" />;
		that material is almost entirely Hokkaidō. For Sakhalin, kana enters with the Japanese
		administration. Kindaichi Kyōsuke's (<span lang="ja">金田一京助</span>) edition of the
		autobiography of the Sakhalin Ainu Yamabe Yasunosuke (<span lang="ja">山辺安之助</span
		>), <span lang="ja">あいぬ物語</span> (1913), prints the Ainu text as katakana ruby
		over the Japanese translation — though the spelling there is Kindaichi's, not
		Yamabe's own <Ref k="nakagawa2006" p="25" paren />. Sentoku, who had written Ainu in
		Cyrillic in 1906, used katakana for the Ainu names and words in his Japanese-language
		book of 1929 <Ref k="sentoku1929" paren />.
	</p>
	<p>
		The Sakhalin-specific kana conventions in use today were fixed by Chiri's 1942
		grammar: syllable codas are written with small kana (<span lang="ain-Kana">ㇷ゚ ッ ㇰ ㇱ
			ㇺ</span> for -p, -t, -k, -s, -m), and the coda fricative /h/ with a small ha-row kana
		agreeing in vowel colour with the preceding vowel — <span lang="ain-Kana">マㇵ</span>
		<i lang="ain">mah</i> 'woman', <span lang="ain-Kana">クㇷ</span>
		<i lang="ain">kuh</i> 'belt', <span lang="ain-Kana">テㇸ</span>
		<i lang="ain">teh</i> 'hand', <span lang="ain-Kana">オㇹ</span>
		<i lang="ain">oh</i> 'spear' <Ref k="chiri1942" p="凡例" paren />. The vowel-matched
		small kana for coda h remain the standard way of writing Sakhalin words in kana and
		are used on this site (§3.5).
	</p>
	<p>
		The main Sakhalin text actually written down in kana by an Ainu writer is the tale
		that Yamamoto Tasuke (1904–1993), a Kushiro Ainu and
		later a prolific kana author, recorded in February 1937 from Kimura Usarusima
		(<span lang="ja">木村ウサルシマ</span>) of Ay on the East coast, and published in his
		Ainu-language journal <span lang="ja">アイヌ・モシリ</span>
		<Ref k="sakaguchi2019yamamoto" paren />. Yamamoto writes codas with small kana, but
		uses <span lang="ain-Kana">シュ</span> in words like <span lang="ain-Kana">クシュ</span>
		for <A w="kusu" gl="in order to" />, and sometimes -p and -k (<span lang="ain-Kana"
			>チカㇷ゚</span
		>, <span lang="ain-Kana">パテㇰ</span>) where Sakhalin -h is expected — possibly
		carrying over his native Kushiro forms <Ref k="sakaguchi2019yamamoto" p="114" paren />.
	</p>
