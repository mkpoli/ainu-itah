<S t="Latin" id="latin">
	<S t="Piłsudski's practical orthography" id="pilsudski-orthography">
		<p>
			The twenty-seven East-coast texts of Piłsudski's <em>Materials</em> are printed in a
			practical Latin orthography which he explains in the prefatory "Remarks on Ainu
			phonetics, and on the transcription of the texts"
			<Ref k="pilsudski1912" p="1–10" paren />. Its values are Polish rather than English:
			<i lang="ain">j</i> is the glide /y/, <i lang="ain">v</i> alternates with
			<i lang="ain">w</i>, and a palatalised sibilant "between s and sh" is written with the
			Polish acute, wavering with plain s in his own hearing — <i lang="ain">iśam</i> ~
			<i lang="ain">isam</i> 'there is not', <i lang="ain">śumari</i> 'fox'
			<Ref k="pilsudski1912" p="5–6" paren />. The letter <i lang="ain">x</i> renders the
			velar fricative that he correctly identified as found "only in Saghalien",
			corresponding to Hokkaidō p, t, k: <i lang="ain">axto</i> 'rain' beside Yeso
			<i lang="ain">apto</i>, <i lang="ain">cux</i> beside <i lang="ain">cup</i> 'sun',
			<i lang="ain">cex</i> beside <i lang="ain">cep</i> 'fish'
			<Ref k="pilsudski1912" p="7, 9" paren />. Voiced letters b, d, g appear wherever he
			heard intervocalic voicing; stress is marked on all non-oxytone words; whispered
			word-final vowels are set as small superscript letters; and words are divided
			etymologically rather than by sandhi <Ref k="pilsudski1912" p="9–10" paren />. Dal
			Corso has recently mapped this notation systematically onto a phonemic analysis
			<Ref k="dalcorso2024" paren />. Example (4) shows one of his sentences in the original
			orthography and in ours.
		</p>
		<Ex
			id="ex-pilsudski"
