<S t="The romanisation of this grammar" id="romanisation">
	<p>
		This grammar writes Sakhalin Ainu with eleven consonant letters — <i lang="ain">p t k
			c s h m n r w y</i> — and five vowel letters <i lang="ain">a e i o u</i>, with long
		vowels doubled (<A w="itah" gl="speech" />, <i lang="ain">kestoono</i> 'the other
		day'). The glottal stop that begins every vowel-initial syllable is predictable and is
		left unwritten. The letter <i lang="ain">h</i> serves both as syllable onset and for
		the coda fricative (<i lang="ain">itah</i>, <i lang="ain">ahkas</i> 'walk'); other
		codas are written as the sources have them (<i lang="ain">isam</i>,
		<i lang="ain">kotan</i>; on the -p, -t, -k retained in the far south see
		<Ref k="sakaguchi2021" p="45" /> and <Xr ch="morphophonology">morphophonology</Xr>). Person clitics are separated with an
