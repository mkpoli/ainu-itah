	<S t="Sentoku Tarōji's letters of 1906" id="sentoku">
		<p>
			Sentoku Tarōji (1872–1929), an East-coast Ainu of Otasan schooled under the Russian
			administration, wrote three letters in Ainu to Piłsudski, dated 4 June, 15 June and
			11 August 1906. They are kept in the Sakhalin Regional Museum and were deciphered and
			published with a Japanese translation by Tangiku Itsuji (<span lang="ja"
				>丹菊逸治</span
			>) and Ogihara Shinko (<span lang="ja">荻原眞子</span>)
			<Ref k="tangiku2001" paren />. They are the earliest known Ainu-language texts written
			by an Ainu hand, antedating Chiri Yukie's (<span lang="ja">知里幸恵</span>)
			<span lang="ja">アイヌ神謡集</span> of 1923
			by seventeen years <Ref k="nakagawa2006" p="21" paren />.
		</p>
		<p>
			Sentoku's orthography is an unstandardised but internally consistent adaptation of
			Russian cursive. In the letters we observe the following habits: the coda fricative
			is <span lang="ain-Cyrl">х</span> (<span lang="ain-Cyrl">яхка</span>
			<i lang="ain">yahka</i> 'even if'); /c/ is written both
			<span lang="ain-Cyrl">ц</span> and <span lang="ain-Cyrl">ч</span> (<span
				lang="ain-Cyrl">цеп</span
			>
			<i lang="ain">cep</i> 'fish', <span lang="ain-Cyrl">нучя</span> ~
			<span lang="ain-Cyrl">нуця</span>
			<i lang="ain">nuca</i> 'Russian'); <span lang="ain-Cyrl">б</span> appears for /p/
			after nasals (<span lang="ain-Cyrl">камбі</span> ~ <span lang="ain-Cyrl">кампі</span>
			<i lang="ain">kampi</i> 'letter'); both <span lang="ain-Cyrl">и</span> and the
			pre-reform <span lang="ain-Cyrl">і</span> serve for /i/; and — most striking — the
			devoiced /i/ of syllables between voiceless consonants is simply not written:
			<span lang="ain-Cyrl">цсе</span> for <A w="cise" gl="house" />,
			<span lang="ain-Cyrl">цкін</span> for <i lang="ain">cikin</i> 'if',
			<span lang="ain-Cyrl">цнукара</span> for <i lang="ain">ci=nukara</i> 'we read it'.
			This matches the strong devoicing of unaccented /i/ that Chiri describes for the
			dialect <Ref k="chiri1942" p="第1章" paren />. Sentoku also mixes in Arabic numerals
			and undigested Russian words (<span lang="ru">помощникъ старосты</span> 'assistant
			village head'), exactly as a bilingual letter-writer would.
		</p>
