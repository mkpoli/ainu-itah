<p>
	Sakhalin Ainu has no traditional script, but it has a longer history of being written
	than is often assumed, and in three different scripts. Russian observers and at least
	one native writer used Cyrillic between the 1860s and 1906; Bronisław Piłsudski and
	every later linguist used some form of the Latin alphabet; and from the Japanese period
	onward Ainu words and whole texts were set down in katakana, by researchers and by Ainu
	writers themselves. This chapter surveys these practices with attested specimens of
	each, then defines the romanisation used throughout this grammar and the
	Latin–Cyrillic–katakana correspondences used by this site. The phoneme inventory that
	the romanisation encodes is justified in <Xr ch="phonology">the phonology chapter</Xr>;
	the sources themselves are surveyed in <Xr ch="dialects-and-sources">the chapter on
	dialects and sources</Xr>.
</p>

<S t="Cyrillic" id="cyrillic">
	<p>
		Cyrillic is the script of two of the earliest bodies of connected Sakhalin Ainu text:
		the specimens appended to Mikhail M. Dobrotvorsky's (<span lang="ru"
			>Михаил Михайлович Добротворский</span
		>) dictionary of 1875, and the letters that Sentoku Tarōji
		(<span lang="ja">千徳太郎治</span>) wrote to Piłsudski in 1906.
	</p>

	<S t="Dobrotvorsky and the earliest records" id="dobrotvorsky">
		<p>
			Dobrotvorsky, a Russian military physician, lived on Sakhalin from 1867 to 1872 and
			compiled an Ainu–Russian dictionary of over ten thousand headwords, more than half of
			them collected by himself, mainly from speakers of the central West coast such as
			Sambaku of Nayoro <Ref k="sakaguchi2021" p="43–44" />. At the end of the dictionary he
			printed two short connected pieces — Ciwokanke's speech and a reconciliation
			dialogue — which Piłsudski already singled out as the first usable Sakhalin texts
			<Ref k="pilsudski1912" p="XXIV" paren />.
		</p>
		<p>
			Dobrotvorsky wrote Ainu with the pre-reform Russian alphabet, marking stress but not
			vowel length <Ref k="sakaguchi2021" p="45–47" paren />. Several of his conventions are
			phonetically revealing. He alternates <span lang="ain-Cyrl">б</span>/<span
				lang="ain-Cyrl">п</span
			>
			and <span lang="ain-Cyrl">к</span>/<span lang="ain-Cyrl">г</span> for /p/ and /k/,
			recording the intervocalic voicing that is allophonic in Sakhalin Ainu (see
			<Xr ch="phonology">phonology</Xr>). Word-initial /r/ he consistently heard with an
			occlusive onset and wrote <span lang="ain-Cyrl">тр</span>, as in
			<span lang="ain-Cyrl">трéкоро</span> for <A w="reekoro" gl="very" /> — an observation
			discussed at length by <Ref k="pilsudski1912" p="8" /> and matched by Chiri's report
			that initial r wavers with a t-like stop (<i lang="ain">reekoro</i> ~
			<i lang="ain">teekoro</i>) <Ref k="chiri1942" p="第1章" paren />. A silent
			<span lang="ain-Cyrl">ъ</span> closes every consonant-final word, as in contemporary
			Russian spelling, and /c/ is normally <span lang="ain-Cyrl">ч</span>. Example (1)
			shows the opening of his first text with our retranscription.
		</p>
