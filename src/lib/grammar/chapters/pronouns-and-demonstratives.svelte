<p>
	Sakhalin Ainu is a pro-drop language: person is carried by the verbal affixes of
	<Xr ch="person-and-alignment">Chapter 13</Xr>, and the words discussed in this chapter —
	personal pronouns, demonstratives, interrogatives — are a small, partly defective class
	added for emphasis, contrast and deixis. Chiri devoted a famously deflationary section to
	them: pronouns take no case forms, no modifiers, head no compounds, and
	<i lang="ain">ani oman</i> ‘HE went’ means no more than <i lang="ain">oman</i> ‘he went’ —
	“the pronoun is mere reinforcement” <Ref k="chiri1942" p="§90" paren />. The data below
	come from Chiri's west-coast survey <Ref k="chiri1942" p="§90–§104" paren />, Hattori's
	Rayciska elicitation <Ref k="hattori1961" paren />, Murasaki's Rayciska grammar and
	conversation course (<Ref k="murasaki1979" />; <Ref k="murasaki2025" />), Sakaguchi's
	east-coast philology (<Ref k="sakaguchi2020utara" />; <Ref k="sakaguchi2024" p="26–56" />),
	the Sentoku letters <Ref k="tangiku2001" paren />, and our own counts over the corpus of
	Asai Take's (<span lang="ja">浅井タケ</span>) tales.
</p>

