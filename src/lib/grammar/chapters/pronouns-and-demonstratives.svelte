	<p>
		The pronouns are built on the person prefixes plus <i lang="ain">ani</i> ‘being’ in the
		singular and <i lang="ain">okay</i> ‘exist.PL’ in the plural; their grammatical number
		follows the series, not the reference, so <i lang="ain">ciokay</i> and
		<i lang="ain">anokay</i> are formally plural even when they mean ‘I’
		(<Xr ch="person-and-alignment" s="indefinite">Chapter 13</Xr>). Genre and dialect sort
		the first persons sharply. In our counts, the three Sentoku Tarōji
		(<span lang="ja">千徳太郎治</span>) letters use <i lang="ain">cokay</i> 32 times,
		<i lang="ain">anokay</i> 7 times, <i lang="ain">eani</i> ~ <i lang="ain">iani</i> 23
		times, and <i lang="ain">kuani</i> never; Asai Take's west-coast tales have
		<i lang="ain">kuani</i> 38 times, <i lang="ain">eani</i> 10, <i lang="ain">anoka</i> 6,
		and <i lang="ain">cokay</i> never. Chiri's consultants felt <i lang="ain">kuani</i> to
		be the politer choice beside homely <i lang="ain">anokay</i>, and treated
		<i lang="ain">ciokay</i> and <i lang="ain">anokay</i> as interchangeable
		<Ref k="chiri1942" p="§90" paren />; the CI-pronoun is at home on the east coast and the
		southern west coast and marginal in Rayciska <Ref k="sakaguchi2024" p="27–29" paren />.
	</p>
	<p>
		Syntactically the pronoun is an optional adjunct that doubles an argument already
		indexed on the verb, whatever its role: subject, object, or possessor
		(<i lang="ain">kuani ku-cipihi ne</i> ‘it is MY boat’, <Ref k="chiri1942" p="§91"
		paren />):
	</p>
