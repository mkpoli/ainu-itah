	<S t="Anaphoric neya and nean in narrative" id="anaphoric">
		<p>
			The anaphoric (“ideational”) series points not into space but into the discourse:
			<i lang="ain">neya cise</i> ‘the aforementioned house’. Chiri stressed that Ainu, with
			no articles, uses exactly this series where English uses the definite article, and
			notes <i lang="ain">nean</i> &gt; <i lang="ain">nan</i> from northern Hokkaidō into
			Sakhalin <Ref k="chiri1942" p="§100–§101" paren />. The east-coast texts use
			<i lang="ain">nea(n)</i>, reduplicable as <i lang="ain">nea nea</i> ‘that
			same — you remember’ (<Xr ch="number" s="ahcin">Chapter 8</Xr>, example (13) there).
			In the west-coast tales of Asai Take the anaphorics dominate the whole demonstrative
			economy. Our counts over the 3,852 corpus sentences: <i lang="ain">neya</i> 437
			tokens and <i lang="ain">neʼan</i> 195 (with <i lang="ain">neeroh</i> 7,
			<i lang="ain">neyan</i> 10), against only 41 of proximal <i lang="ain">tan</i>, 17 of
			<i lang="ain">tara</i> and 7 of <i lang="ain">taranoka</i> — the exophorics confined
			almost entirely to quoted dialogue, the anaphorics carrying reference-tracking in the
			narration. Add pronominal <i lang="ain">tah</i> ‘this (just said)’ (198 tokens) and
			topicalised <i lang="ain">neʼampe</i> ‘that thing; as for that, thereupon’ (916
			tokens, mostly grammaticalized as a topic-switching connective,
			<Xr ch="information-structure">Chapter 24</Xr>), and the deictic word family makes up
			a remarkable share of running narrative text.
		</p>
		<Ex
			m="neyke tani neya horokewpo utah okore taa asip-ahci manu"
			g="and.then now that man PL all then go.out.PL-PL REP"
			tr="And then those men all went out."
			cite="murasaki2001:text 37"
			dial="WS"
			place="Odasu (Asai Take)"
		/>
		<Ex
			m="tani taa ahcahcipo tah nuu kusu ene yee manu"
			g="now then old.woman this hear because thus say REP"
			tr="Now the old woman, having heard this, spoke thus."
