	<S t="Third person: anihi and its substitutes" id="third-person">
		<p>
			Third persons are normally bare nouns or nothing at all. The pronoun
			<i lang="ain">ani(hi)</i> ‘he, she’ — formally the possessive of <i lang="ain">an</i>
			‘being’ — is rare (four tokens in the whole of Piłsudski's Materials,
			<Ref k="sakaguchi2020utara" p="189" paren />) and behaves unlike the speech-act
			pronouns: it can follow a noun phrase as an appositive intensifier, ‘X himself’
			<Ref k="murasaki1979" p="78" paren />:
		</p>
		<Ex
			m="tara aynu anihi naa oman kusu nee manu"
			g="that person 3SG too go.SG PURP COP REP"
			tr="That man himself is going to go too, they say."
			cite="murasaki1979:146; sakaguchi2020utara:198"
			dial="WS"
			place="Rayciska"
		/>
		<Ex
			m="tani anahne anihi isam teh ohacirike ta"
			g="now TOP 3SG not.exist and absence LOC"
			tr="Now that you are away, and in your absence…"
			note="A wife to her husband: the third-person pronoun used of the hearer as respectful avoidance, the singular counterpart of honorific utara (Chapter 8)."
			cite="pilsudski1912:text 21, ll. 190–192; sakaguchi2020utara:194"
			dial="ES"
		/>
		<p>
			For the plural, Chiri recorded <i lang="ain">okay</i> ‘they’ from his consultants
			<Ref k="chiri1942" p="§90" paren />, but no textual token has been found
			<Ref k="sakaguchi2020utara" p="189" paren />. In the texts the slot is filled by bare
			<i lang="ain">utara</i> (anaphoric ‘they’; <Xr ch="number" s="utara">Chapter 8</Xr>)
			and, deictically, by the phrase <i lang="ain">taranoka aynu utah</i> ‘those people’,
			which Murasaki gives as the conversational third person plural
			<Ref k="murasaki2025" p="11" paren />. We take Chiri's <i lang="ain">okay</i> at face
			value as an elicitable but textually moribund form, squeezed out by
			<i lang="ain">utara</i>.
		</p>
	</S>
</S>

<S t="The indefinite-person pronoun anoka(y)" id="indefinite-pronoun">
	<p>
		<A w="anoka" gl="I, we" /> ~ <i lang="ain">anokay</i> is the pronoun of the AN-series
		and shares its whole range — ‘we’, ‘I’, unspecified people, narrative first person —
		described in <Xr ch="person-and-alignment" s="indefinite">Chapter 13</Xr>. Two points
		belong here. First, the dedicated plural <i lang="ain">anokay-ahcin</i>, with the
		nominal plural <i lang="ain">=ahcin</i> of <Xr ch="number" s="ahcin">Chapter 8</Xr>,
		disambiguates ‘we’ from ‘I’; Hattori found it meaning ‘we’ for Rayciska speakers in
		their twenties and thirties but ‘I’ for men over sixty — the plural pronoun travelling
		the same plural-to-singular road as the AN-series itself
		<Ref k="hattori1961" p="6" paren />, <Ref k="sakaguchi2020ahcin" p="6" paren />. Yamabe's
		autobiography has <i lang="ain">anokay-ahsin</i> 102 times against
		<i lang="ain">anokay-utara</i> 14 <Ref k="sakaguchi2020ahcin" p="6" paren />:
	</p>
	<Ex
		m="yaykota e-wente yahka anokay-ahcin hene an-oskoro anpe kayki han ne"
		g="oneself 2SG.A-break although INDEF-PL even INDEF.A-begrudge thing even NEG COP"
		tr="Even if you break it yourself, it is not a thing that we would begrudge."
		note="The brothers speak as a group: anokay-ahcin ‘we (and ours)’, with the AN-series prefix an- on the verb."
		cite="pilsudski1912:text 23, l. 201; sakaguchi2020utara:195"
		dial="ES"
	/>
	<p>
		Second, like the other pronouns it is an emphatic adjunct, and it freely takes numeral
		appositions: <i lang="ain">anoka tu-aynu-ne sap-an</i> ‘the two of us will go down’
		(<Xr ch="number" s="suppletion">Chapter 8</Xr>, example there). Within quoted dialogue
		it alternates with <i lang="ain">kuani</i> by register, not by meaning; the choice of
		pronoun simply follows the person series chosen on the verb.
	</p>
</S>

<S t="Demonstratives" id="demonstratives">
	<p>
		Demonstratives divide into spatial (exophoric) and anaphoric series, and each form is
		either adnominal or pronominal, the pronominals being built from the adnominals with
		the nominalizers <i lang="ain">-pe</i> ~ <i lang="ain">-h</i>
		<Ref k="chiri1942" p="§83, §97" paren />, <Ref k="murasaki2025" p="11, 13" paren />.
		Table 2 assembles the attested system.
	</p>
