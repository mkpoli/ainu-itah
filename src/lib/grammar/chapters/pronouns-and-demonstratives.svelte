	<S t="Demonstrative adverbs" id="adverbs">
		<p>
			Place adverbs are built on the same roots with the locative <i lang="ain">ta</i>:
			<i lang="ain">teeta</i> ‘here’, <i lang="ain">taata</i> ‘there’
			<Ref k="murasaki2025" p="16" paren />; Chiri adds nominal <i lang="ain">teoro</i>
			‘this place’ and <i lang="ain">taani</i> ‘that place’ <Ref k="chiri1942" p="§81, §83"
			paren />. The bare root <i lang="ain">taa</i> is ubiquitous in Asai Take's narration
			(some 6,700 tokens) as a discourse-deictic ‘there, then’, the rhythmic connective of
			spoken narrative; we analyse it as the medial root in adverbial function rather than
			as a filler without content, since it still contrasts with <i lang="ain">teeta</i>
			in dialogue. Manner deictics are <i lang="ain">ene</i> ‘thus, in this way’ (216
			tokens), typically cataphoric before quotations as in example (12), and the
			quotative <i lang="ain">nah</i> ‘so’ (<Xr ch="evidentiality">Chapter 16</Xr>);
			<i lang="ain">tani</i> ‘now’ completes the set as the temporal proximal.
		</p>
		<Ex
			m="taata hemata an-i hii"
			g="there what exist.SG-EP Q"
			tr="What is over there?"
			note="The answer is teeta itanki an ‘here is a cup’; -i is the epenthetic vowel before the final particle hVV."
			cite="murasaki2025:16"
			dial="WS"
		/>
	</S>
</S>

<S t="Interrogatives" id="interrogatives">
	<p>
		The interrogatives of Sakhalin Ainu are nouns and adverbs built on three roots,
		<i lang="ain">hem-</i>, <i lang="ain">ne-</i> ~ <i lang="ain">na-</i> and
		<i lang="ain">tema-</i> <Ref k="chiri1942" p="§104" paren />. The attested inventory,
		with token counts from the Asai Take corpus where it occurs there:
		<A w="hemata" gl="what" /> ‘what’ (162), <A w="naata" gl="who" /> ‘who’ (6; also
		<i lang="ain">nata</i> in Piłsudski's texts, <Ref k="sakaguchi2020utara" p="195"
		paren />), <A w="temana" gl="how" /> ‘how, what kind of’ (22), <i lang="ain">nahta</i>
		‘where; which’ (2), <i lang="ain">neyta</i> ‘where’ (1), <i lang="ain">nakene</i>
		‘whither’, <i lang="ain">nahwa</i> ‘whence’, <i lang="ain">nakoro(ke)he</i> ‘where;
		whose’, <i lang="ain">hempara</i> ‘when’, <i lang="ain">hempah(no)</i> ‘how many, how
		much’, <i lang="ain">hemata kusu</i> ‘why’ <Ref k="murasaki2025" p="19" paren />.
		Adnominal interrogatives are <i lang="ain">hemanu</i> ‘which’, connective-built
		<i lang="ain">neran</i> and <i lang="ain">temana-an</i> ‘what kind of’
		<Ref k="chiri1942" p="§104" paren />. Hokkaidō <i lang="ain">nen</i> ‘who’ and
		<i lang="ain">nep</i> ‘what’ are unattested in Sakhalin material — in Piłsudski
		<i lang="ain">nen</i> occurs only as a variant of <i lang="ain">neno</i> ‘like’
		<Ref k="pilsudski1912" p="247" paren /> — and the person interrogative is uniformly
		<i lang="ain">na(a)ta</i>.
	</p>
	<Ex
		m="hemata kamuy kam"
		g="what god meat"
		tr="The meat of what creature is this?"
		note="The answer is seta kam ‘dog meat’; kamuy covers game animals."
		cite="murasaki2001:text 30"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<Ex
		m="tara aynu naata"
		g="that person who"
		tr="Who is that person?"
		cite="murasaki2025:10"
		dial="WS"
	/>
	<Ex
		m="temana e-kii teh e-makan-i hii"
		g="how 2SG.S-do and 2SG.S-go.uphill.SG-EP Q"
		tr="How did you come to climb up here?"
		cite="murasaki2001:text 34"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		Interrogatives stand in situ in the position of the questioned constituent, usually
		with the final particle <i lang="ain">hii</i> ~ <i lang="ain">hee</i> closing the
		clause <Ref k="murasaki2025" p="18" paren />; there is no interrogative inversion. In
		content questions the verb commonly appears in its nominalized form
		(<i lang="ain">e-makan-i</i> above; <Ref k="chiri1942" p="§40" paren />).
	</p>
</S>

<S t="Indefinite uses: interrogative + ka" id="indefinites">
	<p>
		As across the family, interrogatives double as indefinites when followed by
		<i lang="ain">ka</i> ‘even, also’: <i lang="ain">hemata ka</i> ‘something’ (nine tokens
		in the Asai corpus), <i lang="ain">temana ka</i> ‘somehow, in some way or other’
		(five), <i lang="ain">neyta ka</i> ‘somewhere’, <i lang="ain">hempara ka</i> ‘at some
		time, ever’ <Ref k="murasaki2025" p="51" paren />. Under negation the same string is a
		negative indefinite, ‘nothing, nobody’. Beside the interrogative roots the defective
		noun <i lang="ain">nee</i> ~ <i lang="ain">neh</i> ‘some(thing)’ serves the same
		construction: <i lang="ain">nee aynu ka isam</i> ‘there is nobody’,
		<i lang="ain">neera ampe ka isam</i> ‘there is nothing’
