<S t="Previous research" id="previous-research">
	<p>
		The first extensive grammar centred on Sakhalin Ainu is
		<span lang="ja">アイヌ語法研究</span> <i>Ainu gohō kenkyū</i> by Chiri Mashiho
		<Ref k="chiri1942" paren />, published in the reports of the Karafuto Government
		Museum in Toyohara.
		Chiri planned three parts — phonology, morphology, and phraseology — but completed
		only the phonology and part of the morphology, never reaching syntax, as his preface
		records <Ref k="chiri1942" p="まへがき" paren />. The work treats Ainu as a whole with
		Sakhalin at the centre, systematically marking Hokkaidō and Karafuto forms, and its
		phonology establishes points still cited today: the five-vowel system, the
		Sakhalin-only coda <i lang="ain">x</i> (our <i lang="ain">h</i>), the exclusion of
		stops and <i lang="ain">r</i> from Sakhalin codas, and the lengthening of accented
		vowels
		<Ref k="chiri1942" p="§§1–6" paren />. Its examples are drawn partly from Piłsudski’s
		texts, partly from Chiri’s own Karafuto fieldwork
		<Ref k="sakaguchi2024" p="8" paren />.
	</p>
	<p>
		Murasaki’s grammar <Ref k="murasaki1979" paren /> describes the phonology and
		morphology of Rayciska from her own recordings and remains the reference description;
		Piłsudski’s grammatical notes, Kindaichi’s 1913 outline, and Hattori’s articles are
		its main predecessors <Ref k="sakaguchi2024" p="8–9" paren />. Specialised studies of
		the post-war corpus include <Ref k="tangiku1998" /> on west-coast verbs in
		<i lang="ain">-rV</i>, <Ref k="itabashi2001" /> on the correspondence between
		Sakhalin vowel length and Hokkaidō pitch accent, and <Ref k="murasaki2009" /> on
		numerals.
	</p>
	<p>
		Recent work falls into three strands. One is typological reassessment:
		<Ref k="bugaeva2016" /> argues that the Sakhalin possessive-based nominalisations are
		innovations, and the Handbook of the Ainu Language <Ref k="bugaeva2022" paren />
		contains chapters directly on Sakhalin — <Ref k="tangiku2022" /> on the differences
		between Karafuto and Hokkaidō dialects and <Ref k="shiraishi2022" /> on contact among
		the languages of Sakhalin. The second is the description of grammatical subsystems
		across the whole record: Dal Corso on evidentiality <Ref k="dalcorso2018" paren />,
		the rise of analytic negation <Ref k="dalcorso2021" paren />,
		<Ref k="dalcorso2025neg" paren />, and morphological alignment
		<Ref k="dalcorso2025align" paren />; Sakaguchi Ryō <span lang="ja">阪口諒</span> on
		<i lang="ain">utara</i> <Ref k="sakaguchi2020utara" paren />, the nominal plural
		suffix <i lang="ain">-ahcin</i> <Ref k="sakaguchi2020ahcin" paren />, numerals and
		calculation <Ref k="sakaguchi2022" paren />, and person and number marking across all
		sources in his dissertation <Ref k="sakaguchi2024" paren />. The third is philology:
		Sakaguchi’s editions of early texts, including the Dobrotvorsky texts
		<Ref k="sakaguchi2021" paren />, Tangiku Itsuji’s <span lang="ja">丹菊逸治</span>
		edition of the Sentoku letters <Ref k="tangiku2001" paren />, and Dal Corso’s
		re-edition of the Murasaki corpus and analysis of Piłsudski’s corpus
		<Ref k="dalcorso2024" paren />. No descriptive grammar of any east-coast dialect yet
		exists <Ref k="sakaguchi2024" p="9" paren />; for the east coast, Piłsudski’s corpus
		and the Sentoku letters must do the work that recordings do for the west.
	</p>
</S>

