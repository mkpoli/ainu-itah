<S t="Dialect division" id="dialect-division">
	<p>
		The classical picture of Ainu dialect relations rests on the 200-item lexical survey
		carried out from 1955 by Hattori Shirō <span lang="ja">服部四郎</span> and Chiri
		Mashiho <span lang="ja">知里真志保</span> among evacuee speakers in Hokkaidō, which
		covered six Sakhalin localities: Ociho <span lang="ja">落帆</span>, Tarantomari <span lang="ja">多蘭泊</span>,
		Maoka <span lang="ja">真岡</span>, Shiraura <span lang="ja">白浦</span>, Rayciska <span lang="ja">来知志</span>,
		and Nairo <span lang="ja">内路</span>
		<Ref k="sakaguchi2024" p="8" paren />, <Ref k="ono2020" p="231–232" paren />. The
		cluster analysis of <Ref k="asai1974" p="100" /> grouped these six against all
		Hokkaidō dialects and North Kuril as one of three top-level branches, and divided them
		internally into a northern group (Nairo, in the Tarayka region), a central group
		(Ociho, Maoka, Shiraura, Rayciska), and a southern group (Tarantomari).
		<Ref k="ono2020" /> reanalysed Asai’s cognacy matrix with modern clustering methods
		and found the deepest division in Ainu to run between the Sakhalin dialects and
		everything else; the coherence of Sakhalin as a unit is robust under either analysis.
	</p>
