	<p>
		<strong>Piłsudski 1912.</strong> Bronisław Piłsudski (1866–1918), exiled to Sakhalin
		in 1887, worked among the east-coast Ainu in 1896 and again from 1902 to 1905,
		latterly for the Russian Academy of Sciences <Ref k="dalcorso2024" p="56–57" paren />.
		His <em>Materials for the Study of the Ainu Language and Folklore</em> publishes 27
		<A w="ucaskoma" gl="tradition, historical tale" /> out of a collection he himself put
		at 350 texts <Ref k="pilsudski1912" p="XXI" paren />. The texts were dictated by named
		speakers of four east-coast villages — Tarayka, Hunup, Ay, and Tunayci — each
		introduced in the notes to his or her first text: Sisratoka, aged 28, of Tarayka,
		Ipoxni, aged 32, of Hunup, and several tellers at Tunayci, among them the
		88-year-old Jasinoske
		<Ref k="pilsudski1912" paren />, <Ref k="dalcorso2024" p="58" paren />. Most were monolingual first-language speakers, so the corpus shows the language
		with almost no Japanese or Russian admixture — a state never again captured
		<Ref k="dalcorso2024" p="54, 58" paren />. Each text carries an English translation
		and copious grammatical, lexical, and ethnographic notes, and the preface includes
		Piłsudski’s description of his transcription, a close phonetic one which he was able
		to check with the phonetician Rousselot in London in 1910
		<Ref k="pilsudski1912" p="XV" paren />, <Ref k="dalcorso2024" p="59" paren />. Its
		limitations are those of its method: dictation is slow, interrupts narration, and
		invites unnatural repetition, as Piłsudski himself observed
		<Ref k="pilsudski1912" p="IX" paren />, <Ref k="dalcorso2024" p="59" paren />. He
		also recorded part of his collection on phonograph wax cylinders, many now degraded
		beyond playback, though some audio has been recovered by laser reading
		<Ref k="dalcorso2024" p="59" paren />. Counting later publications, some forty of his
		Sakhalin texts are now in print with the Ainu original
		<Ref k="sakaguchi2024" p="8" paren />.
	</p>
	<p>
		<strong>Sentoku Tarōji.</strong> The one substantial body of Sakhalin Ainu written by
		a native speaker comes from Sentoku Tarōji <span lang="ja">千徳太郎治</span>
		(Ainu name Taronci), a Tsuishikari Ainu who returned to Otasan on the east coast and
		worked with Piłsudski <Ref k="sakaguchi2024" p="8" paren />. His letters to Piłsudski,
		written in 1906 in Cyrillic-script Ainu, were deciphered and published with
		transliteration and Japanese translation by <Ref k="tangiku2001" />; they are
		first-hand epistolary prose, a register otherwise absent from the corpus
		(<Xr ch="orthography">Chapter 3</Xr> discusses their spelling). His Japanese-language
		book <span lang="ja">樺太アイヌ叢話</span> <i>Karafuto ainu sōwa</i>
		<Ref k="sentoku1929" paren /> contains two further oral-literature texts in Ainu
		<Ref k="sakaguchi2024" p="8" paren />.
	</p>
	<p>
		<strong>The Karafuto period.</strong> Kindaichi Kyōsuke
		<span lang="ja">金田一京助</span> visited the new territory in 1907 and published
		detailed reports on the phonetics of Tunayci and Ociho, followed by the
		autobiography of the Tsuishikari-raised speaker Yamabe Yasunosuke
		<span lang="ja">山辺安之助</span>, <span lang="ja">あいぬ物語</span>
		<i>Ainu monogatari</i> (1913), with a grammatical outline as its appendix — the first
		systematic sketch of the language <Ref k="sakaguchi2024" p="8" paren />. The central
		work of the period is <Ref k="chiri1942" />, on which see
		<a href="#previous-research">§2.3</a>. Texts taken down by the physician Wada Bunjirō
		from a speaker of the Usoro dialect have been published in instalments by Kitahara
		(2013–2019) <Ref k="dalcorso2024" p="57–58" paren />, and folktales by Numahata Ume
		<span lang="ja">沼端ウメ</span> of Usoro have likewise appeared from manuscript
		<Ref k="sakaguchi2024" p="9" paren />.
	</p>
	<p>
		<strong>Post-war fieldwork.</strong> After the evacuation, all fieldwork took place in
		Hokkaidō. The Hattori–Chiri survey of 1955 (published 1960) supplied the comparative
		lexicon analysed by <Ref k="asai1974" /> and led to Hattori’s dialect dictionary
		<Ref k="hattori1964" paren />, whose Sakhalin columns rest largely on Rayciska forms
		from Fujiyama Haru; Hattori also published studies of her dialect’s age-graded
		vocabulary (1957), accent (1961), and vowel length (1967)
		<Ref k="sakaguchi2024" p="8–9" paren />, <Ref k="tangiku2022" paren />. Murasaki
		Kyōko <span lang="ja">村崎恭子</span> recorded Fujiyama Haru extensively: her text
		collection <Ref k="murasaki1976" paren /> presents folktales and conversations of
		Fujiyama Haru and of Ōta Yuk, a speaker of the Maoka dialect, and her grammar
		<Ref k="murasaki1979" paren /> is the only full descriptive grammar of any Sakhalin
		dialect; the 1976 corpus was re-edited with glosses and an English translation by Dal
		Corso <Ref k="dalcorso2021reedition" paren /> (on which see also
		<Ref k="dalcorso2024" p="57–58" />). Murasaki’s earliest analysis
		of Rayciska syntax appeared under her maiden name as <Ref k="furukawa1967" paren />.
	</p>
	<p>
		<strong>The last speakers.</strong> Asai Take of Odasu was recorded by Murasaki in
		the 1980s; the corpus files of the present project carry session dates of 1986–1988.
		A first volume of the folktales was issued privately in 1996
		<Ref k="murasaki1996" paren />; the complete set then appeared with audio as
		<span lang="ja">改訂浅井タケ昔話全集</span> in two volumes in the ELPR series
		<Ref k="murasaki2001" paren />, <Ref k="tangiku2022" paren />. A further ELPR volume
		edited by Tamura Suzuko <span lang="ja">田村すず子</span> presents the oral
		literature of Yamada Hayo <span lang="ja">山田ハヨ</span>, recorded in 1958, together
		with other Sakhalin sound materials <Ref k="tamura2003" paren />. Murasaki has since
		published shorter collections of Fujiyama Haru material (2013, 2016)
		<Ref k="tangiku2022" paren /> and a conversation course based on her field data
		<Ref k="murasaki2025" paren />.
	</p>
	<S t="Generational variation" id="generational-variation">
		<p>
			The Rayciska record is deep enough to show variation in apparent time as well as in
			space. Hattori’s dictionary sets certain forms apart as <i lang="ain">onne itah</i>
			‘elders’ speech’, distinct from what his consultants themselves said: the score
			numeral <i lang="ain">hohne</i> ‘twenty’ carries this label, beside the everyday
			decimal <i lang="ain">tu kunkutu</i> <Ref k="hattori1964" p="262" paren />,
			<Ref k="murasaki2009" p="74" paren />, and the same consultants knew the exclusive
			pronoun <i lang="ain">cioka</i> only as the speech of elders or of the southern west
			coast <Ref k="hattori1961" p="5" paren />, <Ref k="sakaguchi2024" p="27" paren />.
			Murasaki draws the same line in her course materials, listing elders’
			<i lang="ain">kaa</i> and <i lang="ain">enkaa</i> beside everyday
			<i lang="ain">kaske</i> and <i lang="ain">enkaske</i> among the relational nouns
			<Ref k="murasaki2025" p="17–18" paren />. These are not mere memories of a lost
			stage: the doublets coexist in running speech — Asai Take’s fifth folktale has both
			<i lang="ain">kasketa</i> and elders’ <i lang="ain">kaata</i> ‘on top of’ within a
			single passage <Ref k="murasaki2001" p="text 5" paren /> — though the elders’ member
			could be moribund, as when Naert’s Rayciska informant produced the score
			construction <i lang="ain">wanpe ikasma sine hoh</i> ‘thirty’ only as an archaism
			<Ref k="sakaguchi2022" p="71" paren />. Hattori’s 1957 article on the age-graded
			vocabulary of Fujiyama Haru’s dialect, noted above, was the first study to take the
			phenomenon as its object <Ref k="sakaguchi2024" p="8–9" paren />.
		</p>
