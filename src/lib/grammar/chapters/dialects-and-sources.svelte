	<p>
		Within Sakhalin, the documentation sorts itself by coast. West-coast varieties are
		known from post-war speakers: Rayciska and Esutoru <span lang="ja">恵須取</span> (Fujiyama Haru
		<span lang="ja">藤山ハル</span>), Odasu <span lang="ja">小田洲</span> (Asai Take
		<span lang="ja">浅井タケ</span>), Usoro <span lang="ja">鵜城</span> (the speaker
		recorded by Wada Bunjirō <span lang="ja">和田文治郎</span>), Maoka and Tarantomari
		(speakers recorded by Murasaki and by the 1950s surveys)
		<Ref k="tangiku2022" paren />, <Ref k="dalcorso2024" p="57–58" paren />. East-coast
		varieties are known chiefly from the Russian era and the first Karafuto years: Ay,
		Tunayci <span lang="ja">富内</span>, Hunup, and Tarayka through Piłsudski’s texts,
		Otasan <span lang="ja">小田寒</span> through Sentoku Tarōji’s letters, and Tunayci and
		Ociho through Kindaichi’s 1907 phonetic survey
		<Ref k="dalcorso2024" p="58" paren />, <Ref k="dalcorso2025align" paren />,
		<Ref k="sakaguchi2024" p="8" paren />.
	</p>
	<p>
		Tarayka and neighbouring Nairo in the north-east stand apart from the rest of
		Sakhalin. They retain the syllable-final stops <i lang="ain">p t k</i> that other
		Sakhalin dialects neutralise to <i lang="ain">h</i> (Tarayka
		<i lang="ain">tek</i> ‘hand’, <i lang="ain">sik</i> ‘eye’ against west-coast
		<i lang="ain">teh</i>, <i lang="ain">sis</i>), and they turn word-initial
		<i lang="ain">r</i> into a stop in a number of words (Nairo
		<i lang="ain">tup</i> ‘ice’ where Esutoru has <i lang="ain">ruh</i>)
		<Ref k="tangiku2022" paren />; <Ref k="vovin1993" /> treats this Nairo
		<i lang="ain">t-</i> as continuing a distinct Proto-Ainu initial, his
		<i lang="ain">*d-</i>. In Asai’s matrix
		Nairo shares only 60–74 of 110 test items with the other Sakhalin localities, which
		themselves share 84–93 with one another <Ref k="asai1974" p="92" paren />,
		<Ref k="ono2020" p="234" paren />. The divergence matches the ethnographic position of
		the Tarayka people, who lived in the contact zone with the Uilta and Nivkh and are
		singled out as culturally distinct in the nineteenth-century Japanese records
		<Ref k="shiraishi2022" paren />.
	</p>
	<S t="What differs between the coasts" id="coast-differences">
		<p>
			The two coastal groups differ at every level of structure; here we point to the
			differences taken up in later chapters. In phonology, beside the coda facts just
			mentioned (<Xr ch="phonology">Chapter 4</Xr>), the Piłsudski corpus shows open-mid
			values of <i lang="ain">e</i> and <i lang="ain">o</i> for the east coast where the
			west-coast recordings show close-mid vowels, frequent intervocalic voicing of
			plosives that is marginal in the west-coast materials, and no trace of the glottal
			stop heard in west-coast speech <Ref k="dalcorso2024" p="59–61" paren />. In the
			lexicon there are coast-bound pairs such as west-coast
			<A w="wampakka" gl="mittens" /> (a Uilta loan) against east-coast
			<A w="matumere" /> <Ref k="shiraishi2022" paren />, and finer-grained isoglosses
			within each coast, for example <i lang="ain">rayki</i> ‘kill’ in Shiraura and
			Esutoru against <i lang="ain">rayke</i> in Maoka, Tarantomari, and Ociho
			<Ref k="tangiku2022" paren />.
		</p>
		<p>
			The sharpest morphological difference found so far concerns person marking
			(<Xr ch="person-and-alignment">Chapter 13</Xr>). The east-coast dialects of
			Piłsudski’s corpus have a dedicated exclusive first-person plural set —
			<i lang="ain">ci-</i> for transitive subjects, <i lang="ain">-as</i> for
			intransitive subjects, and an object prefix <i lang="ain">in-</i> found nowhere
			else in Ainu — while the west-coast materials lack the productive set, retaining at
			most marginal traces (<Xr ch="person-and-alignment" />)
			<Ref k="dalcorso2024" p="66–68" paren />:
		</p>
		<Ex
			m="kamuy utara in-ranu"
			g="god PL 1PL.EXCL.O-love"
			orig="Kamúi utara inránu."
			tr="the gods love me"
			cite="pilsudski1912:86; dalcorso2024:67"
			dial="ES"
			note="in- is the exclusive first-person plural object prefix, here referring to the narrator, as is usual in folklore; the stem is ranup with elision of the final stop."
		/>
		<p>
			In the west-coast corpus the same job falls to the indefinite set: speakers refer
			to themselves with <i lang="ain">an=</i> and its congeners, as in Asai Take’s
		</p>
		<Ex
			m="yee wa an=nuu"
			g="say and INDEF.A=hear"
			tr="tell it, and I will listen"
			cite="murasaki2001:text 5"
			dial="WS"
			place="Odasu (Asai Take)"
		/>
		<p>
			Verbal morphology also diverges in the functions of the applicative and reflexive
			prefixes <i lang="ain">e-</i>, <i lang="ain">ko-</i>, and <i lang="ain">yay-</i>,
			which show valency-preserving uses in the east-coast texts beyond what the
			west-coast materials attest (<Xr ch="valency">Chapter 14</Xr>)
			<Ref k="dalcorso2024" p="68–76" paren />. Since the east-coast record predates the
			west-coast record by sixty to seventy years, each such difference must be weighed
			as possibly diachronic rather than purely geographic
			<Ref k="dalcorso2024" p="53–54" paren />.
		</p>
	</S>
</S>

