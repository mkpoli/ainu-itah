	<p>
		<strong>Dobrotvorsky 1875.</strong> The Ainu–Russian dictionary of Mikhail M.
		Dobrotvorsky <span lang="ru">Михаил Михайлович Добротворский</span> (1836–1874),
		compiled during his five years as a military physician on Sakhalin (1867–1872) and
		published posthumously in Kazan, is the earliest substantial record of the language
		<Ref k="dobrotvorsky1875" paren />, <Ref k="sakaguchi2021" p="43" paren />. It
		numbers 10,930 entries, of which 5,733 — among them about 100 personal names and 511
		place names — come from his own fieldwork, the rest from earlier glossaries; his own
		material is recognisable by its accent marks <Ref k="sakaguchi2021" p="43" paren />.
		An appendix contains two connected texts: a speech addressed by Ciwokanke to the
		priest Simeon at Kusunay, and a reconciliation dialogue. The named consultants of the
		example sentences — Sambaku, Kasituru, Sukenno — lived on the central west coast, and
		the language of the examples, with first-person singular
		<i lang="ain">cokay</i>, differs from the north-west-coast speech recorded after the
		war <Ref k="sakaguchi2021" p="43–44" paren />. The transcription is good for its date
		but runs together <i lang="ain">i</i>/<i lang="ain">e</i> and
		<i lang="ain">o</i>/<i lang="ain">u</i> and does not mark vowel length; the texts
		were re-edited, romanised, and translated by <Ref k="sakaguchi2021" />, whose edition
		this grammar follows when citing them.
	</p>
