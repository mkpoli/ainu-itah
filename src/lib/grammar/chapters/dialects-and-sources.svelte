		<p>
			This generational layering must be kept apart from the register and genre variation
			that runs through the same corpus. The alternation between the KU-series and the
			indefinite AN-series in first-person reference tracks text type, not speaker age:
			the Sentoku letters use <i lang="ain">an=</i> twenty-three times and
			<i lang="ain">ku=</i> never, Yamabe’s autobiography is narrated almost wholly in
			the AN-series, while Asai Take’s <A w="tuytah" gl="folktale" /> use
			<i lang="ain">ku-</i> freely in dialogue <Ref k="sakaguchi2024" p="46" paren />
			(<Xr ch="person-and-alignment" s="indefinite">Chapter 13</Xr>). Both kinds of
			variation bear on the warning given in <a href="#coast-differences">§2.1.1</a>: with
			an east-coast record two generations older than the west-coast one, differences
			between the coasts may be differences between periods, and the elders’ doublets show
			such change caught in progress within a single community.
