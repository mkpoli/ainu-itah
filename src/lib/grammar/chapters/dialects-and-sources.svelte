<p>
	Sakhalin Ainu is known from a record built up in four waves: the Russian-era
	documentation of the 1860s–1900s (Dobrotvorsky, Piłsudski), the writings of native
	authors and Japanese scholars in the Karafuto period (Sentoku, Kindaichi, Chiri), the
	post-war work with evacuee speakers in Hokkaidō (Hattori, Murasaki, Tamura), and the
	philological and descriptive research of recent decades. The waves do not sample the
	language evenly: the nineteenth- and early twentieth-century sources come mostly from
	the east coast, the post-war recordings almost entirely from the west coast. This
	chapter first sets out the dialect landscape and then surveys the sources and the
	research built on them.
</p>

