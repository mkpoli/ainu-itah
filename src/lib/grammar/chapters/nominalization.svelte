<p>
	The negative half of the table is as diagnostic as the positive. Noun modification
	never recruits the form: relative clauses keep the inherited prenominal gap strategy
	throughout the dialect area (<Xr ch="relative-clauses">Chapter 21</Xr>). Lexical
	nominalization — making words for persons, things, and places — remains the preserve
	of the bound nouns of §20.1. And the complement clauses that a predicate selects with
	an overt nominalizing noun likewise resist it
	<Ref k="bugaeva2016" p="105" paren />. Where a possessive-nominalized clause does
	stand as the argument of a higher predicate, as in (3) below, it does so as a clausal
	nominalization in argument function — the construction that
	<Xr ch="complementation" s="evaluative">§22.5</Xr> examines from the predicate's
	side. The innovation thus spread along a single path, from possessed noun through
	adverbial and evidential scaffolding to the main clause, without ever displacing the
	older strategies from the territory they already held.
</p>

<S t="Nominalizing words" id="pe">
	<p>
		The bound nouns <i lang="ain">pe ~ p</i> ‘thing, one’ and <i lang="ain">hi ~ i</i>
		‘thing, place, time’ head participant and event nominalizations. With
		<i lang="ain">pe</i> the result ranges from fully lexicalized nouns to transparent
		headless relatives ‘the one that…’ (<Xr ch="relative-clauses">Chapter 21</Xr>):
	</p>
	<Ex
		m="etuhka ciwteh-pe ne yawa"
		g="crow serve-NMLZ COP FIN"
		tr="He is the emissary of the crow, you see!"
		cite="murasaki2001:198; dalcorso2025neg:49"
		dial="WS"
		place="Odasu (Asai Take)"
		note="ciwtehpe ‘servant, emissary’, a lexicalized pe-nominalization."
	/>
	<Ex
		m="sine cep ka isam-hi neampe"
		g="one fish even not.exist-NMLZ TOP"
		tr="as for the fact that there was not a single fish…"
		cite="pilsudski1912:196; dalcorso2025neg:53"
		dial="ES"
	/>
	<p>
		Event nominalizations in <i lang="ain">hi</i> as in (2) feed topicalization,
		complementation, and the conditional-irrealis form <i lang="ain">kun(i) pe</i>
		(<Xr ch="complementation">Chapter 22</Xr>). The same bound nouns are fossilized
		inside core grammar: the West-coast negator <i lang="ain">hanneh(ka)</i> contains
		<i lang="ain">ham=ne-p</i> ‘NEG=COP-thing’
		(<Xr ch="negation" s="forms">§17.1</Xr>), and the topic particle
		<A w="neampe" /> is a frozen <i lang="ain">ne-an-pe</i> ‘the thing which is’
		(<Xr ch="information-structure">Chapter 24</Xr>). The place noun
		<i lang="ain">usi(ke)</i> ‘place’ supplies locational nominalizations, surviving in
		lexicalized place-words; productive examples in the Sakhalin record are scarce, and
		locational nominalization is normally carried by <i lang="ain">hi</i> or by
		relativization on a locative noun (<Xr ch="relative-clauses">Chapter 21</Xr>).
	</p>
</S>

<S t="The possessive nominalization" id="possessive-nominalization">
	<p>
		Sakhalin Ainu extended the possessive form beyond the noun. Where Hokkaidō varieties
		possess only a subset of nouns, Sakhalin generalized the suffix to all nouns —
		<i lang="ain">an=mosir-ihi</i> ‘our country’ (<Ref k="murasaki1979" p="5" />;
		<Ref k="bugaeva2016" p="105" paren />) — and then carried the same morphology onto
		verbs, yielding a genuinely non-finite form <i>V</i>-<span class="tag">POSS</span>
		that has no counterpart elsewhere in Ainu
		<Ref k="bugaeva2016" p="104–105" paren />. The suffix copies the final vowel of the
		stem with epenthetic <i>h</i>, exactly as on nouns
		(<Xr ch="morphophonology" s="poss-vowel-copy">§5.2</Xr>). Murasaki had recorded
		these verb forms as a separate particle <i lang="ain">hVV</i> of assertion and
		sympathetic questioning <Ref k="murasaki1979" p="72" paren />; Bugaeva identified
		them with the possessive paradigm and unified their uses
		<Ref k="bugaeva2016" p="112" paren />. We adopt her analysis, and gloss the form
		POSS throughout the grammar.
	</p>
	<p>
		As a clausal argument, the nominalized verb stands where a noun phrase would:
	</p>
	<Ex
		m="isa oh-ta ku=oman keray-kusu ku=araka-ha pirika"
		g="doctor place-LOC 1SG=go.SG thanks-because 1SG=be.sick-POSS be.good"
		tr="Because I went to the doctor, my sickness got better."
		cite="murasaki1979:141; bugaeva2016:108"
		dial="WS"
		place="Rayciska (Fujiyama Haru)"
		note="ku=araka-ha ‘my being sick’ is the subject of pirika ‘be good’."
	/>
</S>

<S t="Adverbial and modal hosts" id="v-poss-hosts">
	<p>
		Most occurrences of the possessive nominalization are governed by a small set of
		conjunctions and auxiliaries that contain the copula <i lang="ain">ne(e)</i>:
		manner <i lang="ain">neeno</i> ‘as, like’, adversative <i lang="ain">neya</i>
		‘but’, concessive <i lang="ain">neyah</i> ‘although’, affirmative
		<i lang="ain">neeko</i> ‘surely’, dubitative <i lang="ain">nean</i> and
		<i lang="ain">nee nankor</i> ‘probably’ <Ref k="bugaeva2016" p="106–107" paren />.
		Their Hokkaidō cognates, where they exist, govern finite verbs; the Sakhalin
		pattern <i>V</i>-POSS + <i lang="ain">ne</i>-form is the innovation.
	</p>
	<Ex
		m="ene e=ramu-hu nee-no pirika-no kii wa"
		g="like.this 2SG=think-POSS COP-ADV be.good-ADV do FIN"
		tr="Do it well, the way you think."
		cite="murasaki1979:139; bugaeva2016:106"
		dial="WS"
		place="Rayciska (Fujiyama Haru)"
	/>
	<Ex
		m="an=nukara-ha ne-ya nani hempah isam"
		g="INDEF.A=see-POSS COP-Q soon how.many not.exist"
		tr="I saw it, but it was gone at once."
		cite="murasaki1979:139; bugaeva2016:106"
		dial="WS"
		place="Rayciska (Fujiyama Haru)"
	/>
	<Ex
		m="pohke-no mokoro anah pirika-ha nee nankor"
		g="warm-ADV sleep if be.good-POSS COP probably"
		tr="It will probably be good if he sleeps warm."
		cite="murasaki1979:95; bugaeva2016:107"
		dial="WS"
		place="Rayciska (Fujiyama Haru)"
	/>
</S>

<S t="Nominalization in the evidential construction" id="evidential-nominalization">
	<p>
		The evidential sentences of <Xr ch="evidentiality">Chapter 16</Xr> are themselves
		built on nominalization: the possessed evidential nouns
		<i lang="ain">ruwehe</i>, <i lang="ain">hawehe</i>, <i lang="ain">sirihi</i>,
		<i lang="ain">humihi</i> head the clause, taking the scope proposition as their
		possessor and existential <i lang="ain">an</i> as predicate, on the template
		[possessor clause]–N-POSS–<i lang="ain">an</i>. In the basic pattern the possessor is a
		plain finite clause and no overt nominaliser appears on the scope verb at all —
		<i lang="ain">re monimahpo an ruwehe an</i> ‘evidently three girls lived there’,
		literally ‘the trace of three girls being there existed’
		(<Xr ch="evidentiality" s="inferential">§16.3</Xr>). Sakhalin diverges from Hokkaidō in
		also admitting a copula-linked subtype, in which the lexical verb is overtly nominalised
		with the possessive suffix and joined to the evidential noun by the copula
		<i lang="ain">nee</i> — <i>V</i>-POSS <i lang="ain">nee</i> N-POSS <i lang="ain">an</i>
		<Ref k="bugaeva2016" p="108" paren />:
	</p>
	<Ex
		m="tara aynu itah-no-ho nee sir-ihi an"
		g="that person speak-much-POSS COP look-POSS exist.SG"
		tr="It looks like this person talks too much."
		cite="murasaki1979:46; bugaeva2016:108"
		dial="WS"
		place="Rayciska (Fujiyama Haru)"
	/>
</S>

<S t="Main-clause nominalizations" id="main-clause">
	<p>
		The possessive nominalization also stands sentence-finally with no copula and full
		illocutionary force — an insubordination pattern. Bugaeva's survey of conversation
		finds it in assertions, polar questions, content questions, and exclamations, and
		identifies the common thread as presupposition: the nominalized form presents its
		content as established or taken for granted, while finite forms carry new
		information <Ref k="bugaeva2016" p="109–112" paren />:
	</p>
	<Ex
		m="kuani tani uwas ku=ek-ihii"
		g="1SG now just 1SG=come.SG-POSS"
		tr="I have just come."
		cite="murasaki2013; bugaeva2016:109"
		dial="WS"
		place="Rayciska (Fujiyama Haru)"
	/>
	<Ex
		m="siriman poro-n-no eci=tarap-ihii"
		g="last.night many-EP-ADV 2PL=dream-POSS"
		tr="Did you dream a lot last night?"
		cite="murasaki2013; bugaeva2016:109"
		dial="WS"
		place="Rayciska (Fujiyama Haru)"
	/>
	<Ex
		m="hemanta kusu e=cis-ihi tara hekaci"
		g="what reason 2SG=cry-POSS that boy"
		tr="Why are you crying, boy?"
		cite="murasaki2013; bugaeva2016:109"
		dial="WS"
		place="Rayciska (Fujiyama Haru)"
		note="A content question with an afterthought vocative; on the postverbal noun phrase see §19.2."
	/>
	<p>
		Furukawa's Rayciska data show the same form under her ‘past’ reading —
		<i lang="ain">tara aynu omanihi</i> ‘that person went / did he go?’ — and the
		question particle <i lang="ain">aa</i> selects precisely this nominalized form
		(<Ref k="furukawa1967" p="109–110" />;
		<Xr ch="clause-structure" s="questions">§19.4</Xr>). The perfect-like flavour
		follows from the presuppositional semantics: an event presented as settled fact.
	</p>
	<p>
		That the sentence-final form really is a nominal, and not a finite verb that
		happens to resemble one, can be shown rather than assumed. The interrogative
		final particle <i lang="ain">hetaneya</i> attaches only to nouns; yet it accepts
		the nominalized verb as its host, as in the rhetorical question
		<i lang="ain">an-i-hi hetaneya</i> ‘why on earth is there…?’ of
		<Xr ch="evidentiality" s="questions-negation">§16.6</Xr>. Dal Corso reads this as
		the decisive diagnostic and analyses the sentence-final <i>V</i>-POSS (and
		<i>V</i>-POSS + copula) patterns as true insubordination: formally subordinate
		clauses conventionalized as independent assertions of personal knowledge
		<Ref k="dalcorso2018" p="134–135" paren />.
	</p>
	<p>
		The main-clause use is also where the two coasts part ways. The Rayciska record
		shows the full range of the table in the chapter introduction, insubordinated
		assertions and questions included. The East-coast texts deploy the nominalization
		freely in its embedded functions — as a clausal adjunct under causal
		<i lang="ain">kusu</i>, e.g. <i lang="ain">naha nuu-hu kusu</i> ‘because of his
		hearing that’, and as the object of a cognition verb, e.g. the
		<i lang="ain">…-pi-hi</i> nominalization embedded under
		<i lang="ain">an-eramuskari</i> ‘I do not know’
		<Ref k="pilsudski1912" paren /> (<Xr ch="complementation">Chapter 22</Xr>) — but a
		bare sentence-final assertion or question of the West-coast type is not clearly
		attested in them. We read the asymmetry diachronically: the embedded uses belong
		to the older stages of the grammaticalization path traced in §20.6, the
		insubordinated main clause to the youngest, so the less innovative East retains
		the construction precisely where it arose first. A securely main-clause
		East-coast token would overturn this reading; we have not found one, and we offer
		the gap as a provisional dialectal finding rather than an accident of the record.
	</p>
</S>

<S t="History" id="history">
	<p>
		Bugaeva orders the Ainu nominalization strategies into a grammaticalization
		sequence: zero-nominalization of the bare verb is the oldest layer; the
		nominalizing-word strategy of Hokkaidō is the second; and the Sakhalin possessive
		nominalization, first in copula-based adverbial clauses, then in the evidential
		construction, and finally as a main-clause form, is the youngest
		<Ref k="bugaeva2016" p="113" paren />. All three layers coexist in Sakhalin Ainu,
		which thus preserves the history of the category in its synchrony. She raises the
		possibility that the non-finite verb form arose under the influence of the Tungusic
		languages of Sakhalin, with their fully developed non-finite subordination
		<Ref k="bugaeva2016" p="114" paren />; the contact setting is real
		(<Xr ch="language-and-speakers" s="contact">§1.4</Xr>), and we consider the
		hypothesis plausible but not yet demonstrated — settling it would require dating
		the rise of the verb-possessive forms against the chronology of Ainu–Uilta
		bilingualism.
	</p>
</S>
