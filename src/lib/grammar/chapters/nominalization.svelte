<p>
	Sakhalin Ainu turns clauses into noun phrases in two ways. The first it shares with the
	rest of Ainu: a nominalizing word such as <A w="pe" gl="thing" /> or
	<A w="hi" gl="thing, time" /> closes the clause. The second is its own innovation, and
	the centrepiece of this chapter: the possessive suffix of nouns
	(<Xr ch="nouns-and-possession">Chapter 7</Xr>) attaches to verbs, creating a non-finite
	nominalized form that serves in adverbial clauses, under modal auxiliaries, as a clausal
	argument, in the evidential construction, and finally as an independent sentence form.
	Bugaeva analysed this <i>V</i>-possessive form and its history in the study this chapter
	follows throughout <Ref k="bugaeva2016" paren />.
</p>

<p>
	Because the chapter turns on the possessive form, its functional range is worth
	setting out at the start. The distribution is sharp: the nominalized verb appears in
	four constructions, and it is just as systematically excluded from three others
	<Ref k="bugaeva2016" p="105–106" paren />.
</p>

