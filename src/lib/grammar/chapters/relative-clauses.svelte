<p>
	Relativization in Sakhalin Ainu is simple in form and pervasive in use: the modifying
	clause stands before the head noun, the relativized argument is gapped, and there is no
	relativizer, no special verb form, and no agreement between clause and head. The verb of
	the relative clause keeps its finite shape, including person marking — the possessive
	nominalization of <Xr ch="nominalization">Chapter 20</Xr> never appears here
	<Ref k="bugaeva2016" p="98–99, 105–106" paren />. Furukawa's phrase-structure rule for
	Rayciska places the modifying clause between numeral and head, inside the noun phrase
	template of <Xr ch="clause-structure" s="np-order">§19.3</Xr>
	<Ref k="furukawa1967" p="102" paren />.
</p>

<p>
	The gapped relative clause is the central member of a wider family of noun-modifying
	constructions. Sakhalin also builds gapless ones, in which the modifying clause
	relativizes no argument of its own but stands as possessor to a perception noun — ‘the
	trace / sight / sound of [clause]’ <Ref k="bugaeva2017" p="220–221" paren />; that type
	has been grammaticalized into the evidential system and is treated with it in
	<Xr ch="evidentiality" s="inferential">§16.3</Xr>. This chapter covers the gapped type.
</p>

<S t="The gap strategy" id="strategy">
	<p>
		Any clause may modify a noun. The simplest case is a bare intransitive verb — the
		‘adjectives’ of <Xr ch="word-classes" s="property-verbs" />, as in Furukawa's
		<i lang="ain">kurasno seta</i> ‘black dog’, are exactly this construction with a
		one-word clause. With fuller clauses the pattern is unchanged:
	</p>
	<Ex
		m="ehah-taa monimahpo utah hecire-hci wa sap-ahci"
		g="lily.root-dig girl PL dance-PL and come.down.PL-PL"
		tr="The girls who went lily-root digging are coming down dancing."
		cite="murasaki2001:text 5"
		dial="WS"
		place="Odasu (Asai Take)"
		note="The modifier ehahtaa is itself a noun–verb compound (§14.6); the relativized argument is the S of ‘dig’."
	/>
	<p>
		The head noun fills its ordinary slot in the main clause, and the relative clause may
		in turn contain the person prefixes that its own arguments require. When the head
		corresponds to the object of the modifying clause, the clause-internal A marking
		stays in place:
	</p>
	<Ex
		m="ci-reske moromahpo kami-hi ci-ee rusuy"
		g="1PL.EXCL-raise girl flesh-POSS 1PL.EXCL-eat DES"
		tr="we want to eat the flesh of the girl we raised"
		cite="murasaki2001:text 36"
		dial="WS"
		place="Odasu (Asai Take)"
		note="cireske moromahpo ‘the girl whom we raised’; the head is further possessed (kamihi ‘her flesh’)."
	/>
	<Ex
		m="opista ci=nukara aynu uta"
		g="all 1PL.EXCL=see person PL"
		tr="all the Ainu people whom we met"
		cite="tangiku2001:Letter 3"
		dial="ES"
		place="Otasan (Sentoku Tarōji)"
	/>
</S>

<S t="Accessible positions" id="positions">
	<p>
		Measured against the accessibility hierarchy, Ainu relativization reaches nearly the
		whole way down: subjects, objects, obliques, and inalienable possessors can all head
		a relative clause, and only the alienable possessor and the standard of comparison
		are excluded <Ref k="bugaeva2015" p="43" paren />. Within that wide reach the corpus
		distributes its heads very unevenly. S and O relativize freely and account for
		nearly all heads; (1) and (2)–(3) illustrate each, and existential relatives of the
		shape <i lang="ain">cise ohta an N</i> ‘the N who was in the house’ are among the
		most frequent of all (<Xr ch="copula-and-existence" s="locative">§18.3</Xr>). Heads
		corresponding to A are structurally unproblematic — the modifying clause simply
		retains its overt object — but they are rare in running text, which we attribute to
		discourse rather than grammar: A referents are typically topical protagonists,
		person-marked on the verb and not in need of restrictive modification. The pattern
		matches the cross-linguistic skew of relativization frequency along the
		accessibility hierarchy rather than any Sakhalin-specific constraint. The lower
		positions — obliques, possessors, and the standard of comparison — each recruit
		extra machinery, and the next three sections take them in turn.
	</p>
</S>

<S t="Obliques: retaining the role marker" id="obliques">
	<p>
		Below the core roles the bare gap stops working: verb agreement registers arguments,
		not adjuncts, so an oblique gap would leave its role unrecoverable. The Ainu answer,
		family-wide, is retention — the postposition that would have marked the constituent
		stays behind inside the relative clause, orphaned of its complement, and signals the
		role of the head. In Hokkaidō the device is textbook material; Satō's paradigm pair
		shows the instrumental <i lang="ain">ani</i> left at the clause edge once its
		complement <i lang="ain">makiri</i> ‘knife’ is promoted to head
		<Ref k="sato2008" p="263–264" paren />:
	</p>
	<Ex
		m="makiri ani cep ku=tuye"
		g="knife INS fish 1SG.A=cut"
		tr="I cut fish with a knife."
		cite="sato2008:263–264"
		place="Hokkaidō"
	/>
	<Ex
		m="ani cep ku=tuye makiri"
		g="INS fish 1SG.A=cut knife"
		tr="the knife I cut fish with"
		cite="sato2008:263–264"
		place="Hokkaidō"
		note="The orphaned instrumental is the only signal of the head's role inside the clause; no resumptive element appears."
	/>
	<p>
		Not every marker can be orphaned. Bugaeva delimits the strandable set as the
		instrumental <i lang="ain">ani</i>, the comitative <i lang="ain">tura</i>, the
		dative <i lang="ain">eun</i>, and the terminative <i lang="ain">pakno</i>; the plain
		locative <i lang="ain">ta</i> and allative <i lang="ain">un</i> cannot strand, and a
		spatial adjunct relativizes instead through a relational noun such as
		<i lang="ain">oro</i> ‘place of’, which remains in the clause carrying the locative
		marker <Ref k="bugaeva2015" p="48, 50" paren />. The Sakhalin counterparts of the
		strandable set are <A w="ani" gl="with, by means of" />,
		<A w="tura" gl="together with" />, and the terminative
		<A w="pahno" gl="until, as far as" /> (<Xr ch="postpositions" s="case-particles"
			>§11.1</Xr
		>), the dative work of Hokkaidō <i lang="ain">eun</i> falling to
		<i lang="ain">onne</i>.
	</p>
	<p>
		The one direct Sakhalin token of an oblique relative obeys the constraint exactly.
		In (6) a goal of motion has been relativized, and the clause does not strand bare
		<i lang="ain">ta</i>: it retains <i lang="ain">or-ta</i> ‘at the place (of)’ — the
		relational-noun bridge — which Dal Corso analyses as the cue that keeps the role of
		the gapped constituent recoverable <Ref k="dalcorso2025neg" p="53" paren />:
	</p>
	<Ex
		m="or-ta ariki-an-kun Hokkayto mosiri"
		g="place-LOC come.PL-INDEF.S-COND Hokkaidō land"
		tr="Hokkaidō, to which we would (never have thought of) coming"
		cite="murasaki1976:5; dalcorso2025neg:53"
		dial="WS"
		place="Rayciska"
		note="The retained locative orta marks the relativized constituent as a goal; -kun marks the clause as irrealis (§22.2)."
	/>
	<p>
		Spatial relativization also has a route that needs no retained marker at all: a
		place noun as head. In Sentoku's letters the locus argument of
		<A w="an" gl="exist" /> ‘be (at)’ is gapped under the head
		<i lang="ain">toko</i> ‘place’, whose own semantics supply the spatial role; the
		headed phrase then behaves like any noun, here as complement of the terminative
		(the same token quoted in <Xr ch="postpositions" s="ablative">§11.4</Xr>):
	</p>
	<Ex
		m="israh kara wa cokay e=an toko pak no paye=an kusu iki"
		g="message make and 1PL 2SG=exist.SG place until ADV go.PL=INDEF.S PURP do"
		tr="I will send word, and we will travel as far as the place where you are."
		cite="tangiku2001:Letter 1"
		dial="ES"
		place="Otasan (Sentoku Tarōji)"
		note="e=an toko ‘the place where you are’: the locative argument of an is gapped, and the place-noun head makes a stranded ta unnecessary."
	/>
	<p>
		Retention competes, finally, with the applicatives of
		<Xr ch="valency" s="applicatives">§14.2</Xr>. An oblique promoted to object by
		<i lang="ain">e-</i>, <i lang="ain">ko-</i>, or <i lang="ain">o-</i> relativizes by
		the plain gap, like any other object; for Hokkaidō, Bugaeva shows the applicative
		route taken wherever the lexicon provides it, with retention as the fallback for
		roles no applicative covers <Ref k="bugaeva2010" p="760" paren />. The Sakhalin
		texts give us no secure relative clause headed by an applied object, but the feeding
		relation costs nothing: once <i lang="ain">e-tuhse-ka</i> ‘kick with’ has made the
		instrument its object, nothing distinguishes that object from any other O under
		relativization.
	</p>
</S>

<S t="Possessors" id="possessors">
	<p>
		An inalienable possessor relativizes with nominal rather than adpositional tracking
		material: the possessum stays inside the clause in its possessed form, and the
		retained possessive suffix is what identifies the head as possessor
		<Ref k="bugaeva2015" p="49" paren />. Sakhalin is structurally well prepared for
		this, having generalized the possessed form across nearly the whole noun lexicon
		(<Xr ch="nouns-and-possession" s="possessed-form">§7.1</Xr>). The texts show the
		pattern in phrasings built from clauses like <i lang="ain">maci-hi isan</i> ‘his
		wife was away’ (<Xr ch="negation" s="negative-existential">§17.4</Xr>): in
		<i lang="ain">macihi isan horokewpo</i>-type phrases, the possessive morphology on
		<i lang="ain">maci-hi</i> is all that marks <i lang="ain">horokewpo</i> ‘young man’
		as the one whose wife is meant. We analyse this as the regular gap strategy applied
		to a clause whose subject is a possessed noun — no further machinery is needed. The
		alienable possessor, by contrast, is one of the two genuinely inaccessible positions
		<Ref k="bugaeva2015" p="43" paren />: ‘the man whose dog…’ has no direct rendering,
		and the texts paraphrase through the appositive possession strategy of
		<Xr ch="nouns-and-possession" s="alienable">§7.4</Xr>.
	</p>
</S>

<S t="The standard of comparison" id="comparison">
	<p>
		The other inaccessible position is served by a conventional work-around rather than
		a relative clause proper. The standard of a comparison (‘than X’) cannot itself be
		relativized; what relativizes instead is a degree adjunct in the terminative
		<i lang="ain">pakno</i> ‘up to, as far as’, stranded like the other members of the
		strandable set, with the negative existential <A w="isam" gl="not exist" /> as the
		clause's predicate: ‘a Y [up to whom no Y exists]’, that is, ‘a Y without equal’ — a
		superlative <Ref k="bugaeva2015" p="52–53" paren />. Both the idiom and the equative
		comparison underlying it are on record from Hokkaidō (Saru):
	</p>
	<Ex
		m="a=ona sorekusu pak-no nispa isam nispa ne"
		g="INDEF.A=father truly until-ADV rich.man not.exist rich.man COP"
		tr="My father was a rich man truly without equal."
		lit="my father was a [no rich man (reaching) up to him] rich man"
		cite="bugaeva2015:53"
		place="Hokkaidō (Saru)"
		note="The degree adjunct is relativized and pakno stranded before the negative existential; the matrix clause is equative."
	/>
	<Ex
		m="ne tures-i pak pirka menoko isam"
		g="that younger.sister-POSS until be.good woman not.exist"
		tr="There was no woman as fine as his younger sister."
		cite="bugaeva2015:53"
		place="Hokkaidō (Saru)"
		note="The equative base of the superlative frame: standard + pak + parameter, with existence negated."
	/>
	<p>
		No Sakhalin token of the complete frame survives, but each of its ingredients is in
		place: the terminative <A w="pahno" gl="until, as far as" /> is frequent
		(<Xr ch="postpositions" s="ablative">§11.4</Xr>) and takes a gap-headed relative as
		its complement in (7); <A w="isam" gl="not exist" /> is the everyday negative
		existential (<Xr ch="negation" s="negative-existential">§17.4</Xr>); and degree
		adjuncts belong to the strandable set. We accordingly read the absence as a gap in
		the record rather than in the grammar — though, as elsewhere, confirmation would
		take a token of the shape [X <i lang="ain">pahno</i> Y <i lang="ain">isam</i>] Y
		<i lang="ain">ne</i>, which the surviving texts do not provide.
	</p>
</S>

<S t="Headless relatives" id="headless">
	<p>
		With the bound nouns <i lang="ain">pe ~ p</i> ‘one, thing’ and
		<i lang="ain">hi</i> ‘thing, time, place’ as heads, relativization yields the
		participant and event nominalizations of
		<Xr ch="nominalization" s="pe">§20.1</Xr> — ‘the one that…’, ‘the fact that…’.
		These heads also combine with the possessive suffix, as in
		<i lang="ain">an=oyra-pe-he</i> ‘the things we had forgotten’
		(<Ref k="murasaki1979" p="51" />; <Ref k="bugaeva2016" p="106" paren />). The chain
		noun &gt; bound noun &gt; nominalizer is the same grammaticalization that produced
		the negator <i lang="ain">hanneh(ka)</i> and the topic particle
		<i lang="ain">neampe</i> (<Xr ch="negation" s="forms">§17.1</Xr>;
		<Xr ch="information-structure">Chapter 24</Xr>).
	</p>
</S>

<S t="Relative clauses in the noun phrase" id="np-position">
	<p>
		Within the noun phrase, the relative clause follows demonstratives and numerals and
		immediately precedes the head (<Xr ch="clause-structure" s="np-order">§19.3</Xr>);
		demonstrative plus relative is common in narrative for re-identifying referents
		(<i lang="ain">neya cise ohta an monimahpo</i>-type phrases;
		<Xr ch="pronouns-and-demonstratives" s="demonstratives">§9.3</Xr>). The corpus
		gives no evidence of a restrictive–non-restrictive contrast in form; both readings
		are carried by the same construction and resolved in context. Hokkaidō Ainu allows
		relativization to interact with noun incorporation in ways that have attracted
		theoretical attention <Ref k="dalcorso2020" paren />; the Sakhalin corpus offers
		parallel ingredients — compare the incorporated modifier in (1) — but no studied
		cases, and we leave the interaction as an open question: settling it would require
		elicitation or a larger text base than survives.
	</p>
</S>
