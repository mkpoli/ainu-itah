	<p>
		The negative existential <A w="isam" /> (East-coast spelling often
		<i lang="ain">isan</i>) covers ‘there is no X’, ‘X is absent, away’, and — with a
		possessor — ‘X has no Y’:
	</p>
	<Ex
		m="tani nuca isan renkayne"
		g="now Russian not.exist because"
		tr="now that the Russians are gone…"
		cite="tangiku2001:Letter 1"
		dial="ES"
		place="Otasan (Sentoku Tarōji)"
	/>
	<Ex
		m="maci-hi isan teh okaaketa"
		g="wife-POSS not.exist and afterwards"
		tr="when his wife was away, then…"
		cite="murasaki2001:text 52"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		Following another verb linked with <i lang="ain">wa</i>, <i lang="ain">isam</i> has
		grammaticalized into a conclusive aspect marker, ‘do completely, off, away’
		(<Ref k="dalcorso2025neg" p="41" />; <Xr ch="tense-aspect-mood">Chapter 15</Xr>).
		The pattern is extremely common in Asai Take's narratives:
	</p>
	<Ex
		m="hekimoh makanu wa isam manu"
		g="uphill go.up and not.exist REP"
		tr="(the crow) went off up into the hills, it is said."
		cite="murasaki2001:text 5"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
</S>

<S t="Prohibitives, apprehensives, and irrealis negation" id="prohibitive">
	<p>
		The prohibitive is formed by placing <i lang="ain">hanka</i> (West coast) or
		<i lang="ain">hankayki</i> (East coast) before a verb unmarked for person, exactly
		like the affirmative imperative (<Xr ch="tense-aspect-mood">Chapter 15</Xr>); the
		command may be reinforced by a final particle such as <i lang="ain">wa(a)</i>,
		<i lang="ain">kanne</i>, or <i lang="ain">yan</i>
		<Ref k="dalcorso2025neg" p="53–54" paren />. Sakhalin thus builds its prohibitive
		from the ordinary negative element, whereas the Hokkaidō dialects use the dedicated
		prohibitive word <i lang="ain">iteki</i> and its variants
		<Ref k="dalcorso2025neg" p="54" paren />. The source of the Sakhalin marker is
		typologically remarkable: prohibitives are usually recruited from verbs of abstaining
		or stopping, from modals and desire predicates, or from imperative morphology, whereas
		<i lang="ain">hanka(yki)</i> is the negation of the additive particle
		<i lang="ain">ka(yki)</i> ‘even’, as the table of forms above records, a path with only
		scattered parallels elsewhere
		and one that Dal Corso traces through the negative contrastive-focus use of negated
		particles <Ref k="dalcorso2025neg" p="60" paren />. The segmentation itself is old:
		Chiri already parsed the form as negative <i lang="ain">ham</i> plus emphatic
		<i lang="ain">ka</i> <Ref k="chiri1942" p="536" paren />.
	</p>
	<Ex
		m="suy taa hanka oman kanne teere wa"
		g="again there PROH go.SG FIN wait FIN"
		tr="Don't go off again — wait!"
		cite="murasaki2001:text 5"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		In the West-coast corpus <i lang="ain">hanka</i> is not confined to commands
		addressed to an unmarked verb. The corpus shows it with person-marked verbs in
		directive contexts, where the negated clause functions adverbially; we take this as
		a bridge between prohibition and general irrealis negation:
	</p>
	<Ex
		m="hemata e-nukara-ha hanka e-siina kanne yee wa"
		g="what 2SG-see-POSS NEG 2SG-hide FIN say FIN"
		tr="Tell us what you saw, without hiding it!"
		cite="murasaki2001:text 34"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		The East-coast dialect of Hunup places <i lang="ain">hanna</i> (variant
		<i lang="ain">hana</i>) before a verb to express apprehension — ‘lest…’ — and, when
		the verb carries the intentional auxiliary <i lang="ain">kusu iki</i>, negative
		intention <Ref k="dalcorso2025neg" p="54–55" paren />:
	</p>
	<Ex
		m="hanna mawa-an-ike ta wen kiren-or-ne an-kana-te an-e-kun-pe ka ham=ne yawa"
		g="NEG starve-INDEF.S-and this be.poor Tungus-place-to INDEF.A-ask-and INDEF.A-eat-COND-thing even NEG=COP FIN"
		tr="Lest I starve, should I ask this poor Tungus [for food] and [then find out] it is not something I would eat?"
		cite="pilsudski1912:139; dalcorso2025neg:54–55"
		dial="ES"
		place="Hunup"
		note="Apprehensive hanna; the matrix clause is itself a negated equative with ham=ne."
	/>
	<p>
		The same dialect uses <i lang="ain">hannah</i> to negate the condition of a
		conditional sentence. The protasis closes with the causal-final linker
		<i lang="ain">kusu</i> and the topic particle <i lang="ain">neyke</i>;
		<i lang="ain">hannah</i> follows, before a second <i lang="ain">kusu</i> that
		partially restates the conditional nexus — in effect the negative-focus syntax of the
		next section turned on a whole clause. Where the apodosis predicate is nominalized and
		bears the conditional suffix <i lang="ain">-kun</i>, as below, the sentence is
		counterfactual: the outcome failed to come about because the condition never held
		<Ref k="dalcorso2025neg" p="55–56" paren />.
	</p>
	<Ex
		m="tan ohacisuye tan mosiri-kes pahno an kusu neyke hannah kusu e-ramu-mo-kun-pe kayki ham=ne manu"
		g="this empty.house.spirit this world-end until exist.SG because TOP NEG because APPL-soul-be.quiet-COND-thing even NEG=COP REP"
		tr="If the empty-house spirit had lived until the end of times, that [would] not have been something [people] would have felt safe about."
		cite="pilsudski1912:110; dalcorso2025neg:55–56"
		dial="ES"
		place="Hunup"
	/>
</S>

<S t="Caritive and focus negation" id="focus">
	<p>
		The form <i lang="ain">hamo ~ hamu</i> — historically <i lang="ain">ham</i> plus the
		adverbializer <i lang="ain">-no</i> — negates a bare, non-finite verb that stands in
		an adverbial relation to the following predicate, ‘without doing’
		(<Ref k="murasaki1979" p="116" />; <Ref k="dalcorso2025neg" p="56" paren />). No
		clause linker appears, and the negated verb carries no person marking; the caritive
		form itself signals the dependency (<Xr ch="clause-linking">Chapter 23</Xr>).
	</p>
	<Ex
		m="tu to pahno re to pahno ham ipe makap-an"
		g="two day until three day until NEG eat go.uphill.PL-INDEF.S"
		tr="For two, three days I went on uphill without eating."
		cite="pilsudski1912:123; dalcorso2025neg:56"
		dial="ES"
	/>
	<p>
		No split form *<i lang="ain">ham no</i> is attested, so the segmentation is a
		reconstruction; it is nonetheless well supported. The <i lang="ain">-no</i> is the
		ordinary manner adverbializer, and the missing nasal follows from the same lenition
		seen in <i lang="ain">ponno</i> ~ <i lang="ain">pono</i> ‘a little’; the Hokkaidō
		negator <i lang="ain">somo</i> continues the same Proto-Ainu verb through just such an
		adverbialized form, *<i lang="ain">sam-no</i>, whose final stress explains its rounded
		vowel; and <i lang="ain">hamo</i> itself keeps first-syllable stress despite its CV.CV
		shape, the unstressed final vowel rising to give the variant <i lang="ain">hamu</i> —
		the profile of a stressed negator univerbated with a stressless suffix
		<Ref k="dalcorso2025neg" p="44–45, 47" paren />. Chiri supplies an inner-Ainu
		parallel: in Tokachi (Hokkaidō), <i lang="ain">sem-ki-no</i> ‘without doing’ contracts
		to <i lang="ain">sinkino</i>, the <i lang="ain">s</i>-form negator feeding a caritive
		adverbial in <i lang="ain">-no</i> by the same route
		<Ref k="chiri1942" p="536" paren />.
	</p>
	<p>
		Finally, the East-coast texts attest <i lang="ain">ham=</i> directly on a noun —
