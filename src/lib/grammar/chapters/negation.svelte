	<p>
		The same drift can be read against time within the western records themselves. Of the
		negation tokens in Dobrotvorsky’s West-coast materials of the 1860s–70s, the preverbal
		clitic strategy still accounts for 68.4%; in Murasaki’s 1983 data it has fallen to
		3.7%, while the postverbal light-verb construction has risen from zero to 49%
		<Ref k="dalcorso2021" p="223" paren />. Taken at face value, the figures compress the
		synthetic-to-analytic turnover into roughly forty years, which Dal Corso himself flags
		as suspiciously fast; more plausibly the two dialect groups moved at different speeds,
		the East conservative and the West innovating, so that the staged cline is as much a
		geographic gradient as a chronological sequence — an implicational ordering of
		overlapping strategies rather than a dated relay
		<Ref k="dalcorso2025neg" p="61" paren />.
	</p>
	<p>
		The Hokkaidō negator <i lang="ain">somo</i> itself surfaces in Sakhalin sources only
		marginally: Dal Corso finds it in Piłsudski's Tunayci texts, told by speakers who had
		lived in Hokkaidō, and treats it as contact influence
		<Ref k="dalcorso2025neg" p="41" paren />. The corpus adds one more such setting: in
		Sentoku Tarōji's letters of 1906 the fixed conditional phrase
		<i lang="ain">somo ne ciki</i> ‘if (it) is not (so)’ appears beside native
		<i lang="ain">ham</i>-negation, fitting the same profile of a literate East-coast
		writer in close contact with Hokkaidō usage.
	</p>
