	<p>
		The proclitic shows the regular nasal assimilation described in
		<Xr ch="morphophonology">Chapter 5</Xr>: /m/ assimilates to a following coronal, so
		<i lang="ain">ham=ne</i> yields <i lang="ain">hanne</i>, <i lang="ain">ham=ki</i>
		yields <i lang="ain">hanki</i>, and <i lang="ain">ham=suy</i> ‘not again’ yields
		<i lang="ain">haysuy</i> <Ref k="dalcorso2025neg" p="45–46" paren />. Two further
		observations bear on the clitic analysis. The first is prosodic:
		<i lang="ain">ham=</i> ordinarily fuses with its host into one phonological word,
		re-syllabifying it and drawing the stress onto itself (<i lang="ain">hánne</i>,
		<i lang="ain">háysuy</i>); but Piłsudski’s accented spelling <i lang="ain">hám
		utara</i> for <i lang="ain">ham=utara</i> ‘not the people’ points to a pronunciation
		[ˈha.mu.ta.ɾa] where the regular stress rules of a fused CV.CV word would give
		*[ha.ˈmu.ta.ɾa] — a hint that the negator could still carry stress of its own, as
		befits an erstwhile independent word recently reduced to clisis
		<Ref k="dalcorso2025neg" p="45–46" paren />. The second is positional: on verbs,
		<i lang="ain">ham=</i> stands outside the person prefixes — Piłsudski records
		<i lang="ain">hamecinu</i> ‘you did not listen’, that is
		<i lang="ain">ham=eci-nu</i>, and <i lang="ain">hamanrayki</i>
		‘I did not kill’, that is <i lang="ain">ham=an-rayki</i> — a position at odds with
		ordinary verbal prefixes such as the applicative or the reflexive, which follow
		agreement. Only two tokens in the whole record show the reverse order
		person–<i lang="ain">ham</i>: <i lang="ain">anameyaynu</i>
		(<i lang="ain">an-ham-e-yay-nu</i>) ‘I did not pay attention to it’, from Takoye
		<Ref k="majewicz1998" p="278" paren />, and <i lang="ain">’anhancinukahsiri</i> ‘a
		land I did not know’, from Rayciska. Dal Corso leaves open whether these show
		<i lang="ain">ham=</i> on its way to becoming a true inflectional prefix or merely a
		negator frozen inside a lexicalized word form, on the model of Hokkaidō
		<i lang="ain">somoipere</i> ‘not feed’ <Ref k="dalcorso2025neg" p="46" paren />.
	</p>
	<Ex
		m="ham=eci-nu yayne"
		g="NEG=2PL-hear and.then"
		tr="you did not listen [to my warning], and then…"
		cite="pilsudski1912:121; dalcorso2025neg:50"
		dial="ES"
	/>
</S>

<S t="Standard negation" id="standard-negation">
	<p>
		Direct procliticization as in (1) is essentially an East-coast pattern: 19 of its 26
		verbal tokens come from East-coast texts, and in the West-coast corpus it survives
		only inside the folklore formula <i lang="ain">hancinukahsiri</i> ‘an unseen land’
		<Ref k="dalcorso2025neg" p="49–50" paren />. Everywhere else, declarative negation is
		analytic. The dominant construction — about a third of all negative tokens — places
		the negated verb first, usually followed by <A w="ka" gl="even" /> or its emphatic
		variant <i lang="ain">kayki</i>, and closes the clause with <i lang="ain">hanki</i>,
		the negated light verb ‘do’ (<Xr ch="verb-structure">Chapter 12</Xr> on
		<A w="ki" gl="do" />). The notional verb is a zero-nominalized object of
		<i lang="ain">ki</i>, and the two coasts disagree over where person marking goes. In
		the older layout, agreement sits on <i lang="ain">ki</i>, the syntactic head, and the
		notional verb stays bare; in the layout that dominates the West-coast corpus,
		agreement has migrated onto the nominalized notional verb, leaving
		<i lang="ain">ki</i> uninflected <Ref k="dalcorso2025neg" p="50" paren />. The next
		two examples show the contrast:
	</p>
	<Ex
		m="anoka neampe tohse ka ham=an-ki-no yay-nu-no-ka-an"
		g="1SG TOP sleep.NMLZ even NEG=INDEF.A-do-ADV REFL-listen-well-CAUS-INDEF.S"
		tr="As for me, I kept an ear out [for strange noises] without sleeping."
		cite="pilsudski1912:184; dalcorso2025neg:50"
		dial="ES"
		note="Agreement on the light verb ki; the notional verb tohse ‘sleep’ is bare."
	/>
	<Ex
		m="aynu kur-i an-nukara ka ham=kii"
		g="person figure-POSS INDEF.A-see.NMLZ even NEG=do"
		tr="I did not see the man’s figure."
		cite="kitahara2016:62; dalcorso2025neg:50"
		dial="WS"
		note="Agreement on the nominalized notional verb; ki carries only the negator."
	/>
	<p>
		Dal Corso reads the westward migration of agreement onto the notional verb as a sign
		that the construction was being reanalysed from two predicates into a single predicate
		with a postverbal negator <Ref k="dalcorso2021" paren />;
		<Ref k="dalcorso2025neg" p="57–58" paren />; agreement marked on both verbs at once occurs
		exactly once in the corpus <Ref k="dalcorso2025neg" p="50" paren />.
	</p>
	<p>
		The same light-verb frame admits the univerbated negators of the
		<i lang="ain">hanne</i> family in place of <i lang="ain">hanki</i>
		<Ref k="dalcorso2025neg" p="51–52" paren />:
	</p>
	<Ex
		m="nah kara-hci yahka ampene pirika hanneh kii manuu"
		g="so make-PL although at.all be.good NEG do REP"
		tr="Although they treated her so, she did not get better at all."
		cite="murasaki1976:58; dalcorso2025neg:51"
		dial="WS"
		place="Rayciska"
	/>
	<Ex
		m="si-moymoy-a ka hannehka ki"
		g="REFL-move-VBLZ even NEG do"
		tr="[The monster] did not move."
		cite="murasaki1976:99; dalcorso2025neg:51"
		dial="WS"
		place="Rayciska"
	/>
	<p>
		The same analytic logic appears in the Dobrotvorsky-era texts of the 1860s–70s, the
		oldest connected West-coast record, where <i lang="ain">ham</i> negates the light
		verb in the intentional construction <i lang="ain">kun(i) ki kusu</i>
		(<Xr ch="tense-aspect-mood">Chapter 15</Xr>):
	</p>
	<Ex
		m="maskin han neran itah ham an-ki kun ki kusu"
		g="overly NEG what.kind word NEG INDEF.A-do COND do because"
		tr="I do not intend to say any words of that kind at all."
		cite="dobrotvorsky1875; sakaguchi2021"
		dial="WS"
		place="West coast, central (recorded 1867–1872)"
		note="Note the doubled negation han … ham within one clause, a concord pattern not found in the twentieth-century recordings."
	/>
	<p>
		Alongside the light-verb pattern, the univerbated forms <i lang="ain">hanne</i> (East
		coast), <i lang="ain">hanneh</i>, and <i lang="ain">hannehka</i> (West coast) are
		used preverbally, immediately before the negated verb:
	</p>
	<Ex
		m="tani nuca renkayne aynu utara ta-ene ahkas kayki hanne ki"
		g="now Russian because.of person PL there-to walk even NEG do"
		tr="Now, because of the Russians, the Ainu do not travel there."
		cite="pilsudski1912:140; dalcorso2025neg:51"
		dial="ES"
	/>
	<Ex
		m="tani ahturi hannehka ko-tasko"
		g="now globeflower NEG APPL-tie"
		tr="Now she does not tie [the dried fish] to the globeflower stalks."
		cite="murasaki2001:112; dalcorso2025neg:51"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		The corpus offers no evidence for a dedicated interrogative negation: the strategies
		above appear unchanged in questions <Ref k="dalcorso2025neg" p="49" paren />.
		Negative indefinite readings — ‘not a single, none at all’ — are built with a
		numeral or interrogative plus <i lang="ain">ka(yki)</i> ‘even’ under a negated
		predicate (<Xr ch="information-structure">Chapter 24</Xr> on <A w="ka" />):
	</p>
	<Ex
		m="repohpe sine-p kayki hanneh kusa"
		g="seal one-NMLZ even NEG carry"
		tr="He did not bring home a single seal."
		cite="pilsudski1912:149; dalcorso2025neg:51"
		dial="ES"
	/>
</S>

<S t="Negation of the copula" id="copula-negation">
	<p>
		Equative clauses (<Xr ch="copula-and-existence">Chapter 18</Xr>) are negated on the
		East coast by procliticizing <i lang="ain">ham=</i> to the copula
		<A w="ne" />, yielding <i lang="ain">hanne</i>:
	</p>
	<Ex
		m="mahpo-ho-cin aynu po kayki ham=ne"
		g="daughter-POSS-PL person child even NEG=COP"
		tr="…[but] her daughters were not human children."
		cite="pilsudski1912:59; dalcorso2025neg:49"
		dial="ES"
	/>
	<p>
		In the West-coast recordings this is impossible — <i lang="ain">ham=</i> is no longer
		productive there — and the grammaticalized form <i lang="ain">hannehka</i> has taken
		over the function of a negative copula:
	</p>
	<Ex
		m="anihi humpecehpo hannehka"
		g="self puffer.fish NEG.COP"
		tr="He himself is not a puffer-fish."
		cite="murasaki2001:198; dalcorso2025neg:49"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
</S>

