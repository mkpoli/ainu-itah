<p>
	Negation is one of the areas where Sakhalin Ainu diverges most sharply from the other
	Ainu varieties, and one of the few that has received a dedicated modern treatment:
	<Ref k="dalcorso2021" /> surveys the development of the analytic negative constructions,
	and <Ref k="dalcorso2025neg" /> analyses the full system on the basis of 324 negative
	tokens drawn from the East- and West-coast corpora. This chapter describes the attested
	negators and their syntax, the negative existential, prohibitives and other irrealis
	negation, and the history of the system. The basic facts are these: all Sakhalin negators
	are built on a negative element <i lang="ain">ham</i>; the older pattern, best preserved
	on the East coast, attaches <i lang="ain">ham=</i> directly to the negated word; and the
	West-coast dialects of the twentieth-century recordings negate almost exclusively with
	analytic constructions, above all <A w="hannehka" /> and the postverbal
	<A w="hanki" /> light-verb pattern.
</p>

<S t="The negators and their composition" id="forms">
	<p>
		The negative forms attested in the corpus of Sakhalin Ainu are listed below with the
		morphological analyses of <Ref k="dalcorso2025neg" p="46–47" />. All of them contain
		the element <i lang="ain">ham</i>, which Dal Corso analyses as a proclitic
		<i lang="ain">ham=</i> on the grounds that it is not class-selective — it attaches to
		verbs, nouns, the copula, adverbs, and particles — and that it is phonologically
		dependent on its host.
	</p>
