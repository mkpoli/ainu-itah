<S t="History of the system" id="history">
	<p>
		Dal Corso reconstructs the whole family of negators from a Proto-Ainu stative
		transitive verb <i lang="ain">*sam</i> ‘not exist in (a place)’
		<Ref k="dalcorso2025neg" p="43–45" paren />. One line of development antipassivized
		the verb with <i lang="ain">i-</i> (<Xr ch="valency">Chapter 14</Xr>), yielding the
		negative existential <A w="isam" /> ‘not exist (anywhere)’; the other turned the bare
		verb into a negative marker placed before the negated element, which weakened to the
		proclitic <i lang="ain">ham=</i>, with regular <i lang="ain">*s</i> &gt; <i lang="ain">h</i>
		outside the neighbourhood of <i>i</i>. The Hokkaidō negator <i lang="ain">somo</i>
		continues the same verb through an adverbialized form <i lang="ain">*sam-no</i>; the
		Sakhalin caritive <i lang="ain">hamo</i> is its direct cognate, as set out in the
		previous section.
	</p>
	<p>
		Dal Corso sets this prehistory within two named typological cycles. The step from
		negative verb to negative marker instantiates the Givón Cycle, by which erstwhile
		negative verbs are worn down into negators <Ref k="givon1978" paren />. On Croft’s
		Negative Existential Cycle <Ref k="croft1991" paren />, the reconstructed stage in
		which one form, *<i lang="ain">sam</i>, served as both verbal negator and negative
		existential is Type C — the rarest of Croft’s types, reported otherwise mainly from
		Dravidian and Polynesian languages <Ref k="veselinova2016" paren />
		<Ref k="dalcorso2025neg" p="56–57" paren />. The split
		into existential <i lang="ain">isam</i> and verbal <i lang="ain">ham=</i>, driven by
		the antipassive and by the sound change, took Sakhalin Ainu directly to Type B, with
		distinct negators for the two domains, skipping the intermediate Type A in which one
		strategy covers both; since the stages of the cycle need not be sequential, the skip
		is unexceptional <Ref k="dalcorso2025neg" p="57" paren />.
	</p>
	<p>
		From procliticized <i lang="ain">ham=</i> (Stage 1) the analytic system then unfolded
		in implicational order <Ref k="dalcorso2025neg" p="57–59" paren />. Negation of the
		copula gave <i lang="ain">hanne</i>, which in the East was reanalysed as a preverbal
		verbal negator and drawn into the light-verb frame to mark emphasis (Stage 2); because
		<i lang="ain">hanne</i> was still felt to be copular, the construction was renewed
		with overt nominalization as <i lang="ain">hanneh</i> (<i lang="ain">ham=ne-p</i>) and
		emphatic <i lang="ain">hannehka</i> (Stage 3). In the West, where
		<i lang="ain">ham=</i> had ceased to be productive, preverbal
		<i lang="ain">hanneh(ka)</i> was then pressed into service as the plain, non-emphatic
		negator (Stage 4) — an emphatic form bleaching into the default, the profile familiar
		from the Jespersen cycle of negator renewal. Finally, by ellipsis of
		<i lang="ain">ki</i>, <i lang="ain">hannehka</i> came to stand as a negative copula in
		its own right (Stage 5): a verbal negator turning copular with no new affirmative
		existential beside it, a backward transition in the Negative Existential Cycle that
		Dal Corso suggests is so far unparalleled <Ref k="dalcorso2025neg" p="58–59" paren />.
		The light-verb frame itself need not be a purely internal invention: negation through
		a nominalized predicate governed by a dummy verb recurs in Hokkaidō Ainu, in Nivkh,
		and in Chukotko-Kamchatkan, and diffusion around the southern Okhotsk Sea is a live
		possibility <Ref k="dalcorso2021" p="223–225" paren />;
		<Ref k="dalcorso2025neg" p="57–58" paren />.
	</p>
	<p>
		The corpus counts lay the cline out on the ground. Every later stage is the stronger
		represented the further west and the later the recording: synthetic
		<i lang="ain">ham=</i> and <i lang="ain">hanne</i> are essentially Eastern,
		<i lang="ain">hanki</i> and <i lang="ain">hannehka</i> overwhelmingly or exclusively
		Western <Ref k="dalcorso2025neg" p="47" paren />.
	</p>
