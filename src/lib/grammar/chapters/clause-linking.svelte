<p>
	Sakhalin Ainu links clauses with a set of clause-final particles —
	<A w="teh" /> and <A w="wa" /> ‘and’, <A w="yayne" /> ‘while, until eventually’,
	<A w="kusu" /> ‘because, in order to’, <A w="yahka" /> ‘although’,
	<A w="anah" /> and <A w="cikin" /> ‘if, when’, and others — and with a parallel set of
	sentence-initial connectives that carry narrative from sentence to sentence. Furukawa
	counted some forty clause-final linkers in Rayciska alone
	<Ref k="furukawa1967" p="110" paren />; their status between coordination and
	subordination has never been settled for the language
	<Ref k="dalcorso2025neg" p="40, 49" paren />. We treat the system as clause chaining:
	the linked verb keeps its person marking, takes no independent final particle or
	evidential, and shares illocutionary force with the clause that closes the chain — a
	cosubordination profile rather than either pole. Within the chain, the general ‘and’
	linkers sort themselves along a single semantic scale, the temporal gap between the
	linked events (§23.1–23.2); the final section (§23.7) then reads Furukawa's forty
	against Chiri's catalogue and the corpus. The one linker that is formally bound
	to dependency is negative: caritive <i lang="ain">hamo</i> ‘without doing’ marks its
	clause as non-finite all by itself (<Xr ch="negation" s="focus">§17.6</Xr>).
</p>

<S t="Sequential linkage: wa, teh, and the temporal gap" id="teh">
	<p>
		The two general ‘and’ linkers divide the territory by event structure, and Furukawa's
		elicitation captured the line between them. <i lang="ain">wa</i> joins actions with no
		gap, so that the two fuse into a single event; <i lang="ain">teh</i> sequences
		distinct actions with a perceptible interval between them; the narrative-succession
		linkers of §23.2 then push the second event into a new episode altogether. Her
		consultant accepted both of the following but rejected
		<i lang="ain">*ku-koro wa ku-eh</i>, where buying and coming are separate events
		<Ref k="furukawa1967" p="110–111" paren />:
	</p>
	<Ex
		m="ku-koro-wa eci-konte"
		g="1SG-buy-and 1SG>2SG-give"
		tr="I bought it and gave it to you."
		cite="furukawa1967:110"
		dial="WS"
		place="Rayciska (Fujiyama Haru)"
		note="On the portmanteau eci- ‘I…you’ see §13.4."
	/>
	<Ex
		m="ku-koro-teh ku-eh"
		g="1SG-buy-and 1SG-come.SG"
		tr="I bought it and came (here)."
		cite="furukawa1967:111"
		dial="WS"
		place="Rayciska (Fujiyama Haru)"
	/>
	<p>
		Both linkers feed grammaticalization: <i lang="ain">teh an</i> is the stative
		periphrasis and <i lang="ain">wa isam</i> the conclusive
		(<Xr ch="tense-aspect-mood">Chapter 15</Xr>;
		<Xr ch="negation" s="negative-existential">§17.4</Xr>). In Asai Take's narration
		<i lang="ain">teh</i> is the default sequencer, often several to a sentence; the
		Sentoku letters use <i lang="ain">wa</i> more freely. In
		<i lang="ain">nukara konte wa</i> ‘please read it’ (Letter 1), though, the
		<i lang="ain">wa</i> is the request-softening final particle that follows the
		benefactive, not a sequencer (<Xr ch="texts">Chapter 26</Xr>).
	</p>
	<p>
		The interval that <i lang="ain">teh</i> asserts can be made explicit by a following
		<A w="orowa" /> ‘after that’ — transparently the possessed relational noun
		<i lang="ain">oro</i> ‘place (of it)’ plus the ablative <i lang="ain">wa</i> ‘from’
		(<Xr ch="postpositions">Chapter 11</Xr>), so literally ‘from there’. The reinforced
		sequence <i lang="ain">teh orowa</i> ‘and after that’ keeps the chain open for
		further links, here a purpose clause in <i lang="ain">kusu</i> before the single
		finite close:
	</p>
	<Ex
		m="e=niina teh orowa e=i-wooneka kusu teeta e=san-i-hi ne=an ike=aa"
		g="2SG=gather.wood and.then after.that 2SG=INDEF.O-check.on PURP here 2SG=go.down.SG-EP-PERS COP=PFV CONJ=FIN"
		tr="You gathered firewood and then came down here to check on us, didn't you?"
		cite="dalcorso2018:210"
		dial="WS"
		note="teh orowa = explicit delayed sequence; kusu adds a purpose clause; the whole chain closes on one nominalized predicate with personal-knowledge -hi (Chapter 16)."
	/>
</S>

<S t="Narrative succession: (h)ike and (h)ine" id="ike">
	<p>
		At the wide end of the temporal-gap scale stand the narrative-succession linkers.
		<A w="ike" /> ‘and (so), when’ — historically the nominalizer
		<i lang="ain">-(h)ike</i> ‘the one that…’ reanalysed as a linker — is the Sakhalin
		workhorse: Chiri notes the <i lang="ain">h-</i> doublet and states that Sakhalin
		<i lang="ain">(h)ike</i> serves ‘in the same sense as <i lang="ain">wa</i>,
		<i lang="ain">hine</i>, <i lang="ain">tek</i>’, in which use it is best regarded as a
		conjunctive particle outright <Ref k="chiri1942" p="§51, §61" paren />. In narrative
		it typically chains a backgrounded presentational clause onto the event line:
	</p>
	<Ex
		m="re aynu okay-ahci ike ceh-ki-hci ko e-hci"
		g="three person exist.PL-PL and fish-catch-PL whenever eat-PL"
		tr="Three men were living (there), and whenever they caught fish they would eat them."
		cite="kitahara2016"
		dial="WS"
		note="ike chains the presentational existential to the main event line; on habitual-correlative ko(h) see Chapter 15. Segmentation ours."
	/>
	<p>
		Its partner <A w="hine" /> ~ <i lang="ain">ine</i>, which Chiri derives from
		<i lang="ain">a-ine</i>, is the great narrative converb of Hokkaidō but thin in the
		Sakhalin texts, where <i lang="ain">ike</i> — and its reportative fusion
		<i lang="ain">manuyke</i> (§23.6) — does the corresponding work;
		<i lang="ain">hine</i> survives most clearly in the stative periphrasis
		<i lang="ain">hine an</i> that Chiri lists beside <i lang="ain">teh an</i>
		(<Xr ch="tense-aspect-mood" s="resultative">Chapter 15</Xr>). The comparative
		picture runs through <i lang="ain">teh</i> itself: in Saru the cognate
		<i lang="ain">tek</i> narrowed into an auxiliary ‘do for a moment’, leaving sequence
		to <i lang="ain">wa</i> and <i lang="ain">hine</i>, while Sakhalin
		<i lang="ain">teh</i> keeps the older linking value
		<Ref k="bugaeva2022" paren />. Chiri's catalogue further records a
		counter-expectational <i lang="ain">a(h)i</i> ~ <i lang="ain">a(h)ike</i> ‘…when
		(unexpectedly), …but’ built on the same formative
		<Ref k="chiri1942" p="§39" paren />; the corpus does not let us draw its line
		against plain <i lang="ain">ike</i>, a point taken up in §23.7.
	</p>
</S>

<S t="Temporal and durative linkers" id="yayne">
	<p>
		<i lang="ain">yayne</i> marks a prolonged event overtaken by what follows — ‘while
		doing, until eventually’:
	</p>
	<Ex
		m="ku-henoye yayne ku-mokoro hemaka"
		g="1SG-doze while.eventually 1SG-sleep finish"
		tr="While I dozed, I eventually fell fast asleep."
		cite="furukawa1967:111"
		dial="WS"
		place="Rayciska (Fujiyama Haru)"
	/>
	<p>
		The same family supplies the sentence-initial <i lang="ain">(an)ayne</i> ‘in time,
		meanwhile’ of §23.6. <i lang="ain">kanne</i> links a simultaneous backgrounded
		action, ‘while’ —
		<i lang="ain">kuwa koro kanne ahkas</i> ‘walks holding a stick’
		(<Xr ch="clause-structure" s="word-order">§19.2</Xr>) — beside its final-particle
		use in requests (<Xr ch="clause-structure" s="clause-types">§19.6</Xr>). Posterior
		‘after’ is built on the nominal <i lang="ain">okaaketa</i> ‘in the aftermath of’,
		as in <i lang="ain">macihi isan teh okaaketa</i> ‘after his wife had gone out’
		(<Xr ch="negation" s="negative-existential">§17.4</Xr>); anteriority needs no
		dedicated marker beyond <i lang="ain">teh</i>.
	</p>
</S>

<S t="Causal and purposive linkage" id="kusu">
	<p>
		<i lang="ain">kusu</i> covers cause and purpose, the reading resolved by the
		relation between the clauses; with the copula it builds the intentional future
		<i lang="ain">kusu ne</i> and with <i lang="ain">iki</i> the assertive future
		(<Xr ch="tense-aspect-mood">Chapter 15</Xr>). Nominal cause is carried by
		<i lang="ain">renkayne</i> ‘owing to’, which also governs clauses, as in Sentoku's
		<i lang="ain">tani nuca isan renkayne</i> ‘now that the Russians are gone’
		(<Xr ch="negation" s="negative-existential">§17.4</Xr>). Negative purpose uses
		<i lang="ain">kuni no</i> (<Xr ch="complementation" s="kuni">§22.2</Xr>). A single
		sentence of the Dobrotvorsky-era texts stacks causal <i lang="ain">ne kusu</i> with
		a concessive:
	</p>
	<Ex
		m="sine utahta ne kusu porono ham e-ye yahka pirika nankoro pe ne"
		g="one family COP because much NEG 2SG-say although be.good probably NMLZ COP"
		tr="Since we are of one family, even if you do not say much, it will be well."
		cite="dobrotvorsky1875; sakaguchi2021"
		dial="WS"
		place="West coast, central (recorded 1867–1872)"
	/>
</S>

<S t="Conditionals and concessives" id="conditional">
	<p>
		The conditional linkers are <i lang="ain">anah(ka)</i> and
		<i lang="ain">cik(in) ~ ciki</i>; both follow the bare protasis verb.
		<i lang="ain">anah</i> shades from ‘if’ into topic marking, a polysemy taken up in
		<Xr ch="information-structure">Chapter 24</Xr>:
	</p>
	<Ex
		m="siripirika anah ku-oman kusu neya"
		g="weather.be.good if 1SG-go.SG PURP FIN"
		tr="If the weather were good, I would go."
		cite="furukawa1967:108"
		dial="WS"
		place="Rayciska (Fujiyama Haru)"
		note="kusu plus the final particle neya expresses an unrealizable wish (Furukawa 1967: 108)."
	/>
	<Ex
		m="sonko an=omante cikin nukara konte wa"
		g="message INDEF.A=send if see give FIN"
		tr="I send you word — when it reaches you, please read it."
		cite="tangiku2001:Letter 1"
		dial="ES"
		place="Otasan (Sentoku Tarōji)"
		note="konte after a verb is benefactive ‘do for someone’; cikin also appears in the contact phrase somo ne ciki ‘if not’ (§17.7)."
	/>
	<p>
		The East-coast texts add the heavier protasis marker <i lang="ain">kusu neyke</i>,
		which interacts with negation in the <i lang="ain">hannah kusu</i> conditional
		construction (<Xr ch="negation" s="prohibitive">§17.5</Xr>;
		<Ref k="dalcorso2025neg" p="55–56" paren />). The concessive is
		<i lang="ain">yahka</i> ‘although, even if’, as in (6) and Furukawa's
		<i lang="ain">ekasre seeseh yahka wen</i> ‘even too much warmth is bad’
		<Ref k="furukawa1967" p="111" paren />.
	</p>
</S>

<S t="Narrative chaining" id="chaining">
	<p>
		Connected narrative organizes these links into a recognizable rhythm. Clauses chain
		with <i lang="ain">teh</i> and <i lang="ain">ike</i> ‘and, when’ up to a finite
		verb capped by the reportative (<Xr ch="evidentiality">Chapter 16</Xr>); the next
		sentence then re-opens by repeating the last verb of the previous one — tail-head
		linkage, ubiquitous in Asai Take's telling:
	</p>
	<Ex
		m="otakaa-ta san manu. otakaa-ta san ike"
		g="beach-LOC go.down.SG REP beach-LOC go.down.SG and"
		tr="He went down to the beach. Going down to the beach, (he swam…)"
		cite="murasaki2001:text 5"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		The fusion of the reportative with <i lang="ain">ike</i> yields the narrative
		linker <i lang="ain">manuyke</i> ‘…, it is said, and’, which keeps a chain running
		across what is formally a sentence boundary:
	</p>
	<Ex
		m="reske-hci yayne poro-hci manuyke"
		g="raise-PL while.eventually be.big-PL REP.and"
		tr="they raised them and they eventually grew up, and then…"
		cite="murasaki2001:text 19"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		Above the clause, a closed set of sentence-initial connectives — built from the
		copula, the existential, and <i lang="ain">orowa</i> ‘from there, then’ — manages
		the larger discourse: <i lang="ain">neeteh</i> and <i lang="ain">nahteh</i> ‘and
		then’, <i lang="ain">orowa(no)</i> ‘after that’, <i lang="ain">anayne</i>
		‘meanwhile, in time’, <i lang="ain">neyke</i> ‘and so’. They open the majority of
		sentences in the Asai Take corpus, often doubled
		(<i lang="ain">neeteh orowa tani…</i>); this inventory and its layering are our
		generalisation from the corpus. The same forms carry the topic-resumption work
		described in <Xr ch="information-structure">Chapter 24</Xr>.
	</p>
</S>

