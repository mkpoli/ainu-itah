		</tbody>
	</table>
	<p>
		Within the possessed paradigm, then, number marking is close to obligatory: a possessed
		noun without <i lang="ain">=ahcin</i> is singular — or distributively singular, one per
		possessor, as <i lang="ain">an-seta-ha</i> shows. The same distributive singular is
		textually attested: Yamabe writes <i lang="ain">an-tek-i naa uk-ahsi</i> ‘they took our
		hands (each one hand)’ <Ref k="sakaguchi2020ahcin" p="6" paren />, and Hattori elicited:
	</p>
	<Ex
		m="anokay-ahcin an-tek-ihi ohta ama yan"
		g="INDEF-PL INDEF.A-hand-POSS in put IMP.PL"
		tr="Put it into our hands (into each one's hand)."
		note="Elicited by Hattori from his Rayciska consultants; tek-ihi remains unmarked because each person's single hand is meant."
		cite="hattori1961:7; sakaguchi2020ahcin:5"
		dial="WS"
		place="Rayciska"
	/>
	<p>
		The suffix is equally at home in the west-coast narrative corpus, on kin terms, body
		parts and belongings:
	</p>
	<Ex
		m="kotan-ta sine acahcipo taa mici-hi-hcin tura an manu"
		g="village-LOC one old.woman there grandchild-POSS-PL with exist.SG REP"
		tr="In a village an old woman lived together with her grandchildren."
		note="The next sentence specifies re horokewpo ‘three young men’; an agrees with the singular subject."
		cite="murasaki2001:text 51"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		Semantically <Ref k="sakaguchi2020ahcin" p="8–9, 16–17" /> shows that
		<i lang="ain">=ahcin</i> expresses additive plurality only — sets of like objects:
		siblings, fingers, shoes, pots. This is why it never attaches to personal names, whose
		referent is unique, and it is the key to the contrast with
		<i lang="ain">-utara</i> drawn by Murasaki from her Fujiyama Haru materials:
		<i lang="ain">ku=mici-hi=hcin</i> ‘my grandchildren’ (each one my grandchild) against
		<i lang="ain">mit-utara</i> ‘the grandchildren (and such small fry)’, where the
		associative reading is available <Ref k="sakaguchi2020ahcin" p="15–16" paren />. A
		marginal associative residue does exist: <i lang="ain">an-(h)ok-ho=cin</i>, literally
		‘my husbands’, refers in its tale to the husband and his younger brother, and Chiri
		glossed precisely this type ‘the husband and his companion’
		<Ref k="sakaguchi2020ahcin" p="8–9" paren />. In Asai Take's tales we likewise find
		<i lang="ain">aaca-ha-hcin</i> ‘her father and his household’ rendered associatively by
		Murasaki; we analyse such kin-term tokens as the same marginal associative extension,
		expected on relational nouns whose referents come in households, and keep the additive
		characterisation for the core. One further host type is the evidential nominalizers
		<i lang="ain">hawe(he)</i> ‘voice’, <i lang="ain">humi(hi)</i> ‘sound, feel’,
		<i lang="ain">sir(ihi)</i> ‘appearance’, <i lang="ain">ruu(he)</i> ‘trace’
		(<Xr ch="evidentiality">Chapter 16</Xr>, <Xr ch="nominalization">Chapter 20</Xr>):
		formally possessive forms, they take <i lang="ain">=ahcin</i> when the nominalized
		event is plural — and then the verb inside the clause carries no
		<i lang="ain">-hci</i>, the two markers being in complementary service
		<Ref k="sakaguchi2020ahcin" p="9–12" paren />:
	</p>
	<Ex
		m="nispa-utara pinaponne u-moymoye hawhe-sin an manu"
		g="chief-PL secretly RECP-shake.awake voice.NMLZ-PL exist.SG REP"
		tr="There came the sound of the chiefs secretly shaking one another awake."
		cite="pilsudski1912:text 21, l. 57; sakaguchi2020ahcin:10"
		dial="ES"
	/>
	<Ex
		m="nea nea oyas-utah tani ruwesan okaakara makah siri-hcin an manu"
		g="that that demon-PL now shore.path along go.uphill.PL appearance.NMLZ-PL exist.SG REP"
		tr="And then those demons were just coming up along the path from the shore."
		cite="tamura2003:84; sakaguchi2020ahcin:12"
		dial="SA"
		place="Yamada Hayo"
	/>
	<p>
		Outside these types only the reciprocal verb <i lang="ain">uriwahne</i> ‘be siblings to
		one another’ takes the suffix, in nominal use: <i lang="ain">uriwahne-hcin</i>
		‘brothers and sisters’ <Ref k="hattori1964" p="42" paren />,
		<Ref k="sakaguchi2020ahcin" p="12–13" paren />. The element is formally similar to the
		verbal plural <i lang="ain">-hci</i> of §8.3, and Chiri treated the two as one morpheme
		<Ref k="chiri1942" p="§41" paren />; with <Ref k="sakaguchi2024" p="103" /> we keep them
		apart synchronically — their hosts and shapes differ — while accepting the historical
		connection.
	</p>
</S>

<S t="Number in the verb: the plural clitic -hci" id="verbal-number">
	<p>
		Verbs mark number twice over: a small set of stems alternates or supplements for number
		(§8.4), and any finite verb may take the plural clitic <i lang="ain">-hci</i>
		(<i lang="ain">-ahci</i> after consonants; east coast also <i lang="ain">-(a)hsi</i>,
		<i lang="ain">-si</i>, west coast south <i lang="ain">-cin</i>), whose form rules Chiri
		established <Ref k="chiri1942" p="§41" paren /> and whose syntax
		<Ref k="sakaguchi2024" p="77–102" /> has now clarified. The clitic attaches outside the
		person suffixes (<i lang="ain">paye-an=ahci</i> ‘we went’), follows even auxiliaries,
		and disappears under nominalization, exactly like person marking — Sakaguchi therefore
		ranks it with the clitic-like person markers rather than with derivational plural stems
		<Ref k="sakaguchi2024" p="79–80, 102" paren />:
	</p>
	<Ex
		m="usaanpe emuyke an-ruura hemaka-hsi"
		g="various.thing all INDEF.A-carry finish-PL"
		tr="We finished carrying all the goods."
		note="Yamabe & Kindaichi (1913: 180); -hsi cliticises to the auxiliary hemaka, outside the lexical verb."
		cite="sakaguchi2024:80"
		dial="ES"
	/>
	<p>
		What does it index? Murasaki listed it as an optional third person plural
		<Ref k="murasaki1979" p="49–51" paren />, but it co-occurs with overt person markers of
		any person, so it is a number marker, not person agreement
		(<Xr ch="person-and-alignment">Chapter 13</Xr>). Its controller is a core argument:
		normally the subject, but Piłsudski already glossed <i lang="ain">an-kuyra-si</i> ‘I
		stole up on them’ as plural “because the object (the gods) is in the pl.”
		<Ref k="pilsudski1912" p="40–41" paren />. Sakaguchi's synthesis of Hattori's elicitation
		and the texts is that <i lang="ain">-hci</i> supplements person marking wherever person
		affixes leave plurality of a human subject or primary object unexpressed: it is usual
		with plural third persons (zero-marked) and with the formally ambiguous indefinite
		series, and it is not used where <i lang="ain">eci-</i> 2PL or the CI-series already
		settle the number <Ref k="sakaguchi2024" p="97–102" paren />. The controller is animate;
		plural inanimate subjects take a plural stem but not <i lang="ain">-hci</i> (see example
		(20) below). In narrative it is pervasive:
	</p>
	<Ex
		m="ehah-taa monimahpo utah hecire-hci wa sap-ahci kusu ikii"
		g="lily.root-dig girl PL dance-PL and come.down.PL-PL PURP do"
		tr="The girls who went digging lily roots will come dancing down."
		note="Relative clause ehah-taa + utah; both verbs carry -hci, and sap is the plural stem (§8.4)."
		cite="murasaki2001:text 5"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<S t="Plural imperatives" id="imperatives">
		<p>
			Commands to more than one addressee add <i lang="ain">(y)an</i> to the verb; after a
			consonant the <i lang="ain">y</i> drops <Ref k="murasaki1979" p="30" />,
			<Ref k="sakaguchi2024" p="48" paren />. The particle is the same element that marks
			second-person plurality in the 1↔2 interaction forms
			(<Xr ch="person-and-alignment" s="portmanteau">Chapter 13</Xr>):
		</p>
		<Ex
			m="iineahsuy monimahpo uta tan pon horokewpo tura-yan kanne sap-an"
			g="hey girl PL this young man accompany-IMP.PL while go.down.PL-IMP.PL"
			tr="Now, girls, go down together with this young man."
			note="The old woman addresses several girls; on sap the imperative surfaces as -an."
			cite="murasaki2001:text 5"
			dial="WS"
			place="Odasu (Asai Take)"
