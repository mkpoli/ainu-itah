<p>
	Sakhalin Ainu nouns do not inflect for number. <A w="seta" gl="dog" /> is ‘a dog’ or ‘dogs’,
	<A w="aynu" gl="person" /> ‘a person’ or ‘people’; Kindaichi's sketch already pairs
	<i lang="ain">sine aynu</i> ‘one person’ with <i lang="ain">wan aynu</i> ‘ten people’, the
	noun unchanged <Ref k="sakaguchi2020ahcin" p="2" paren />, and Chiri states flatly that
	number is not a grammatical category of the noun <Ref k="chiri1942" p="§67" paren />.
	Plurality is nonetheless richly expressible, by an interlocking set of optional devices:
	the plural word <A w="utah" gl="people" /> ~ <i lang="ain">utara</i>, the suffix
	<i lang="ain">=ahcin</i> on possessed nouns, number-distinguishing demonstratives
	(<Xr ch="pronouns-and-demonstratives">Chapter 9</Xr>), suppletive and alternating verb
	stems, and the verbal plural clitic <i lang="ain">-hci</i>. None of these pluralises a
	singular; each makes overt a plurality already understood, and all may be left off. This
	chapter describes the nominal markers first, then number in the verb, and ends with
	numeral phrases and the mismatches that the optionality of the whole system produces. The
	chief modern studies are <Ref k="sakaguchi2020utara" /> on <i lang="ain">utara</i>,
	<Ref k="sakaguchi2020ahcin" /> on <i lang="ain">=ahcin</i>, and chapters 5–8 of
	<Ref k="sakaguchi2024" /> on verbal number; we follow them closely and add corpus
	observations from the West-coast tales of Asai Take (<span lang="ja">浅井タケ</span>).
</p>

<S t="The plural word utah ~ utara" id="utara">
	<p>
		West-coast <A w="utah" gl="people" /> ~ <i lang="ain">uta</i>, East-coast
		<i lang="ain">utara</i>, corresponds regularly to Hokkaidō <i lang="ain">utar</i> (coda
		<i lang="ain">r</i> → <i lang="ain">h</i> ~ <i lang="ain">rV</i>,
		<Xr ch="phonology">Chapter 4</Xr>). Three uses must be kept apart
		<Ref k="sakaguchi2020utara" p="188" paren />. First, it is an ordinary noun ‘kin,
		companions, people’, with the possessive form <i lang="ain">utari(hi)</i>
		(<Xr ch="nouns-and-possession">Chapter 7</Xr>). Second, placed after an adnominal, a
		noun or a relative clause it builds a plural noun phrase — <i lang="ain">nea utara</i>
		‘those people’, <i lang="ain">aynu utara</i> ‘the people’, <i lang="ain">kotan ohta okay
		utara</i> ‘the people who are in the village’ <Ref k="sakaguchi2020utara" p="188" paren
		/> — and in this use it has become the language's general plural marker. Third, unlike
		Hokkaidō <i lang="ain">utar</i>, Sakhalin <i lang="ain">utara</i> also occurs bare, with
		no modifier at all, as a pronoun-like plural ‘they, people, we, you’; this use was first
		described by Satō in 1987 and has been reanalysed by
		<Ref k="sakaguchi2020utara" />, whom we follow.
	</p>
	<S t="utara as plural marker" id="utara-suffix">
		<p>
			As a postposed plural marker <i lang="ain">-utara</i> attaches to any noun denoting
			something animate (or treated as animate: Piłsudski's texts have <i lang="ain">ikoro
			utara</i> ‘treasures’, <i lang="ain">kosonto utara</i> ‘silk robes’,
			<Ref k="chiri1942" p="§67" paren />), whether in the bare (conceptual) form or in the
			possessed form, and even to nouns that are already plural — Chiri cites
			<i lang="ain">eciokay utara</i> ‘you-PL-people’ with the 2PL pronoun
			<Ref k="chiri1942" p="§67" paren />, and Yamabe Yasunosuke
			(<span lang="ja">山辺安之助</span>) writes <i lang="ain">anokay-utara</i> ‘we’ beside
			the commoner <i lang="ain">anokay-ahsin</i>
			<Ref k="sakaguchi2020ahcin" p="6" paren />. With demonstratives the marker interacts
			with the number distinction of the demonstrative itself: <i lang="ain">nea seta</i>
			‘that dog’, <i lang="ain">nea seta-utar(a)</i> ‘those dogs’, <i lang="ain">neeroh
			nuso-utara</i> ‘those sledges’ <Ref k="chiri1942" p="§67" paren />. In Asai Take's
			tales the combination of anaphoric demonstrative, noun and <i lang="ain">utah</i> is a
			narrative staple:
		</p>
		<Ex
			m="neya kamuy utah renkayne wahka ohta hecire-hci wa okay-ahci manu"
			g="that god PL plentifully water in dance-PL and exist.PL-PL REP"
			tr="Those gods, a great crowd of them, were dancing in the water."
			cite="murasaki2001:text 46"
			dial="WS"
			place="Odasu (Asai Take)"
		/>
		<p>
			Semantically <i lang="ain">-utara</i> covers both additive plurality (‘more than one
			X’) and associative plurality (‘X and those with X’)
			<Ref k="sakaguchi2020ahcin" p="13–14" paren />. The associative reading is forced when
			the host is a personal name:
		</p>
		<Ex
			m="Eekichi-utara hopihsanne makap-ahsi"
			g="Eekichi-PL beach.from go.uphill.PL-PL"
			tr="Eekichi and his party came up from the beach."
			note="From Yamabe Yasunosuke's autobiography (Yamabe & Kindaichi 1913: 75). The plural stem makap and the clitic -ahsi (§8.3) agree with the associative group."
			cite="sakaguchi2020ahcin:14"
			dial="ES"
		/>
		<Ex
			m="Muramatsu Yoshino-utara naa eramusinne-hsi"
			g="Muramatsu Yoshino-PL too be.relieved-PL"
			tr="Muramatsu and Yoshino, too, were relieved."
			note="Yamabe & Kindaichi (1913: 183). The context shows that exactly the two named men are meant: an exemplative use of the associative plural (‘the ones named, as a set’), in Yamakoshi's terms."
			cite="sakaguchi2020ahcin:17"
			dial="ES"
		/>
		<p>
			In the last example no third party is included: <i lang="ain">-utara</i> lists its
			hosts exhaustively, what <Ref k="sakaguchi2020ahcin" p="17" /> identifies (after
			Yamakoshi) as an exemplative associative. The marker also attaches to possessed
			forms — <i lang="ain">an-cinkew-he utara</i> ‘my ancestors’
			<Ref k="chiri1942" p="§67" paren /> — so the often-repeated rule that
			<i lang="ain">-utara</i> takes the bare form and <i lang="ain">=ahcin</i> the
			possessed form (<Ref k="murasaki1979" p="85, 92" paren />) describes a strong tendency,
			not a constraint; the real division of labour is semantic (§8.2).
		</p>
	</S>
	<S t="Bare utara and its grammaticalization" id="utara-bare">
		<p>
			Bare <i lang="ain">utara</i> is frequent: 109 tokens in Piłsudski's Materials alone,
			against four of the third-person pronoun <i lang="ain">anihi</i>
			<Ref k="sakaguchi2020utara" p="189" paren />. Its uses are three
			<Ref k="sakaguchi2020utara" p="192–197" paren />. Generic <i lang="ain">utara</i>
			refers to people at large, with no antecedent:
		</p>
		<Ex
			m="keraykusu asin tan mosiri kes ta koramusine okay utara ki manu"
			g="thanks.to.that now this world end LOC be.at.ease exist.PL people do REP"
			tr="Thanks to that, people now live at ease in this latter age of the world."
			note="okay ‘exist.PL’ is here nominalized, ‘living, life’, the object of ki ‘do’."
			cite="pilsudski1912:text 10, ll. 102–103; sakaguchi2020utara:188"
			dial="ES"
		/>
		<p>
			Anaphoric <i lang="ain">utara</i> picks up a plural human antecedent and is in effect
			a third person plural pronoun:
		</p>
		<Ex
			m="ay-yup-utari-hi sap haworoke-sin an manu apa tuyka ta utara ahuh manu"
			g="INDEF.A-elder.brother-PL-POSS go.down.PL voice.NMLZ-PL exist.SG REP door top LOC people enter.PL REP"
			tr="There was the sound of my brothers coming down; they came in at the door."
			note="utara resumes ay-yup-utari-hi ‘my brothers’. On hawor-oke-sin see §8.2."
			cite="pilsudski1912:text 21, ll. 87–88; sakaguchi2020utara:189"
			dial="ES"
		/>
		<p>
			Third, within quoted speech <i lang="ain">utara</i> is used deictically of people
			present in the speech situation: ‘we’ when the speaker is included, ‘you (pl.)’ when
			not <Ref k="sakaguchi2020utara" p="193–197" paren />:
		</p>
		<Ex
			m="nahteh orowa enciw onne ikoiranu ham utara ki kusu"
			g="then from.now human to fall.in.love NEG people do PURP"
			tr="So from now on let us no longer fall in love with humans."
			note="Fox brothers deliberating among themselves: utara includes speaker and hearers."
			cite="pilsudski1912:text 24, l. 219; sakaguchi2020utara:190, 194"
			dial="ES"
		/>
		<Ex
			m="utara urayki rusuy kusuneyke urayki yahka pirika"
			g="people fight DES if fight although be.good"
			tr="If you want to fight it out, you may fight."
			note="An old man to his visitor, of the visitor and the old man's son: utara = the hearer's party, excluding the speaker."
			cite="pilsudski1912:text 12, ll. 155–156; sakaguchi2020utara:196"
			dial="ES"
		/>
		<p>
			Sakaguchi takes the second-person use to be honorific avoidance — the addressee is not
			named directly, exactly as a wife addresses her husband with third-person
			<i lang="ain">anihi</i> (<Xr ch="pronouns-and-demonstratives">Chapter 9</Xr>) — since
			the plain 2PL pronoun <i lang="ain">ec(i)okay</i> was available and is attested in
			brusque commands <Ref k="sakaguchi2020utara" p="196–197" paren />. The deictic uses are
			not confined to the east coast; Murasaki's west-coast conversation recordings have
			bare <i lang="ain">uta</i> of one's own circle:
		</p>
		<Ex
			m="sake naa uta i-kuu-re"
			g="wine too people INDEF.O-drink-CAUS"
			tr="They (everybody) gave me wine to drink, too."
			note="i- is the indefinite object prefix in its first-person use (Chapter 13)."
			cite="murasaki1976:77; sakaguchi2020utara:193"
			dial="WS"
			place="Maoka"
		/>
		<p>
			How far has bare <i lang="ain">utara</i> grammaticalized? Sakaguchi's syntactic
			evidence points to a clitic-like element rather than a free pronoun. In negative
			clauses it stands between the negative adverb and the verb — <i lang="ain">ham utara
			ki</i> ‘they/we do not do’ — where independent pronouns may not stand
			(<i lang="ain">kuani han ku-nu</i> ‘I did not hear’, never *<i lang="ain">han kuani
			ku-nu</i>); it strongly prefers the position immediately before the verb, after an
			overt object; and it is never itself topicalised with <i lang="ain">neanpe</i>
			<Ref k="sakaguchi2020utara" p="190–191" paren />:
		</p>
		<Ex
			m="an-matak-ihi nah ye yahka ham utara nu-hci"
			g="INDEF.A-younger.sister-POSS QUOT say although NEG people listen-PL"
			tr="Though my younger sister said so, they would not listen."
			cite="pilsudski1912:text 12, ll. 58–59; sakaguchi2020utara:190"
			dial="ES"
		/>
		<p>
			Yet an object may exceptionally intervene between <i lang="ain">utara</i> and the verb
			(<i lang="ain">utara kam eimeh</i> ‘she dealt them out meat’,
			<Ref k="sakaguchi2020utara" p="191" paren />), so the attachment is not yet
			morphological. Diachronically the anaphoric use is best derived by ellipsis of the
			modifier from the X-<i lang="ain">utara</i> construction, and the generic and deictic
			uses from the noun's own sense ‘companions, kin’
			<Ref k="sakaguchi2020utara" p="198–199" paren />. We accordingly describe bare
			<i lang="ain">utara</i> as a defective plural pro-form on its way from noun to
			agreement-like clitic — functionally parallel to the AN-series of Hokkaidō Saru, but a
			Sakhalin development out of native material. It refers only to animates, and the verb
			beside it may, but need not, carry the plural clitic <i lang="ain">-hci</i>: Satō
			found <i lang="ain">-hci</i> on about half of the predicates of bare
			<i lang="ain">utara</i> in the east-coast folktales <Ref k="sakaguchi2024" p="97"
			paren />, and in our count of Asai Take's tales a <i lang="ain">-hci</i>-marked verb
			appears in roughly six out of seven sentences containing a noun phrase with
			<i lang="ain">utah</i> — usual, never obligatory.
		</p>
	</S>
</S>

<S t="The nominal plural =ahcin" id="ahcin">
	<p>
		The second nominal plural is the suffixal clitic <i lang="ain">=ahcin</i>, with
		allomorphs <i lang="ain">=hcin</i> after vowels (the usual realisation, since its hosts
		end in the possessive suffix <i lang="ain">-hV</i>), <i lang="ain">=ahcin</i> after
		consonants, and southern and west-coast variants in <i lang="ain">s</i>
		(<i lang="ain">=(a)hsin</i>, <i lang="ain">=sin</i>, <i lang="ain">=cin</i>)
		<Ref k="sakaguchi2020ahcin" p="4–5" paren />. Nothing like it exists in Hokkaidō; within
		Sakhalin it has been recognised since Piłsudski, who called <i lang="ain">cin ~ sin</i> a
		plural sign typically of inanimates <Ref k="pilsudski1912" p="36" paren />, while
		Kindaichi mistook it for an emphatic particle — refuted by Chiri, who showed that it
		stands before case particles, where no such particle can stand
		<Ref k="chiri1942" p="§67" paren />. Its distribution is rigidly restricted: it attaches
		only to the possessive (“belonging”) form of nouns, never to the bare conceptual form
		and never to proper names <Ref k="sakaguchi2020ahcin" p="5–8, 13" paren />. Hattori's
		Rayciska paradigm shows the system <Ref k="hattori1961" p="6–7" paren />:
	</p>
