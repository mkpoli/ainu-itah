<S t="Numeral phrases, optionality, and mismatches" id="mismatches">
	<p>
		Numerals modify the unmarked noun directly — <i lang="ain">sine aynu</i> ‘one person’,
		<i lang="ain">tu monimahpo</i> ‘two girls’, <i lang="ain">re horokewpo</i> ‘three young
		men’ — or stand free with the classifier suffixes <i lang="ain">-n</i> (humans) and
		<i lang="ain">-p ~ -pe(h)</i> (things) <Ref k="chiri1942" p="§105, §113" paren />,
		<Ref k="sakaguchi2022" paren /> (<Xr ch="numerals">Chapter 10</Xr>). A numerally
		quantified noun normally takes no plural marker, and since Kindaichi it has been
		reported that it also prefers the <em>singular</em> verb stem, the numeral making
		plurality explicit enough <Ref k="sakaguchi2024" p="131" paren />. The corpus bears
		this out:
	</p>
	<Ex
		m="re monimahpo an ruwehe an manu"
		g="three girl exist.SG trace.NMLZ exist.SG REP"
		tr="There were signs that three girls lived there."
		cite="murasaki2001:text 5"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		But it is a preference, not a rule. <Ref k="sakaguchi2024" p="133–140" /> shows numeral
		and <i lang="ain">poronno</i> ‘many’ phrases with either stem, and argues that the real
		conditioning is construal: a quantity taken as one bounded mass selects the singular
		stem, individuated members select the plural. A single sentence of Yamabe's makes the
		contrast:
	</p>
	<Ex
		m="aynu ruu poronno an wahkayki husko ruu pateh okay"
		g="person track much exist.SG although old track only exist.PL"
		tr="There were many human tracks, but only old ones."
		note="Yamabe & Kindaichi (1913: 108). The mass of tracks takes an; the old tracks, individually inspected and newsworthy, take okay — and no -hci, the subject being inanimate."
		cite="sakaguchi2024:140"
		dial="ES"
	/>
	<p>
		The same looseness runs through the whole system. In Asai Take's tales we find
		<i lang="ain">tu monimahpo utah an manu</i> ‘there were the two girls’, a
		plural-marked noun phrase with the singular existential (text 51), and
		<i lang="ain">ahun</i> ‘enter (sg.)’ predicated of the plural wives in example (17)
		above; conversely fully redundant strings like example (1), where demonstrative,
		<i lang="ain">utah</i>, plural stem and <i lang="ain">-hci</i> all co-occur. We
		analyse the situation thus: number in Sakhalin Ainu is everywhere optional and
		semantically driven — markers are recruited to make plurality (or its associative and
		distributive shadings) explicit where it matters to the discourse, and left off where
		it is recoverable, the only near-grammaticalised corners being the possessed-noun
		paradigm of §8.2 and the grammatical concord of person series with stem number. The
		genuinely open question is the division of labour between plural stem and
		<i lang="ain">-hci</i> with third persons; our working hypothesis, after
		<Ref k="sakaguchi2024" p="140" />, is that the stem tracks construal (mass vs.
		individuated) while <i lang="ain">-hci</i> tracks animate participants, and texts in
		which inanimate plurals take <i lang="ain">-hci</i> would falsify it.
	</p>
</S>
