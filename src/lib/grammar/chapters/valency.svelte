	<p>
		The meaning runs the whole causative range from coercion to giving and letting:
		<i lang="ain">konte</i> ‘cause to have’ is the ordinary verb ‘give’, and Chiri analysed apparent
		datives as causees on exactly these verbs
		<Ref k="chiri1942" p="§68" paren />. The causee is the indexed object (<i lang="ain">in=konte</i
		>
		‘give me’, <i lang="ain">eci=nu-re</i> ‘I will let you hear’; <Xr ch="person-and-alignment"
			>Chapter 13</Xr
		>).
	</p>
