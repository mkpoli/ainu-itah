<p>
	Since every Sakhalin Ainu verb stem is lexically intransitive or transitive (<Xr
		ch="verb-structure"
		s="transitivity">Chapter 12</Xr
	>), all adjustment of valency is done by derivational morphology, and the language maintains a
	full double set of it. Valency is increased by the causative suffixes <i lang="ain">-te</i>/<i
		lang="ain">-re</i
	>,
	<i lang="ain">-ke</i>, <i lang="ain">-ka</i> and <i lang="ain">-yara</i> and by the applicative
	prefixes <i lang="ain">e-</i>, <i lang="ain">ko-</i>, <i lang="ain">o-</i>; it is decreased by the
	antipassive <i lang="ain">i-</i>, the reflexives
	<i lang="ain">yay-</i> and <i lang="ain">si-</i>, the reciprocal <i lang="ain">u-</i>, and noun
	incorporation. Chiri catalogued both sets for Sakhalin in one programmatic paragraph <Ref
		k="chiri1942"
		p="§33"
		paren
	/>, and Dal Corso confirms the same inventory in Piłsudski's east-coast corpus <Ref
		k="dalcorso2024"
		p="70"
		paren
	/>. The richness and mutual combinability of these ‘voices’ is the celebrated typological
	trademark of Ainu (<Ref k="shibatani1990" paren />; <Ref k="bugaeva2022" paren />); this chapter
	documents what of it is attested in Sakhalin sources, ending with the combinations and a
	productivity assessment. Two further markers touch valency only obliquely: the resultative
	<i lang="ain">ci-</i>, which detransitivises by deleting the agent (<i lang="ain">ci-ranke</i> ‘be
	lowered’ <Ref k="chiri1942" p="§49" paren />,
	<Ref k="dalcorso2024" p="67" paren />), and the impersonal passive built on the indefinite person,
	treated with person marking in
	<Xr ch="person-and-alignment" s="indefinite">Chapter 13</Xr>.
</p>

<p>
	Each operation below also has a fixed effect on Chiri's classification of predicates into complete
	(object-less) and incomplete (object-requiring) verbs (<Xr ch="word-classes" s="verbs"
		>Chapter 6</Xr
	>). The increasers — applicatives and causatives — turn a complete verb into an incomplete one,
	which thereafter takes the prefixal agreement of a transitive; the decreasers — antipassive,
	reflexive, reciprocal, incorporation — fill or remove the object slot and return an incomplete
	verb to the complete class, with suffixal agreement <Ref k="chiri1942" p="§33" paren />. The whole
	chapter can accordingly be read as the derivational traffic between Chiri's two classes, each
	‘voice’ a one-way crossing.
</p>

<S t="Causatives" id="causative">
	<p>
		The productive causative is <i lang="ain">-te</i> ~ <i lang="ain">-re</i>:
		<i lang="ain">-re</i> after vowel-final stems, <i lang="ain">-te</i> after consonant-final ones <Ref
			k="chiri1942"
			p="§55"
			paren
		/>,
		<Ref k="tangiku1998" p="229–230" paren />, <Ref k="tangiku2022" paren />. For the verbs in
		<i lang="ain">-rV</i>
		the choice follows from the two stem classes established in <Xr
			ch="morphophonology"
			s="r-alternations">Chapter 5</Xr
		>: a Class I verb (underlying /r/) counts as consonant-final and takes <i lang="ain">-te</i>
		with the regular shift of <i lang="ain">r</i> to <i lang="ain">n</i>
		(<i lang="ain">kor-te</i> → <i lang="ain">konte</i> ‘give’), while a Class II verb (lexically
		vowel-final) takes <i lang="ain">-re</i> like any vowel stem. Citation forms that look identical
		therefore part ways under the suffix —
		<i lang="ain">nukara</i> → <i lang="ain">nukante</i> but <i lang="ain">kira</i> →
		<i lang="ain">kirare</i> — and the causative is in practice the readiest test of a verb's class
		membership. Hokkaidō's third allomorph <i lang="ain">-e</i> after
		<i lang="ain">r</i> (<i lang="ain">kor-e</i>, <i lang="ain">nukar-e</i>) is replaced in Sakhalin
		by <i lang="ain">-te</i>
		<Ref k="chiri1942" p="§55" paren />, so the
		<i lang="ain">-nte</i> causatives are among the clearest Sakhalin-defining forms in the verbal morphology.
		Table 1 lists base ~ causative pairs for which both members are attested in Sakhalin sources.
