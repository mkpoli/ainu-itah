	<p>
		Three minor causative formatives partition the lexicon with
		<i lang="ain">-te</i>/<i lang="ain">-re</i>. (i) <i lang="ain">-ke</i> pairs with the
		<i lang="ain">n</i>/<i lang="ain">p</i> number stems (<Xr ch="verb-structure" s="verbal-number"
			>Chapter 12</Xr
		>): singular
		<i lang="ain">ahun-ke</i> ‘put one in’ against plural <i lang="ain">ahup-te</i> ‘put many in’,
		so that the suffix choice itself signals object number
		<Ref k="chiri1942" p="§§36, 55" paren />; the corpus supplies
		<i lang="ain">asinke</i>, <i lang="ain">ahunke</i>, <i lang="ain">makanke</i> and the east-coast
		<i lang="ain">an-e-makan-ke</i>
		‘I sent you uphill’ (<Ref k="pilsudski1912" p="text 22" paren />; <Ref
			k="sakaguchi2024"
			p="ex. 3-33"
			paren
		/>). (ii) <i lang="ain">-ka</i> derives causatives chiefly from property and posture verbs:
		<i lang="ain">tuhse-ka</i>
		‘kick’ above, <i lang="ain">hosipi-ka</i> ‘send back’ <Ref k="chiri1942" p="§55" paren />. (iii)
		The thematic vowels
		<i lang="ain">-a/-i/-u/-e/-o</i> transitivise bare roots (<i lang="ain">mak-a</i> ‘open’,
		<i lang="ain">as-i</i>
		‘stand sth up’)
		<Ref k="chiri1942" p="§55" paren />. Finally <i lang="ain">-yara</i> is the indefinite-causee
		causative, ‘have people do’: Chiri's
		<i lang="ain">e-yara</i> ‘have (people) eat’ <Ref k="chiri1942" p="§55" paren />, and
		lexicalised in <i lang="ain">sinukarara</i> ‘show oneself off’ from
		<i lang="ain">si-nukara-yara</i> ‘have people look at oneself’
		<Ref k="murasaki1979" paren />, <Ref k="tangiku1998" p="229–230" paren />.
	</p>
	<p>
		Stacked causatives of the shape <i lang="ain">-te-re</i> or <i lang="ain">-re-te</i>
		do not occur in the corpus or in the descriptions we control. Double causation is instead expressed
		by a causative over an already derived stem (<i lang="ain">ipe-re</i> on antipassive
		<i lang="ain">ipe</i>,
		<i lang="ain">sinukarara</i> with reflexive plus <i lang="ain">-yara</i>) or analytically with
		the light verb <i lang="ain">ki</i>. We take the gap to be real and systematic — the causee
		slot, once filled, is not reopened — though a form like
		<i lang="ain">nukante-yara</i> ‘have people show’ would be the expected shape if it were; finding
		one in unpublished recordings would settle the point.
	</p>
</S>

<S t="Applicatives" id="applicatives">
	<p>
		The applicative prefixes promote an oblique participant to object, transitivising intransitives
		and ditransitivising transitives. The three are not interchangeable: the choice tracks the
		semantic role of the promoted argument
		<Ref k="dalcorso2024" p="71" paren />. <i lang="ain">e-</i> covers location, goal, topic
		(‘about’), path, instrument, purpose, cause and accompaniment — Chiri illustrates each for
		Sakhalin, e.g. <i lang="ain">neampe an-e-mina</i> ‘we laughed about it’ from
		<i lang="ain">mina</i>
		‘laugh’, <i lang="ain">oya kotan e-oman</i> ‘he went to another village’ <Ref
			k="chiri1942"
			p="§§33, 56"
			paren
		/>.
		<i lang="ain">ko-</i> adds a goal or addressee, typically a person
		<Ref k="chiri1942" p="§58" paren />; <i lang="ain">o-</i> adds places only
		<Ref k="chiri1942" p="§57" paren />. For the southern Hokkaidō dialects Bugaeva's corpus study
		(Bugaeva 2006: 188–193) quantifies the same division of labour —
		<i lang="ain">ko-</i> the most and <i lang="ain">o-</i> the least frequent, with about a third of
		all verb lexemes containing an applicative — and the Sakhalin corpus matches that ranking, as set
		out below.
	</p>
	<Ex
		m="suma kema e-tuhse-ka"
		g="rock foot APPL-jump-CAUS"
		tr="He kicked the rock with his foot."
		note="The instrument kema ‘foot’, expressed obliquely with ani ‘with’ in the unprefixed construction, is promoted to object by e- (Dal Corso 2024: 71)."
		cite="murasaki1979:32; dalcorso2024:71"
		dial="WS"
		place="Rayciska"
	/>
	<Ex
		m="rekoro ituyaskara rekoro an=e=ko-yay-irayki"
		g="very feel.affection very INDEF.A=2SG.O=APPL-REFL-be.grateful"
		tr="I feel great affection, and I am deeply grateful to you."
		note="ko- adds the addressee, indexed by e= ‘you’, to the intransitive yay-irayki ‘be thankful’."
		cite="dobrotvorsky1875; sakaguchi2021"
		dial="WS"
		place="West coast, central (recorded 1867–1872)"
	/>
	<p>
		Piłsudski's corpus also shows <i lang="ain">ko-</i> in a use where no applied object can be
		recovered, on zero-valent weather predicates; Dal Corso reads this as a relic of a broader
		high-transitivity function of the prefix
		<Ref k="dalcorso2024" p="73" paren />:
	</p>
	<Ex
		m="ko-sistono to-noske ko-oman kane"
		g="APPL-dawn day-middle APPL-go.SG while"
		tr="Day had dawned and, when it had gone on to midday, …"
		note="ko-oman ‘(the day) went on to (midday)’ is a regular goal applicative; ko-sistono ‘it dawned’ has no retrievable applied object."
		cite="pilsudski1912:146; dalcorso2024:73"
		dial="ES"
	/>
	<p>
		The prefix <i lang="ain">o-</i> is marginal in the Sakhalin corpus: we find it only inside
		lexicalised place-object verbs (Chiri's locative sense, §57), never as a live alternation
		parallel to the <i lang="ain">e-</i> pair above. We treat it as a closed lexical class in
		Sakhalin, against its modest productivity in Hokkaidō (Bugaeva 2006: 193). Applicatives also
		feed the rest of the system: an applied object is incorporable (§14.6), and the applicative slot
		is what reciprocal and reflexive prefixes fill in combinations like <i lang="ain">u-ko-</i> and
		<i lang="ain">yay-ko-</i> (§14.7).
	</p>
</S>

<S t="The antipassive i-" id="antipassive">
	<p>
		Prefixed to a transitive verb, <i lang="ain">i-</i> suppresses the object, yielding an
		intransitive of generic activity: ‘the unspecified, the usual thing’ is understood. Chiri
		glosses it ‘things’ and lists Sakhalin examples in which the conventionalised object has
		lexicalised: <i lang="ain">i-ku</i> ‘drink (i.e. liquor)’ from
		<i lang="ain">ku</i> ‘drink’, <i lang="ain">inkara</i> ‘look (about)’ from
		<i lang="ain">i-nukar(a)</i>, <i lang="ain">i-ruska</i> ‘be angry’ from
		<i lang="ain">ruska</i> ‘resent sth’ <Ref k="chiri1942" p="§51" paren />. The pair
		<A w="ipe" gl="have a meal" /> beside transitive <A w="ee" gl="eat" /> (from
		<i lang="ain">i-e</i>) is the showcase: the two verbs even split as complete versus incomplete
		predicates in Chiri's classification <Ref k="chiri1942" p="§30" paren />. All four are richly
		attested in the corpus; <i lang="ain">inu</i> ‘listen’ (<i lang="ain">i-nu</i> ‘hear things’) joins
		them in the Rayciska recordings.
	</p>
	<Ex
		m="okay=an teh ikuu=an raapoke ta"
		g="exist.PL=INDEF.S and ANTIP.drink=INDEF.S while in"
		tr="I sat at home, and while I was smoking…"
		note="iku ‘drink/smoke (the usual thing)’ takes the intransitive person suffix =an, confirming detransitivisation."
		cite="tamura2003"
		dial="WS"
		place="Rayciska (Fujiyama Haru)"
	/>
	<Ex
		m="orowa munaynu taa cise kaa-ta rikin teh nay onne inkara-ha neampe"
		g="then grass.man EMPH house top-LOC climb.SG and river to ANTIP.see-POSS when"
		tr="Then the grass-man climbed onto the roof, and when he looked out toward the river, …"
		cite="murasaki2001:text 10"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		The antipassive output re-enters derivation freely: <i lang="ain">ipe-re</i> ‘give a meal to’
		(§14.1) causativises it, and <i lang="ain">inkara</i> behaves as an underlying
		<i lang="ain">r</i>-stem (<i lang="ain">inkan rusuy</i>,
		<Xr ch="verb-structure" s="stem-classes">Chapter 12</Xr>). The prefix is segmentally identical
		to the indefinite object marker <i lang="ain">i-</i>, from which it historically derives; the
		synchronic division — referential first-person or logophoric reading for the agreement marker,
		generic-object reading here — is drawn in <Xr ch="person-and-alignment" s="object-marking"
			>Chapter 13</Xr
		>. In our corpus counts, productive (non-lexicalised) antipassives are rare: the construction
		survives chiefly in the conventionalised activity verbs listed above, which the dictionaries
		already treat as lemmas (<Ref k="hattori1964" paren />).
	</p>
</S>

<S t="Reflexives yay- and si-" id="reflexive">
	<p>
		The reflexive <i lang="ain">yay-</i>, from the root ‘self’, intransitivises a transitive verb,
		identifying agent and patient: <i lang="ain">yay-rayki</i> ‘kill oneself’,
		<i lang="ain">yay-nuyna</i>
		‘hide oneself’
		<Ref k="chiri1942" p="§53" paren />. The valency decrease is visible in person marking:
		east-coast <i lang="ain">yay-reske-an</i> ‘I raised myself’ takes the intransitive suffix where
		<i lang="ain">reske</i>
		‘raise’ takes prefixes
		<Ref k="dalcorso2024" p="73–74" paren />.
	</p>
	<Ex
		m="sine monimahpo yay-reske"
		g="one girl REFL-raise"
		tr="A girl lived all alone (lit. raised herself)."
		cite="murasaki2001:text 39"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<Ex
		m="yay-nuyna teh taa nukara manu"
		g="REFL-hide and EMPH watch REP"
		tr="He hid himself and watched."
		cite="murasaki2001:text 37"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		A second pattern leaves valency intact: <i lang="ain">yay-</i> marks the possessor of an
		incorporated noun, coreferent with the subject — the ‘quasi-possessive reflexive’ (Bugaeva and
		Kobayashi, in <Ref k="bugaeva2022" paren />). Piłsudski's
		<i lang="ain">yay-cise-koro</i> ‘have a house of one's own’
		<Ref k="pilsudski1912" p="238" paren />, <Ref k="dalcorso2024" p="74" paren /> recurs verbatim in
		Asai Take's tales:
	</p>
	<Ex
		m="tu horokewpo yay-cise-koro-hci okay-ahci manuyke"
		g="two man REFL.POSS-house-have-PL exist.PL-PL and.then"
		tr="The two men lived each keeping a house of his own, and…"
		cite="murasaki2001:text 51"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		Lexicalised <i lang="ain">yayko-</i> (reflexive plus applicative) means ‘by oneself, within
		oneself’ <Ref k="chiri1942" p="§25" paren />; the nineteenth-century west-coast conversations
		use it in verbs of mental settlement:
	</p>
	<Ex
		m="sonnoka an-yayko-ramuosma"
		g="truly INDEF.A-REFL.APPL-be.convinced"
		orig="Соннока а яйкурамуосьма."
		origLang="ain-Cyrl"
		tr="Truly, we are persuaded."
		note="ramuosma ‘consent’ is itself ramu ‘mind’ incorporated into osma ‘rush into’."
		cite="dobrotvorsky1875; sakaguchi2021"
		dial="WS"
		place="West coast, central (recorded 1867–1872)"
	/>
	<p>
		The parallel prefix <i lang="ain">si-</i> covers the middle range: with plain transitives
		‘oneself’ (<i lang="ain">si-etaye</i> ‘draw back’), with causatives ‘get oneself V-ed’ (<i
			lang="ain">si-kasuy-re</i
		>
		‘get oneself helped’) and ‘feign’ (<i lang="ain">si-ray-re</i> ‘play dead’) <Ref
			k="chiri1942"
			p="§53"
			paren
		/>; Sakhalin <i lang="ain">sinukarara</i> ‘show oneself off’ (§14.1) builds on it. In the corpus
		<i lang="ain">yay-</i>
		outnumbers <i lang="ain">si-</i> by an order of magnitude, and only <i lang="ain">yay-</i> shows
		the possessive-reflexive use — a distributional statement that is our own.
	</p>
</S>

<S t="Reciprocal and sociative u-" id="reciprocal">
	<p>
		The prefix <i lang="ain">u-</i> ‘each other’ intransitivises a transitive verb just as the
		reflexive does <Ref k="chiri1942" p="§52" paren />. Corpus pairs include
		<A w="sam" gl="marry" /> → <i lang="ain">u-sam</i> ‘marry each other’,
		<A w="nukara" gl="see" /> → <i lang="ain">u-nukara</i> ‘meet’ (<Ref
			k="murasaki2001"
			p="text 34"
			paren
		/>), <A w="koyki" gl="attack, beat" /> →
		<i lang="ain">u-koyki</i> ‘fight one another’:
	</p>
	<Ex
		m="re horokewpo okore mahnuu re monimahpo u-sam-ahci"
		g="three man all wife.take three girl RECP-marry-PL"
		tr="The three men all took wives; the three girls were wedded (to them)."
		note="mahnuu ‘take a wife’ is denominal: mah ‘wife’ with the verbaliser -nu(u) ‘get, have’, beside hoku-nu ‘take a husband’ (Chiri 1942: §92)."
		cite="murasaki2001:text 44"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<Ex
		m="cisekoro horokewpo tura taa u-koyki-hci u-koyki-hci omantene taa cisekoro horokewpo rayki teh taa ociwe"
		g="house.own man with EMPH RECP-fight-PL RECP-fight-PL and.then EMPH house.own man kill and EMPH throw"
		tr="He grappled and grappled with the master of the house, killed him, and threw him out."
		note="The co-participant is expressed with comitative tura while the verb itself is reciprocal — the regular discontinuous construction."
		cite="murasaki2001:text 50"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		Beside true reciprocity, <i lang="ain">u-</i> has a sociative-collective use that Chiri singles
		out as characteristically Sakhalin: on intransitives, with or without a causative, it means ‘in
		numbers, all together’, sometimes amounting to mere plurality —
		<i lang="ain">utara u-mokon-te</i>
		‘the people slept’,
		<i lang="ain">u-ariki-re</i> ‘they came along’ <Ref k="chiri1942" p="§52" paren />. Sentoku's
		letters supply both this and the combination with <i lang="ain">ko-</i>:
	</p>
	<Ex
		m="opista uwekari ukopakari an=ki-hci"
		g="all RECP.gather RECP.APPL.consult INDEF.A=do-PL"
		orig="обісьта увекарі укобакарі анкіхці"
		origLang="ain-Cyrl"
		tr="We will all gather and take counsel together."
		note="uwekari is u- plus e-kari with regular glide insertion; ukopakari is u-ko-pakari ‘reckon with one another’."
		cite="tangiku2001:Letter 1"
		dial="ES"
		place="Otasan (Sentoku Tarōji)"
	/>
	<p>
		Lexicalisation has gone furthest in <i lang="ain">ukoyki</i> itself. In the same letter Sentoku
		writes <i lang="ain">cep ukoyki an=e-hci</i>, which Tangiku and Ogihara translate ‘we catch a
		little fish and eat it’
		<Ref k="tangiku2001" p="Letter 1" paren /> — here <i lang="ain">ukoyki</i> is not ‘fight each
		other’ but the collective activity verb ‘take (fish), do the fishing’, built on
		<i lang="ain">koyki</i>
		‘attack, catch (game)’; in Letter 3 it serves as a plain noun,
		<i lang="ain">ukoyki porono an</i>
		‘the catch was plentiful’
		<Ref k="tangiku2001" p="Letter 3" paren />. The reciprocal, sociative and lexical readings shade
		into one another along a path the corpus shows still open.
	</p>
</S>

<S t="Noun incorporation" id="incorporation">
	<p>
		A noun in its bare conceptual form may be compounded into the verb, ceasing to be a syntactic
		argument <Ref k="chiri1942" p="§§24–25" paren />, <Ref k="tangiku2022" paren />. The canonical
		case incorporates the object of a transitive activity verb:
	</p>
	<Ex
		m="toyaynu wahka-taa kusu san manu"
		g="earth.man water-draw PURP go.down.SG REP"
		tr="The earth-man went down to draw water."
		cite="murasaki2001:text 10"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<Ex
		m="eani wahka-taa eani wahka-taa"
		g="2SG water-draw 2SG water-draw"
		tr="You fetch the water! You fetch the water!"
		cite="murasaki2001:text 9"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<Ex
		m="orowa nean niina kusu kina-taa kusu makan"
		g="then that.one firewood.gather PURP plant-pick PURP go.uphill.SG"
		tr="Then she went up into the hills to gather firewood and pick butterbur."
		note="niina ‘gather firewood’ (nii ‘wood’) is fully lexicalised; kina-taa ‘pick plants’ is built like wahka-taa."
		cite="murasaki2001:text 37"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		That these are words and not object–verb phrases is shown by three convergent facts, which we
		state from our corpus analysis. First, the plural suffix treats the whole as one stem: <i
			lang="ain">cehkoyki-hci</i
		>
		‘they caught fish’,
		<i lang="ain">ehahtaa-hci</i> ‘they dug lily roots’, with
		<i lang="ain">-hci</i> outside noun plus verb. Second, the incorporated noun is non-referential
		and unmodifiable: a counted or possessed object always stands outside (<i lang="ain"
			>ponno ponno cep ukoyki</i
		>
		‘catch a few fish’, Letter 1). Third, the complex serves as a bare complement of
		<i lang="ain">kusu</i>
		‘in order to’ exactly like a simple verb, as above. The same nouns occur loose with the light verb
		(<i lang="ain">ceh kii</i>, <i lang="ain">ehah taa</i> as two words,
		<Xr ch="verb-structure" s="light-verbs">Chapter 12</Xr>), so univerbation is a gradient the
		speakers exploit.
	</p>
	<Ex
		m="niina-hci koh kuru-hci ceh-koyki-hci koh e-hci ehah-taa-hci koh e-hci"
		g="firewood.gather-PL whenever burn-PL fish-catch-PL whenever eat-PL lily.root-dig-PL whenever eat-PL"
		tr="They would gather firewood and burn it, catch fish and eat, dig lily roots and eat."
		cite="murasaki2001:text 18"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		Which argument incorporates is constrained by grammatical relation. Shibatani's survey of Ainu
		concluded that incorporation targets objects (including derived, applicative objects) and
		patient-like intransitive subjects, obliques only after applicative promotion (Shibatani 1988:
		210–211); Satō's Hokkaidō counts give object incorporation 85.9 percent, natural-force subject
		incorporation (<i lang="ain">sir-pirka</i> type) 6.8 percent, with possessor-stranding and
		transitive-subject incorporation marginal (Satō 2022: ch. 16, in
		<Ref k="bugaeva2022" paren />). The Sakhalin corpus fits: object incorporation as above; subject
		incorporation in the weather verbs (<i lang="ain">siri-pirika</i>, <Xr
			ch="verb-structure"
			s="weather">Chapter 12</Xr
		>); and incorporation of a promoted locative object in <i lang="ain">cip-oo</i> ‘ride in a boat’
		(<i lang="ain">cip</i> ‘boat’ with <i lang="ain">o(o)</i> ‘sit in’):
	</p>
	<Ex
		m="mah wa taa sine aynu taa cip-oo wa san manu"
		g="upstream from EMPH one person EMPH boat-ride and go.down.SG REP"
		tr="From upstream a man came down riding in a boat."
		cite="murasaki2001:text 37"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		Incorporation also interlocks with the other voices: <i lang="ain">yay-</i> rides on an
		incorporated possessed noun (<i lang="ain">yay-cise-koro</i>, §14.4), and body-part
		incorporation underlies lexical prefixes like <i lang="ain">he-</i> ‘head’ and
		<i lang="ain">ho-</i> ‘rear’, which Chiri groups with the detransitivisers (<i lang="ain"
			>he-pen-u</i
		>
		‘raise one's face’) <Ref k="chiri1942" p="§§33, 41" paren />.
	</p>
</S>

<S t="Combinations and productivity" id="combinations">
	<p>
		The valency machinery is freely recursive within fixed scope: each prefix construes with
		everything to its right, so the outermost prefix corresponds to the outermost derivation.
		Attested Sakhalin stackings include <i lang="ain">u-ko-</i> (reciprocal filling the
		applicative's object slot: <i lang="ain">u-ko-itak</i> ‘talk to each other’ <Ref
			k="chiri1942"
			p="§25"
			paren
		/>, <i lang="ain">u-ko-pakari</i> above),
		<i lang="ain">yay-ko-</i> (§14.4), <i lang="ain">ko-yay-</i> (applicative over a reflexive base:
		<i lang="ain">an=e=ko-yay-irayki</i>, §14.2), causative over antipassive (<i lang="ain"
			>ipe-re</i
		>), reflexive plus indefinite causative (<i lang="ain">si-nukara-yara</i>), and applicative over
		reflexive-applicative:
	</p>
	<Ex
		m="cise-ta paye-hci ike tani taa acahcipo reekoh e-yayko-nopuru manu"
		g="house-LOC go.PL-PL and now EMPH old.woman very APPL-REFL.APPL-be.delighted REP"
		tr="They came to the house, and the old woman was overjoyed at it."
		note="yayko-nopuru ‘rejoice (within oneself)’ takes a further e- introducing the stimulus."
		cite="murasaki2001:text 51"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		Our productivity assessment from the corpus, text frequencies weighed against type counts, is as
		follows. Fully productive: causative
		<i lang="ain">-te</i>/<i lang="ain">-re</i> (new formations on loanwords and derived stems; no
		lexical gaps observed) and the reciprocal <i lang="ain">u-</i>. Productive but lexically
		anchored: <i lang="ain">yay-</i>, applicative
		<i lang="ain">ko-</i> and <i lang="ain">e-</i> — most tokens belong to recurring lexemes, yet
		fresh combinations occur in single texts. Closed or fossilised: applicative
		<i lang="ain">o-</i>, antipassive <i lang="ain">i-</i>,
		<i lang="ain">si-</i>, the causatives <i lang="ain">-ka</i>, <i lang="ain">-ke</i>
		(alive only inside the number-paired motion verbs) and <i lang="ain">-yara</i>. Object
		incorporation sits in between: the pattern is well represented but its productive core is a
		small set of subsistence-activity verbs. Dal Corso reaches a compatible conclusion from the
		other direction, observing that the secondary, valency-preserving functions of
		<i lang="ain">e-</i>, <i lang="ain">ko-</i> and
		<i lang="ain">yay-</i> common in Piłsudski's 1900s east-coast corpus have all but vanished from
		the west-coast recordings of the 1960s, an erosion he connects with the general loss of
		productive verbal morphology under Japanese contact
		<Ref k="dalcorso2024" p="70–71" paren />. The system the last speakers kept intact was the core:
		causative, reciprocal, reflexive, and the lexicalised stock of everything else.
	</p>
</S>
