		Alongside <A w="aynu" />, Sakhalin Ainu has a second word for ‘human being’ with no
		counterpart in Hokkaidō: <A w="enciw" gl="human being" />, recorded by
		<Ref k="chiri1942" p="§2" /> in the form <i lang="ain">enciw</i> among words showing
		voicing after a nasal, and used historically as a self-designation by the Sakhalin Ainu
		<Ref k="dalcorso2024" p="55" paren />. The modern descendants of the Sakhalin Ainu have
		adopted <i lang="ain">enciw</i> as the label that distinguishes them from the Hokkaidō
		Ainu <Ref k="janhunen2022" paren />, and in today’s revival movement the language is
		called <A w="enciw itah">Enciw itah</A> ‘language of the Enciw’
		<Ref k="dalcorso2024" p="55" paren />, a usage also followed by
		<Ref k="murasaki2025" paren />.
	</p>
	<p>
		The second element of that name, <A w="itah" gl="speech, language" />, is the Sakhalin
		form corresponding to Hokkaidō <i lang="ain">itak</i>, with the regular Sakhalin coda
		neutralisation described in <Xr ch="phonology">Chapter 4</Xr>. It is an old element of
		the language’s own metalinguistic vocabulary: the title page of the first dictionary of
		the language renders ‘Ainu–Russian dictionary’ in Cyrillic-script Ainu as
		<span lang="ain-Cyrl">а́йну-ру́сскій ита́ку-чо́менъ</span>, with <i lang="ain">itaku</i>
		for ‘word, speech’ <Ref k="dobrotvorsky1875" paren />. In the
		scholarly literature the variety is most often called Sakhalin Ainu in English and
		<span lang="ja">樺太アイヌ語</span> <i>Karafuto ainugo</i> ‘Karafuto Ainu’ in Japanese,
		after the Japanese name of the island <Ref k="dalcorso2024" p="57" paren />; both names
		are used of the same object, and this grammar writes Sakhalin Ainu.
	</p>
</S>

<S t="Geographic setting and history" id="history">
	<p>
		The Ainu settlement area on Sakhalin covered the southern part of the island, from
		Aniwa Bay in the south up both coasts to roughly the 50th parallel. Archaeological
		evidence places the Ainu expansion from Hokkaidō onto southern Sakhalin at about 1300
		CE <Ref k="janhunen2022" paren />. On the west coast, facing the Strait of Tartary, lay
		villages such as Tarantomari <span lang="ja">多蘭泊</span> (now <span lang="ru">Калинино</span>), Maoka <span lang="ja">真岡</span> (now
		Kholmsk), Rayciska <span lang="ja">来知志</span> (now <span lang="ru">Красногорск</span
		>), Odasu <span lang="ja">小田洲</span> (now <span lang="ru">Парусное</span>), Usoro
		<span lang="ja">鵜城</span>, and Esutoru <span lang="ja">恵須取</span> (now <span lang="ru">Углегорск</span>); on the
		east coast, facing the Sea of Okhotsk, Ociho <span lang="ja">落帆</span>, Tunayci
		<span lang="ja">富内</span>, Ay, Otasan <span lang="ja">小田寒</span>, Hunup, and, on
		the Bay of Patience in the north-east, Tarayka <span lang="ja">多来加</span> and Nairo <span lang="ja">内路</span>
		<Ref k="tangiku2022" paren />, <Ref k="ono2020" p="232" paren />,
		<Ref k="dalcorso2024" p="58" paren />. The Poronay basin
		behind Tarayka was the zone where the Ainu were in community-level contact with the
		Uilta and the Nivkh, their only ethnic neighbours on the island apart from Japanese and
		Russians <Ref k="janhunen2022" paren /> (see <a href="#contact">§1.4</a>).
	</p>
