<p>
	Sakhalin Ainu is the variety of Ainu once spoken across the southern half of Sakhalin
	island. Together with Hokkaidō Ainu and Kuril Ainu it forms one of the three major
	branches of the small Ainu language family <Ref k="janhunen2022" paren />. The language
	ceased to be passed on to children in the first half of the twentieth century and lost
	its last native speaker in 1994 <Ref k="dalcorso2024" p="55, 58" paren />, so that
	everything we know of it rests on a documentary record stretching from the 1860s to the
	1990s. This chapter introduces the language and its speakers; <Xr ch="dialects-and-sources"
		>Chapter 2</Xr
	> surveys the dialects and the sources in detail.
</p>

