	<p>
		Both colonial powers reshaped Ainu life on Sakhalin in the nineteenth century. Japanese
		fisheries operated along the coasts, with Ainu working in and later running them
		<Ref k="pilsudski1912" p="X" paren />, while Russia ran the island as a penal colony; it
		was as a military physician in this colony, in 1867–1872, that Mikhail M. Dobrotvorsky
		<span lang="ru">Михаил Михайлович Добротворский</span> made the first substantial
		record of the language <Ref k="sakaguchi2021" p="43" paren />. By the Treaty of Saint
		Petersburg of 1875 the whole island passed to Russia, and 841 Ainu of the Aniwa Bay
		area were moved to Sōya in northernmost Hokkaidō and then, against their declared
		wishes, resettled by the colonial administration at Tsuishikari
		<span lang="ja">対雁</span> near Sapporo. Cholera and smallpox epidemics in 1879 and
		1886–1887 killed about half of them; survivors began returning to Sakhalin around 1890,
		and after the island’s southern half became the Japanese territory of Karafuto
		<span lang="ja">樺太</span> in 1905 nearly all of the Tsuishikari community went home
		<Ref k="sakaguchi2024" p="7–8" paren />.
	</p>
	<p>
		At the start of the Karafuto period (1905–1945) the Sakhalin Ainu numbered about 2,000,
		and nearly all of them spoke the language <Ref k="tangiku2022" paren />;
		<Ref k="pilsudski1912" p="V" /> put the entire Ainu population of Hokkaidō, Sakhalin,
		and Shikotan at about 20,000 at the same date. Japanese assimilation policy
		discouraged the use of Ainu and pressed a Japanese lifestyle on the community, and
		transmission to children broke off in the first half of the twentieth century
		<Ref k="dalcorso2024" p="57" paren />. Small, mainly Ainu hamlets nevertheless
		persisted on Karafuto until the end of the Second World War, and several hundred
		speakers are estimated to have remained in 1945 <Ref k="tangiku2022" paren />. When
		southern Sakhalin reverted to the Soviet Union after the war, almost the whole Ainu
		population, holding Japanese citizenship, was evacuated to Hokkaidō and points south by
		1948 and dispersed there; only a small number stayed behind in Russia
		<Ref k="sakaguchi2024" p="8" paren />, <Ref k="dalcorso2024" p="57" paren />.
	</p>
	<p>
		The fluent speakers known to post-war research were all evacuees living in Hokkaidō.
		The two whose speech is best documented are Fujiyama Haru
		<span lang="ja">藤山ハル</span> (1900–1974), raised in Esutoru and long resident in
		Rayciska, who was found through the dialect survey of the 1950s living at Tokoro in
		eastern Hokkaidō <Ref k="tangiku2022" paren />,
		<Ref k="sakaguchi2024" p="8" paren />, and Asai Take <span lang="ja">浅井タケ</span>
		(1902–1994) of Odasu <Ref k="tangiku2022" paren />. With Asai Take’s death in 1994 the
		language lost its last native speaker <Ref k="dalcorso2024" p="55, 58" paren />.
	</p>
</S>

<S t="Genetic affiliation and typological profile" id="typological-profile">
	<p>
		Ainu has no demonstrated relatives. Proposals linking it to “Altaic”, Japanese,
		Nivkh, and other languages have not withstood scrutiny <Ref k="shibatani1990" p="5–7"
			paren />, <Ref k="vovin1993" paren />, so Ainu is either an isolate or, given its
		considerable internal diversity, a small family of three closely related languages
		corresponding to the Hokkaidō, Sakhalin, and Northern Kuril groups
		<Ref k="janhunen2022" paren />. The position of Sakhalin Ainu as a primary branch is
		supported quantitatively: in the lexicostatistical survey conducted by Hattori Shirō
		<span lang="ja">服部四郎</span> and Chiri Mashiho <span lang="ja">知里真志保</span> in
		the 1950s, the cluster analysis of <Ref k="asai1974" p="100" /> set the Sakhalin dialects
		apart from both Hokkaidō and North Kuril as one of three top-level groups, and the
		statistical reanalysis of <Ref k="ono2020" /> yields an even sharper picture in which
		the deepest split in the family runs between the Sakhalin dialects and everything else.
		A Bayesian phylogeographic analysis of the same Hattori–Chiri wordlists — which sample the
		Hokkaidō and Sakhalin dialects but not Kuril — likewise splits them into a Sakhalin and a
		Hokkaidō subgroup and traces their spread from a homeland in northern Hokkaidō
		<Ref k="lee2013" paren />.
		Sakhalin Ainu separates from Hokkaidō Ainu by regular phonological correspondences and
		by its own grammatical retentions and innovations; <Ref k="vovin1993" /> draws on
		Sakhalin evidence, including the Nairo dialect, throughout his reconstruction of
		Proto-Ainu. The internal division of Sakhalin Ainu is treated in
		<Xr ch="dialects-and-sources">Chapter 2</Xr>.
	</p>
	<p>
		Typologically, Sakhalin Ainu is a polysynthetic, agglutinating, strongly head-marking
		language with verb-final order: intransitive clauses are SV and transitive clauses AOV
		<Ref k="dalcorso2024" p="56" paren />. Adpositions follow their complement
		(<Xr ch="postpositions">Chapter 11</Xr>), and modifiers precede their head. Personal
		prefixes and clitics on the verb index the core arguments, in a mixed alignment system
		that includes the indefinite set <i lang="ain">an-</i>, <i lang="ain">-an</i>,
		<i lang="ain">i-</i> discussed in
		<Xr ch="person-and-alignment">Chapter 13</Xr> <Ref k="dalcorso2025align" paren />.
		Nouns distinguish a plain and a possessed (“belonging”) form, and the possessive
		suffix has a wider range in Sakhalin than in Hokkaidō, extending into the
		nominalisation of verbs (<Xr ch="nouns-and-possession">Chapter 7</Xr>,
		<Xr ch="nominalization">Chapter 20</Xr>) <Ref k="bugaeva2016" paren />,
		<Ref k="tangiku2022" paren />. The phonology has five vowels with a length contrast
		and a coda system in which the stops of Hokkaidō Ainu are neutralised to
		<i lang="ain">h</i> (<Xr ch="phonology">Chapter 4</Xr>) <Ref k="tangiku2022" paren />,
		<Ref k="itabashi2001" paren />. There is no tense morphology; mood, aspect, and
		evidentiality are expressed by synthetic and analytic means
		<Ref k="dalcorso2024" p="56" paren />, among them the reportative
		<A w="manu" /> that recurs at the end of narrative sentences throughout the folktale
		corpus (<Xr ch="evidentiality">Chapter 16</Xr>) <Ref k="dalcorso2018" paren />:
	</p>
	<Ex
		m="orowa sine monimahpo neampe poniwne monimahpo nee manu"
		g="then one girl TOP be.younger girl COP REP"
		tr="and one of the girls was the younger daughter, it is said"
		cite="murasaki2001:text 19"
		dial="WS"
		place="Odasu (Asai Take)"
	/>
	<p>
		Numerals are built on a base of twenty, <A w="hoh" gl="twenty" />, with vigesimal
		counting amply attested in the nineteenth-century record; alongside it the Sakhalin
		sources show borrowed decimal bases such as <A w="tanku" gl="hundred" /> and
		<A w="wantanku" gl="thousand" />, and decimal counting gained ground over the period of
		documentation (<Xr ch="numerals">Chapter 10</Xr>) <Ref k="sakaguchi2022" paren />,
		<Ref k="murasaki2009" paren />. Negation is analytic, with preverbal negators such as
		<A w="hannehka" /> that developed within Sakhalin Ainu
		(<Xr ch="negation">Chapter 17</Xr>) <Ref k="dalcorso2021" paren />,
		<Ref k="dalcorso2025neg" paren />.
	</p>
</S>

<S t="Language contact" id="contact">
	<p>
		On Sakhalin the Ainu bordered on the Uilta (Orok), speakers of a Tungusic language,
		and the Nivkh, speakers of another isolate; the principal contact area was the Poronay
		basin and the Tarayka region in the north-east, with a second mixed zone on the
		northern west coast <Ref k="janhunen2022" paren />, <Ref k="shiraishi2022" paren />,
		<Ref k="gruzdeva1996" paren />.
		Japanese travel records of the late eighteenth and nineteenth centuries show that
		multilingualism was limited and localised rather than general: in contact situations
		around Lake Tarayka it was usually the Uilta who spoke Ainu, while command of Ainu
		among the Nivkh was restricted to particular individuals
		<Ref k="shiraishi2022" paren />.
	</p>
	<p>
		Lexical borrowing nevertheless ran in both directions. Sakhalin Ainu took over
		dog-sled vocabulary from Nivkh, including <A w="nuso" gl="dog sled" /> (also
		<i lang="ain">nuhsu</i>) from Nivkh <i>nuci</i> — an etymology going back to Chiri —
		and shows pairs such as <A w="muhru" gl="pillow" /> beside Nivkh <i>mut</i> with the
		regular <i>r</i>-for-<i>t</i> correspondence of loans between the two languages
		<Ref k="shiraishi2022" paren />. From Uilta the west-coast dialects took
		<A w="wampakka" gl="mittens" />, where the east coast uses
		<A w="matumere" /> <Ref k="shiraishi2022" paren />. Older strata of contact include
		<A w="tunakay" gl="reindeer" />, connected with the Nivkh (Ghilyak) word for the
		pulling animal, and the early Japonic loans <A w="sippo" gl="salt" /> and
		<A w="pasuy" gl="chopsticks" />, which entered Ainu as a whole well before the
		historical period <Ref k="janhunen2022" paren />. The decimal numeral bases mentioned in
		<a href="#typological-profile">§1.3</a> are likewise borrowings
		<Ref k="sakaguchi2022" p="67–68" paren />.
	</p>
	<p>
		Japanese supplied the names of trade goods that reached the Ainu from the south,
		for example <A w="hasami" gl="scissors" /> and <A w="konkaani" gl="gold, metal" />
		<Ref k="shiraishi2022" paren />, and during the Karafuto period it became the dominant
		contact language altogether; the post-war recordings of Asai Take contain frequent
		Japanese asides and code-switches around an Ainu grammatical core
		<Ref k="murasaki2001" paren />. Russian left a lighter mark on the lexicon, but a
		distinctive one on writing: pupils of the school Piłsudski ran on the east coast
		learned to write their own language in Russian characters
		<Ref k="pilsudski1912" p="XII" paren />, and Sentoku Tarōji’s Cyrillic-script letters
		of 1906 — see (1) — are the chief monument of this practice
		<Ref k="tangiku2001" paren /> (on script, see <Xr ch="orthography">Chapter 3</Xr>).
		The Russians themselves appear in the letters under the Ainu name
		<A w="nuca" gl="Russian" />.
	</p>
</S>

<S t="Endangerment and present-day status" id="status">
	<p>
		Sakhalin Ainu is today a dormant language: natural transmission ended in the first
		half of the twentieth century, and no native speaker has lived since 1994
		<Ref k="dalcorso2024" p="55, 57" paren />, <Ref k="janhunen2022" paren />; the UNESCO
			<i>Atlas of the World’s Languages in Danger</i> accordingly records Sakhalin Ainu
			among the languages that have become extinct since they were linguistically described
			<Ref k="unesco2010" paren />. It is not,
		however, an unused one. Descendants of the Sakhalin Ainu in Japan study and revive the
		language under the name <i lang="ain">Enciw itah</i>, with the scholar and activist
		Kitahara Jirōta Mokottunas <span lang="ja">北原次郎太</span> among those leading the
		effort <Ref k="dalcorso2024" p="55" paren />, and teaching materials now exist,
		including the conversation course of <Ref k="murasaki2025" />. The recorded legacy is
		substantial: Piłsudski’s wax-cylinder recordings of the Ainu, made in 1902–1903, have been partly
		restored from the 1980s onward by a laser-beam reflection method developed for the purpose <Ref k="iwai1987" paren />, <Ref k="dalcorso2024" p="59" paren />, and the
		post-war tape recordings of Fujiyama Haru and Asai Take were published with audio
		<Ref k="murasaki2001" paren />, <Ref k="tangiku2022" paren />.
	</p>
	<p>
		This grammar is one part of that documentation-based revival. It is published beside a
		dictionary of Sakhalin Ainu on this site, and every Ainu word and morpheme in the
		examples is linked to the dictionary so that the attested record, rather than any
		modern reconstruction, remains the point of reference throughout. The sources on which
		the description rests are set out in <Xr ch="dialects-and-sources">Chapter 2</Xr>.
	</p>
</S>
