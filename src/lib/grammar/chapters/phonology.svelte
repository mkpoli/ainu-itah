<S t="Prosody" id="prosody">
	<p>
		Sakhalin Ainu has no distinctive pitch accent. Hattori and Murasaki agree that the
		West-coast dialects oppose no word pairs by pitch; what one hears is a predictable
		prominence pattern — the first syllable is high if it is heavy (a long vowel or a
		closed syllable), otherwise the second syllable is high:
		<i lang="ain">héekopo</i> ‘younger sister’, <i lang="ain">káhkemah</i> ‘lady’,
		<i lang="ain">cikáh</i> ‘bird’, <i lang="ain">sapá</i> ‘head’
		(<Ref k="hattori1967" paren />; <Ref k="murasaki1979" p="5–6" paren />;
		<Ref k="tangiku2022" p="§2.5" paren />). Murasaki describes the system in moraic
		terms — main prominence on the second mora, with secondary beats on later
		even-numbered moras, shifted leftward when the expected mora is the second half of a
		long vowel or a coda (as summarised in <Ref k="itabashi2001" p="90" paren />). The
		contrastive work done by pitch in Hokkaidō is thus carried in Sakhalin by vowel
		length (§4.3). Chiri's description, by contrast, sets up two lexical accent classes
		(peak on the first or on the second syllable) and distinguishes derivation from
		compounding by accent placement <Ref k="chiri1942" p="§§9–12" />;
		<Ref k="tangiku1998" p="§2-3" /> takes this to reflect his East-coast material, whose
		prosody was never instrumentally recorded. We treat the question of an East-coast
		accent contrast as open; the Hokkaidō correspondences (§4.3) predict that if any
		dialect kept pitch distinctive it would show up precisely in pairs like
		<i lang="ain">niina</i> ‘crush’ ~ ‘gather firewood’, and Piłsudski's accent marks,
		which Dal Corso leaves uninterpreted, are the only place to test it.
	</p>
