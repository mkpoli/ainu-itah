<p>
	Sakhalin Ainu has a compact segmental inventory — eleven consonants and five vowel
	qualities — combined with two properties that together give the variety its
	characteristic sound: a contrast of vowel length, and a strict limit on syllable codas
	that converts the stops <i lang="ain">*p *t *k</i> (and often <i lang="ain">*r</i>) into
	<i lang="ain">h</i> at the end of a syllable. The first systematic description is
	<Ref k="chiri1942" p="§§1–20" />, written largely on the basis of East-coast speech;
	the West-coast dialects of Rayciska and Odasu were analysed phonologically by Hattori
	<Ref k="hattori1967" paren /> and <Ref k="murasaki1979" p="1–10" paren />, and the
	East-coast dialects recorded by <Ref k="pilsudski1912" p="1–9" /> in 1903–04 have been
	reassessed by <Ref k="dalcorso2024" p="59–66" />. On vowel length and its history see
	<Ref k="itabashi2001" />. Examples are given in the romanisation defined in
	<Xr ch="orthography">Chapter 3</Xr>; phonetic detail is added in square brackets where
	the sources supply it.
</p>

