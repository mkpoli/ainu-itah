			route as a West-coast extension of debuccalisation from the obstruents to
			<i lang="ain">r</i>: having converted <i lang="ain">*p *t *k</i> to
			<i lang="ain">h</i>, the western varieties drew the one remaining eligible coda
			into the same lenition, while the East keeps <i lang="ain">r</i> distinct by
			epenthesis. Even in the west the two forms coexist word by word
			(<i lang="ain">utara</i> and <i lang="ain">utah</i> are both current), so the
			residue is lexical variation rather than rule.
