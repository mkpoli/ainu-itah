	<S t="Obstruents and the absence of a voicing contrast" id="obstruents">
		<p>
			Voicing is not contrastive anywhere in the system. <Ref k="chiri1942" p="§2(1)" />
			states flatly that no pair of words is distinguished by voicing: the plosives and the
			affricate are voiceless in careful speech, but voiced variants [b d ɡ dʑ] appear
			freely after nasals, as in his <i lang="ain">membe</i> ‘grandmother’,
			<i lang="ain">unji</i> for <A w="unci" gl="fire" /> and <i lang="ain">henge</i> for
			<A w="henke" gl="grandfather" />. Piłsudski heard the same instability on the East
			coast: between vowels and after <i lang="ain">y</i> the plosives “waver” between the
			voiceless and voiced series, which <Ref k="dalcorso2024" p="61" /> renders as
			partially voiced [p̬ t̬ k̬] (e.g. <i lang="ain">pájgara</i> for
			<A w="paykara" gl="spring" />). The two coasts are reported to differ in degree:
			Piłsudski found voicing commoner in the west, Chiri likewise heard more voiced
			tokens on the West coast, while in Murasaki's West-coast recordings of the 1960s
			voicing of <i lang="ain">p</i> is unattested and even <i lang="ain">t k</i> are
			rarely voiced (<Ref k="dalcorso2024" p="61" paren />). Dal Corso suggests contact
			with Japanese as one reason for the retreat of voicing in the later data.
		</p>
		<p>
			The affricate <i lang="ain">c</i> is alveolo-palatal [tɕ]: Piłsudski insists that it
			is neither [ts] nor English-type [tʃ] but “akin to the sound of the Polish ć”
			(<Ref k="pilsudski1912" p="7" paren />; <Ref k="dalcorso2024" p="63–64" paren />).
			After nasals it has the voiced variant [dʑ], as in <i lang="ain">únʑi</i> for
			<A w="unci" gl="fire" />. A regular source of <i lang="ain">c</i> is /t/ before
			<i lang="ain">i</i>, where <i lang="ain">t</i> shifts to <i lang="ain">c</i> without
			exception, e.g. <i lang="ain">kuci</i> ‘his belt’ from <i lang="ain">kut-i</i>
			(<Ref k="chiri1942" p="§16,6" paren />); this feeds the coda alternations described
			in <Xr ch="morphophonology" s="coda-alternations">Chapter 5</Xr>. In the East-coast
			dialects of Ay and Tunayci, <i lang="ain">p</i> and <i lang="ain">k</i> (but not
			<i lang="ain">t</i>) are palatalised before <i lang="ain">e</i> [ɛ], e.g.
			<i lang="ain">emújḱe</i> for <A w="emuyke" gl="all" />, <i lang="ain">tamb́e</i> for
			<A w="tanpe" gl="this thing" /> <Ref k="dalcorso2024" p="61–62" paren />; nothing
			comparable is recorded for the West coast.
		</p>
		<p>
			The sibilant <i lang="ain">s</i> varies between [s] and [ɕ]. Before and after
			<i lang="ain">i</i>, and generally in coda position, it is [ɕ] on both coasts
			(<i lang="ain">śine</i> for <A w="sine" gl="one" />, <i lang="ain">iśo</i> for
			<A w="iso" gl="bear" />); elsewhere word-initially the two sounds vary freely,
			Piłsudski recording both <i lang="ain">suj</i> and <i lang="ain">śuj</i> for
			<A w="suy" gl="again" /> (<Ref k="pilsudski1912" p="5" paren />;
			<Ref k="dalcorso2024" p="62–63" paren />). Chiri adds that plain [s] is the usual
			West-coast value, [ɕ] being obligatory only next to <i lang="ain">i</i> and frequent
			in codas <Ref k="chiri1942" p="§2(2)" paren />.
		</p>
		<p>
			The glottal fricative <i lang="ain">h</i> occurs both as onset and — its most
			distinctive role — as coda. As a coda it is a fricative whose place is determined by
			the preceding vowel: velar [x] after <i lang="ain">a o e</i> (<i lang="ain">mah</i>
			[max] ‘woman’), palatal [ç] after <i lang="ain">i</i>, and labialised, near [ɸ],
			after <i lang="ain">u</i> (<i lang="ain">uh</i> [uɸ] ‘take’); Chiri, who writes this
			segment <i lang="ain">x</i> and counts it a separate phoneme, notes that
			utterance-finally it is so weak as to escape notice <Ref k="chiri1942" p="§2(3)" />.
			We follow Hattori and Murasaki in treating onset [h] and the coda fricatives as one
			phoneme /h/. As onset before or after <i lang="ain">u</i>, /h/ is [ɸ]
			systematically on the West coast but only variably in Piłsudski's data
			(<i lang="ain">fura</i> beside <i lang="ain">húxkara</i>), and he also heard voiced
			[ɦ] between vowels (<Ref k="dalcorso2024" p="63" paren />).
		</p>
	</S>

	<S t="The tap r" id="liquid">
		<p>
			The single liquid is a tap [ɾ] between vowels. Word-initially, and after
			<i lang="ain">n</i>, Piłsudski describes an articulation made “simultaneously” with
			a <i lang="ain">t</i> placed further back than usual, pointing to a retroflex [ʈ] or
			affricated [ʈɽ], with much variation between speakers
			(<Ref k="pilsudski1912" p="8–9" paren />; <Ref k="dalcorso2024" p="64" paren />).
			The hardening is lexicalised in part of the Sakhalin area: <i lang="ain">tetara</i>
			for <A w="retara" gl="be white" /> is widespread, and the northern East-coast
			dialect of Nairo turns initial <i lang="ain">r</i> into <i lang="ain">t</i> in a
			whole set of words (<i lang="ain">tup</i> for <i lang="ain">ruh</i> ‘ice’)
			<Ref k="tangiku2022" p="§2.6" paren />. In codas <i lang="ain">r</i> does not occur:
			where Hokkaidō has syllable-final <i lang="ain">r</i>, Sakhalin shows either an
			echo vowel or <i lang="ain">h</i> (§<Xr ch="phonology" s="coda-r">4.4.4</Xr>).
		</p>
	</S>

	<S t="Nasals and approximants" id="nasals">
		<p>
			The nasals are <i lang="ain">m</i> and <i lang="ain">n</i>. Coda <i lang="ain">m</i>
			survives only in careful speech: <Ref k="chiri1942" p="§16,2" /> observes that
			word-final <i lang="ain">m</i> is normally pronounced <i lang="ain">n</i>
			(<i lang="ain">isan</i> for <A w="isam" gl="not exist" />, <i lang="ain">aman</i>
			for <A w="amam" gl="grain" />), and that before <i lang="ain">k t n</i> the shift is
			categorical (<i lang="ain">enko</i> ‘half’, <i lang="ain">kinta</i> ‘in the
			mountains’). Murasaki's corpus bears this out: Asai Take's tales have
			<i lang="ain">isan teh an</i> beside slower <i lang="ain">isam</i>. Conversely
			<i lang="ain">n</i> assimilates to following labials and glides
			(<i lang="ain">pompe</i> from <i lang="ain">pon-pe</i> ‘little thing’;
			<Ref k="chiri1942" p="§16,3" paren />); the fuller sandhi picture is taken up in
			<Xr ch="morphophonology" s="plural-sandhi">Chapter 5</Xr>. Piłsudski additionally
			records palatal [ɲ] for /n/ before <i lang="ain">e</i> and word-finally
			(<i lang="ain">etunńe</i> ‘not want’), and a rare [ŋ]
			<Ref k="dalcorso2024" p="62" paren />.
		</p>
		<p>
			The approximants are <i lang="ain">w</i> and <i lang="ain">y</i>. In the East-coast
			corpus /w/ is regularly written <i lang="ain">v</i>, a labiodental value Piłsudski
			reports for all the dialects he documented; Dal Corso treats [v] as an East-coast
			allophone of /w/ rather than a separate phoneme
			<Ref k="dalcorso2024" p="63" paren />. Syllable-finally the two approximants form
			the falling diphthongs <i lang="ain">ay uy ey oy</i> and
			<i lang="ain">aw iw ew ow</i>; that these offglides count as consonants, not as
			vowel sequences, is shown by morphology — stems in <i lang="ain">-y -w</i> select
			the consonant-stem allomorphs of suffixes, e.g. nominalising <i lang="ain">-pe</i>
			rather than <i lang="ain">-h</i> (<i lang="ain">ray-pe</i> ‘dead one’), a point
			already made as a “morphophoneme” argument by <Ref k="chiri1942" p="§2(5)" />.
		</p>
	</S>

	<S t="The glottal stop" id="glottal-stop">
		<p>
			Every vowel-initial word begins phonetically with a glottal stop, strongest in
			accented syllables; Chiri regards it as fully predictable and therefore not a
			phoneme <Ref k="chiri1942" p="§1(3)" />. Hattori's phonemicisation of Rayciska
			includes /ʔ/ in the consonant chart but bars it from codas (reported in
			<Ref k="tangiku1998" p="§2-1" paren />), and Dal Corso admits it for West Sakhalin
			only “provisionally, in free variation” <Ref k="dalcorso2024" p="59–60" paren />.
			Piłsudski never marks it, which may reflect his self-confessed “Slav ear” rather
			than its absence in the East <Ref k="dalcorso2024" p="60" paren />. We write no
			glottal stop: word-initially it is automatic, and word-internally its only role is
			to keep adjacent vowels apart at morpheme boundaries (see
			<Xr ch="morphophonology" s="prefix-sandhi">Chapter 5</Xr>). Murasaki's apostrophes
			(<i lang="ain">'oman</i>, <i lang="ain">'e'ani</i>) are accordingly not reproduced
			in our examples.
		</p>
