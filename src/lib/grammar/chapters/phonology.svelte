	<S t="Coda r: echo vowel and debuccalisation" id="coda-r">
		<p>
			Coda <i lang="ain">r</i> patterns apart from the stops. Chiri gives two
			resolutions for syllable-final <i lang="ain">*r</i>: either the preceding vowel
			is copied, opening the syllable — <i lang="ain">utara</i> ‘people’,
			<i lang="ain">kisara</i> ‘ear’, <A w="mukara" gl="axe" />,
			<A w="koro" gl="have" /> against Hokkaidō <i lang="ain">utar, kisar, mukar,
			kor</i> — or the <i lang="ain">r</i> debuccalises like a stop, word-finally in
			<i lang="ain">utah</i> and before a consonant in <A w="unahpe" gl="aunt" />
			(Hokkaidō <i lang="ain">unarpe</i>) <Ref k="chiri1942" p="§16,4" paren />. He
			presents the routes as alternatives, citing <i lang="ain">utara</i> and
			<i lang="ain">utax</i> from the very same page of Piłsudski's
			<i>Materials</i> — so no purely segmental conditioning can decide between them.
		</p>
		<p>
			The corpus shows the choice to be in large part dialectal. In the East-coast
			texts the echo vowel is all but categorical. In the tale Piłsudski took down at
			Takoye from the nineteen-year-old Kusurikoya, <i lang="ain">utara</i> recurs from
			one end to the other — <i lang="ain">onneru utara</i> ‘the old people’,
			<i lang="ain">mahnekuh utara</i> ‘the women’, <i lang="ain">ohkayo utara</i>
			‘the men’ — and debuccalised <i lang="ain">utah</i> never appears; Sakaguchi,
			surveying the whole East-coast folktale corpus, states the generalisation
			directly: the variants <i lang="ain">uta ~ utah</i> are West-coast forms, his
			eastern materials showing almost exclusively <i lang="ain">utara</i>
			(<Ref k="sakaguchi2020utara" p="187" paren />;
			<Ref k="sakaguchi2020tuyta" paren />). The <i lang="ain">h</i>-forms cluster in
