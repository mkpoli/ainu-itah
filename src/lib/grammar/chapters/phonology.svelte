		<p>
			The glottal stop of §4.1.4 belongs in part to prosody. Chiri's formulation is
			prosodic from the outset — the onset appears on the initial vowel of an
			<em>accented</em> syllable, and is “especially conspicuous” in Sakhalin
			<Ref k="chiri1942" p="§1(3)" /> — and the West-coast orthographic tradition
			tracks the same conditioning: Murasaki's apostrophes sit precisely on
			accent-bearing vowel-initial morphemes (<i lang="ain">'itah</i>,
			<i lang="ain">'oman</i>, and word-internally at junctures such as
			<i lang="ain">'e'ani</i>), not on every orthographic vowel. For the East coast,
			where Piłsudski wrote nothing, the segment betrays itself indirectly, through
			sandhi. A word-final stop debuccalises only when no vowel follows; before a
			genuinely vowel-initial word it should resyllabify as an onset and keep its stop
			value. At the juncture <i lang="ain">nah an</i> ‘being so’, however, Piłsudski
			hears [nax án]: the coda of <i lang="ain">nah</i> is treated as pre-consonantal,
			which means the accented <i lang="ain">an</i> must begin with a consonant — the
			glottal onset <Ref k="dalcorso2024" p="61" paren />. The accent-linked glottal
			stop is thus recoverable for the East from its phonological effect even though
			no transcriber recorded it directly.
