	<p>
		The five qualities <i lang="ain">a e i o u</i> are constant across all Ainu varieties
		<Ref k="chiri1942" p="§1" paren />. The back vowel <i lang="ain">u</i> is only weakly
		rounded and approaches [o], which is why early hearers wrote
		<i lang="ain">ayno</i> for <A w="aynu" /> and <i lang="ain">inao</i> for
		<A w="inaw" gl="ritual shaving" /> <Ref k="chiri1942" p="§1(1)" paren />. The mid
		vowels differ by coast: West Sakhalin <i lang="ain">e o</i> are close-mid [e o],
		while Piłsudski describes open-mid [ɛ ɔ] for the East coast
		(<Ref k="pilsudski1912" p="1" paren />; <Ref k="dalcorso2024" p="60" paren />). In
		unaccented syllables the mid and high vowels approach one another — Piłsudski's
		<i lang="ain">e o</i> “narrow” and his <i lang="ain">i u</i> “open”, hence frequent
		interchange of <i lang="ain">e~i</i> and <i lang="ain">o~u</i> in the Ay and Tunayci
		texts and in Chiri's material (<Ref k="chiri1942" p="§19,1" paren />). A more exotic
		East-coast allophone is [ɨ] for /e/ in the causative <i lang="ain">-te</i> after a
		voiced nasal-stop cluster, as in <i lang="ain">ćivéndy</i> for
		<i lang="ain">ciwen-te</i> ‘was destroyed’, a sound Piłsudski compares to Russian
		<span lang="ru">ы</span> <Ref k="dalcorso2024" p="60" paren />. Unaccented
		<i lang="ain">i</i> between voiceless consonants devoices, in Sakhalin conspicuously
		so <Ref k="chiri1942" p="§1(4)" paren />. Finally, where Hokkaidō has word-final
		<i lang="ain">-e</i> several Sakhalin words show <i lang="ain">-i</i>:
		<A w="tani" gl="now" />, <A w="poni" gl="bone" /> (Hokkaidō <i lang="ain">tane</i>,
		<i lang="ain">pone</i>) <Ref k="tangiku2022" p="§2.1" paren />.
	</p>
</S>

<S t="Vowel length" id="length">
	<p>
		Length is the hallmark of Sakhalin Ainu vocalism. Hattori established for Rayciska
		that long and short vowels contrast (<Ref k="hattori1967" paren />), and Murasaki's
		grammar and text collections write the contrast consistently
		(<Ref k="murasaki1979" paren />; <Ref k="murasaki1976" paren />). Chiri, working
		mainly from East-coast speech, had denied any contrast, treating long vowels as a
		by-product of accent <Ref k="chiri1942" p="§1(2)" />; <Ref k="tangiku1998" p="§2-3" />
		attributes the disagreement to the difference between the two dialect groups, the
		East-coast prosody never having been adequately recorded. For the West coast at least
		the contrast is real, though its functional load is modest; some pairs follow.
	</p>
