	<S t="Pitch, length, and Proto-Ainu" id="prosody-diachrony">
		<p>
			Set against Hokkaidō, the Sakhalin system looks like one half of an old
			inheritance. The correspondences of §4.3 — Hokkaidō pitch matching Sakhalin
			length in the regular cases, with residues like <i lang="ain">niná</i> :
			<i lang="ain">niina</i> ‘crush’ that no single-feature account derives — point,
			with <Ref k="vovin1993" paren /> and <Ref k="itabashi2001" p="95–98" paren />, to
			a proto-language in which vowel length and pitch were independent distinctive
			features (against Hattori's length-only reconstruction, §4.3). Each daughter
			branch then kept one feature and made the other predictable. Hokkaidō
			phonemicised pitch, length surviving only as a phonetic correlate of the accented
			open syllable; Sakhalin phonemicised length, pitch surviving only as the
			automatic second-mora contour described above. The two systems carry the same
			old prominence by different means, which is why the cognate sets translate so
			regularly between an accent mark on one side of the strait and a doubled vowel
			on the other.
		</p>
		<p>
			Kuril Ainu can be placed in this scheme only tentatively. Its record is confined
			to early wordlists — Krasheninnikov, Voznesenskii, Dybowski, Torii — which are
			fragmentary and mutually inconsistent on quantity: ‘two’ appears as
			<i lang="ain">tuup ~ tuuh ~ tubic</i> across collectors. From this material
			<Ref k="itabashi2001" p="90–91" /> nonetheless argues that Kuril had a pitch
			accent of the Hokkaidō type, a distinctive low-to-high rise, while the irregular
			long-vowel spellings leave open whether length was also contrastive. If that is
			right, the length-for-pitch reanalysis is a specifically Sakhalin innovation, not
			a shared northern development — and the pieces described in this chapter, the
			length contrast (§4.3), the bimoraic minimum (§4.4.1) and the accent-conditioned
			glottal onset, form a single prosodic complex organised around quantity.
