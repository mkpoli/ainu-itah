	<p>
		Historically, Sakhalin length corresponds in large measure to the pitch accent of the
		Hokkaidō dialects: where a Hokkaidō word has an irregular high accent on an open
		first syllable, the Sakhalin cognate has a long vowel there — <i lang="ain">ká</i> :
		<i lang="ain">kaa</i> ‘thread’, <i lang="ain">hése</i> : <i lang="ain">heese</i>
		‘breathe’, <i lang="ain">réra</i> : <i lang="ain">reera</i> ‘wind’,
		<i lang="ain">túnas</i> : <i lang="ain">tuunas</i> ‘be early’
		(<Ref k="itabashi2001" p="93–95" paren />; <Ref k="hattori1964" paren />). On this
		basis Hattori reconstructed Proto-Ainu with distinctive length and no accent,
		Sakhalin being conservative <Ref k="hattori1967" paren />. The correspondence is not
		one-to-one, however: <Ref k="itabashi2001" p="95" /> lists pairs such as Hokkaidō
		<i lang="ain">niná</i> ‘crush’ (accent on the second syllable) against Sakhalin
		<i lang="ain">niina</i>, or <i lang="ain">nociw</i> : <i lang="ain">noociw</i>
		‘star’, and concludes — with <Ref k="vovin1993" /> — that the proto-language needed
		both length and pitch as independent features. The opposite position, that Sakhalin
		length is a secondary development from accent, is argued by Satō on the strength of
		the Japanese loan <A w="tuuki" gl="lacquered cup" /> (long <i lang="ain">uu</i>
		matching the irregular accent of Hokkaidō <i lang="ain">túki</i>, though the Japanese
		source vowel is short) and of alternations like <A w="nii" gl="tree" /> beside the
		compound <i lang="ain">ah-ni</i> ‘elm’ <Ref k="bugaeva2022" paren />. The debate does
		not affect the synchronic statement: in the recorded West-coast language, length
		distinguishes morphemes and must be written.
	</p>
</S>

<S t="Syllable structure and phonotactics" id="syllable">
	<p>
		The syllable is (C)V(V)(C). Onsets contain at most one consonant — there are no
		clusters and no onset glide sequences of the Japanese <i lang="ja">きゃ</i> type —
		and an apparently onsetless syllable receives a glottal onset
		(<Ref k="chiri1942" p="§§3–5" paren />; <Ref k="itabashi2001" p="89" paren />). The
		nucleus is a short or long vowel; long vowels occur freely in open syllables but a
		coda forces a short nucleus, so that possessive suffixation shortens a long stem
		vowel (<i lang="ain">imii</i> → <i lang="ain">imi-yehe</i> ‘garment’,
		<Xr ch="morphophonology" s="poss-vowel-copy">Chapter 5</Xr>). Word-internal
		consonant sequences are exhausted by coda + onset: <i lang="ain">ahkas</i> ‘walk’,
		<i lang="ain">tehkup</i> ‘wing’, <i lang="ain">muysankeh</i> ‘broom’. Sequences of
		unlike vowels within a morpheme are rare, and at morpheme boundaries hiatus is
		repaired by glide or <i lang="ain">h</i> insertion
		(<Xr ch="morphophonology" s="prefix-sandhi">Chapter 5</Xr>).
	</p>

	<S t="Syllable types and the bimoraic minimum" id="syllable-types">
		<p>
			Five syllable shapes occur: V, CV, VC, CVC, and — the shape that sets Sakhalin
			apart from Hokkaidō — CVV with a long nucleus
			(<Ref k="itabashi2001" p="89" paren />). The two vowel-initial types are
			restricted: in native simplex words V and VC syllables stand only word-initially,
			and where word formation would create one elsewhere it is absorbed into the
			preceding syllable (<i lang="ain">sik-o</i> ‘eye attaches’ &gt;
			<i lang="ain">siko</i> ‘be born’, <i lang="ain">rup-us</i> ‘ice attaches’ &gt;
			<i lang="ain">rupus</i> ‘freeze’) or removed by haplology or vowel deletion
			(<i lang="ain">kera-an</i> &gt; <i lang="ain">keran</i> ‘be tasty’). Chiri
			accordingly reduces the basic templates to CV and CVC, with the onsetless types as
			word-initial variants carrying a zero onset <Ref k="chiri1942" p="§5" paren />.
		</p>
		<p>
			Following Murasaki's moraic description, <Ref k="itabashi2001" p="89" /> treats
			Sakhalin as a mora-counting language: a CV sequence, a bare V, and a coda
			consonant each contribute one mora, and the minimal accent-bearing free form is
			two moras. This bimoraic minimum is what lies behind the lengthening of open
			monosyllables noted in §4.3: a content word of the bare shape (C)V would be a
			single mora, too light to carry the accent it requires, and so surfaces as CVV —
			<A w="nii" gl="tree" />, <A w="kuu" gl="bow" />, <A w="too" gl="day, lake" /> —
			while clitics and affixes, which are unaccented, escape the requirement and stay
			short. The same arithmetic excludes long vowels from closed syllables: CVVC would
			run to three moras, and the language repairs it by shortening
			(<i lang="ain">imii</i> → <i lang="ain">imi-yehe</i> above).
		</p>
	</S>

