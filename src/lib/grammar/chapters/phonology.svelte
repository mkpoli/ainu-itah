			The coda is where Sakhalin phonotactics departs most sharply from Hokkaidō.
			Hokkaidō Ainu closes syllables with any of nine consonants,
			<i lang="ain">p t k s r m n w y</i>; Sakhalin permits only six,
			<i lang="ain">s n m w y h</i>, with coda <i lang="ain">m</i> marginal (§4.1.3) —
