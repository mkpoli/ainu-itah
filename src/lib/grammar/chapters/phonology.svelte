	<p>
		Two phonetic regularities interact with the contrast. Monosyllabic content words of
		the shape (C)V are always long — <A w="nii" gl="tree" />, <A w="kuu" gl="bow" />,
		<A w="ree" gl="name" />, <A w="too" gl="day, lake" />, <A w="poo" gl="child" /> —
		whereas clitics and affixes of the same shape are short
		(<Ref k="chiri1942" p="§1(2)" paren />; <Ref k="itabashi2001" p="89" paren />). And
		some of these monosyllables shorten under suffixation, e.g. <A w="kii" gl="do" /> but
		<i lang="ain">ki-hci</i> ‘they do’, which leads <Ref k="tangiku2022" p="§2.1" /> to
		analyse the vowel of such roots as phonemically short with surface lengthening; the
		long vowels of polysyllables (<A w="heese" gl="breathe" />,
		<A w="niina" gl="gather firewood" />) do not alternate and must be underlying.
	</p>
